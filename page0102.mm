<p>The funny thing
about running with Lola was that I nearly dropped her. Not funny
ha-ha. Dropping Lola while trying to escape a dimly-lit subterranean
parking from Better Future security personnel, that would not have
been funny. That, after all the effort I&rsquo;d expended to reach
this point, would have been incredibly frustrating. Also possibly
fatal. I mean I didn&rsquo;t realize how quickly I&rsquo;d adapted to
moving around in the Contours&mdash;how to lean with them as they
moved, how a tiny click near the hip meant they were about to
jag&mdash;until I tried to do it with a hundred ten-pound woman in my
arms.</p>
<p>The Contours bolted
out of the elevator. Lola yelped and slipped out of my grasp, sliding
around my side. Her fingers clawed at me. I managed to get hold of
her, then the Contours sidestepped a guard I hadn&rsquo;t even seen
and Lola slipped all the way around to my back. Her legs locked
around my waist. Her arms wrapped my neck in a choke hold. My eyes
watered. There was a loud noise like metal striking concrete and I
had no idea what the hell was going on. Then we burst into sunlight
and Lola bounced around to my front again. The top of her skull
popped me in the nose. As we hit the road, we clung to each other
like lovers, lovers who were very angry with each other.</p>
<p><em>Safe</em>,
I willed the Contours. <em>Take me somewhere safe</em>.</p>
