<p>My doctor didn&rsquo;t want to let me out of the hospital, which was
funny, because the nurses couldn&rsquo;t be rid of me fast enough.
They argued outside my room in low, strained voices. &ldquo;There&rsquo;s
nothing wrong with him,&rdquo; a nurse said. &ldquo;We need that
bed.&rdquo;</p>
<p>&ldquo;I still need a psych evaluation,&rdquo; said the doctor. &ldquo;He&rsquo;s
on suicide watch, why don&rsquo;t I have something from psych?&rdquo;</p>
<p>&ldquo;His company won&rsquo;t consent to a psychological profile.
They say his state of mind is commercial-in-confidence.&rdquo;</p>
<p>&ldquo;Then who put him on the watch?&rdquo;</p>
<p>&ldquo;His company. They specifically requested no visitors. And they
want him released into their care right away.&rdquo;</p>
<p>The doctor sighed, like she spent all day being thwarted by
bureaucrats, and here it was again. &ldquo;He&rsquo;s clearly
unstable.&rdquo;</p>
<p>I could hear the nurse&rsquo;s shrug. &ldquo;He&rsquo;s physically
fine. What do I tell them?&rdquo;</p>
<p>The doctor sighed again. She was going to go home after this and sip
red wine and stare at the wall, I could tell. She would wonder why
she was doing this, struggling against commercial interests at a
corporate hospital when all she wanted to do was help people, and in
the morning, when she walked out of her beautiful home and unlocked
her sports car, she would remember.</p>
<p>&ldquo;Tell them,&rdquo; she said, &ldquo;I strongly advise he be
kept away from industrial-grade cutting or stamping equipment.&rdquo;</p>
