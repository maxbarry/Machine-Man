<p>I stumbled out into
the corridor, calling for help. At least, that&rsquo;s what it felt
like in my head. In reality, each step I took was machine-smooth and
solid as steel. My hooves punched into the tiles and gripped. There
was probably a setting to make them stop doing that, a command like
<em>imagine flexing your left big toe three times</em> so I wouldn&rsquo;t
tear up the floor when indoors, but I didn&rsquo;t know it. I had no
user documentation.</p>
<p>&ldquo;Help,&rdquo;
I croaked. This was the biological part of me, failing. Smoke stung
my eyes. My thumb-and-forefinger grip slipped from my artery, and
when I tried to pinch it closed again, it hurt. The anaesthetic was
fading. Where were those drugs Cassandra Cautery had mentioned, the
ones that would make me <em>functional pretty much immediately</em>? I
needed those. Not as badly as the man in green scrubs needed a
doctor, but still. I wondered if there had been some surgical clamps
back there, maybe in that overturned trolley. I should have looked
for those before going out for help. I could pass out and that guy
would die waiting. Would he understand if I went back and hunted
around for clamps, or would that be really offensive and selfish?</p>
<p>This
light-headedness was becoming a problem.</p>
<p>These Contours felt
different. I couldn&rsquo;t put my finger on it, ha ha. But
definitely different. It wasn&rsquo;t something in particular. It was
everything. Did that make sense? No. It didn&rsquo;t. I glanced back
(<em>swzzzz</em>, whispered my torso) and saw a lot of blood in the
water. That was not good. &ldquo;Blood,&rdquo; I said, for no reason.</p>
<p>A man appeared in
the corridor ahead of me. It was moments before I recognized him:
Jason, my lab assistant. &ldquo;He&rsquo;s here!&rdquo; Jason
shouted. &ldquo;He&rsquo;s alive! Quickly!&rdquo; He gestured to
people I couldn&rsquo;t see. I had never been particularly fond of
Jason before, but in this moment, I loved him very much.</p>
