<h5>CHAPTER  SEVEN</h5>
<p>Carl opened the limo door and I emerged into bright sunshine to two
dozen Better Future employees lining the path to the lobby. When they
saw me, they began to clap. Carl lifted me into a gleaming
wheelchair; there were cheers. As Carl pushed me past the crowd, they
put their hands out for high-fives. I didn&rsquo;t understand what
was happening.</p>
<p>Inside the lobby, Cassandra Cautery waited with folded arms and
gleaming eyes. &ldquo;Welcome back.&rdquo; Her voice was choked with
emotion, as if I were her son returned from a first semester at
college.</p>
<p>&ldquo;Thanks,&rdquo; I said.</p>
<p>She trotted alongside me as Carl steered me through security. The
steel wheelchair didn&rsquo;t set off any alarms, which was
interesting. Or maybe they did, but the guards were under instruction
to ignore it. &ldquo;I&rsquo;ve taken the liberty of expanding your
staff,&rdquo; she said. &ldquo;What do you think of that Jason Huong?
I left him, in case he&rsquo;s a favorite, but really, you could do
better.&rdquo;</p>
<p>&ldquo;Jason&rsquo;s okay.&rdquo;</p>
<p>She put a hand on my shoulder. Carl took the hint and stopped
pushing. Cassandra Cautery looked deep into my eyes. She was so
short, we were almost level. &ldquo;It would be no problem to get rid
of him. Permanently.&rdquo;</p>
<p>&ldquo;Jason&rsquo;s fine,&rdquo; I said.</p>
<p>She nodded. She was still holding my shoulder. &ldquo;I just don&rsquo;t
want anything to get in your way.&rdquo;</p>
<p>&ldquo;In the way of building prosthetics, you mean.&rdquo; I felt it
was important to clarify.</p>
<p>Cassandra Cautery&rsquo;s hand tightened on my shoulder. &ldquo;Of
anything,&rdquo; she said, &ldquo;that your brilliant, brilliant mind
might feel the urge to do.&rdquo;</p>
