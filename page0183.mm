<p>The woman&rsquo;s
eyes flicked at someone or something behind me. She was a little
close for comfort, this woman. I was glad to be awake and hopefully
safe but I wished she would back up a little out of my personal
space. When her eyes returned to mine, they shone even more brightly
than before. For a second I thought we were about to kiss. She turned
to the people I couldn&rsquo;t see. &ldquo;A response. A clear
response.&rdquo;</p>
<p><em>Where am I? </em>I
