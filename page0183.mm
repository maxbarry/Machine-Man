<p>&ldquo;Online,&rdquo;
said the first voice. The word sounded heavy, the way you might say:
<em>We made it</em>. &ldquo;Online.&rdquo;</p>
<p>I tried to blink.
This didn&rsquo;t work. I could see, though. I could see a vase. The
vase was clear and contained three yellow flowers. Daffodils? Yes.
The vase with the daffodils sat on a plain white surface, and behind
that was a wall with a painting. It seemed to me that this white
surface was probably a bedside table, so tried to turn my head to see
the rest of the room. But this didn&rsquo;t work, either.</p>
<p>A face appeared
beside the vase. It was a friendly face, somewhat familiar. Perhaps a
woman I had worked with one time. From behind brown-rimmed glasses,
her eyes shone. &ldquo;Dr. Neumann?&rdquo;</p>
<p>I swallowed, or
tried to. I said: <em>What..?</em></p>
<p>The woman&rsquo;s
eyes flicked at something or someone behind me. She was a little
close for comfort, this woman. I was glad to be awake and hopefully
safe but I wished she would back up out of my personal space. When
her eyes returned to mine, she was even happier than before. For a
second I thought we were about to kiss. Instead, she turned to the
people I couldn&rsquo;t see. &ldquo;A response. That&rsquo;s a clear
response.&rdquo;</p>
<p><em>Where am I? </em>I
tried to say, but again, I made no sound. I felt panic, because when
no part of your body is doing what you tell it, not a single muscle,
that&rsquo;s pretty worrisome. But it didn&rsquo;t seem to matter to
the woman: she nodded, her eyes roving above my head, as if she
understood anyway.</p>
<p>&ldquo;You&rsquo;re
in a research and development facility, Dr. Neumann. A lot of people
here have spent a lot of time taking care of you. You were brought
here as part of the asset diversification process that occurred
following the breakup of Better Future. After the hearings, various
parts were acquired by different private, government, and joint
venture interests, and...&rdquo; She glanced behind her. &ldquo;Should
I..?&rdquo;</p>
<p>&ldquo;Go ahead,&rdquo;
someone said.</p>
<p>Her eyes settled on
me. They did not rove around my face, I noticed. They were steady and
unblinking, like almonds. &ldquo;When the paramedics found you, you
had lost consciousness. They couldn&rsquo;t move you. They could only
keep you alive until a larger medical team arrived. You might
appreciate their challenge, since you had no human heart they could
compress, and little in the way of functional major organs. But they
did it. They kept you alive. During the ensuring weeks, you nearly
died many times. But each time, a way was found to save you. This...&rdquo;
She looked around. &ldquo;I&rsquo;m not sure we really want to...&rdquo;
She nodded and turned back to me. &ldquo;Dr. Neumann, this next part
will come as a shock. There&rsquo;s no way for me to break this to
you gently.</p>
<p>&ldquo;The battle
to save you, it required sacrifices. Several times the decision had
to be made to abandon part of your body in order to save the
remainder. And... well, eventually it became clear that you had no
real chance of making a meaningful recovery. Of regaining any kind of
quality of life. Many believed that no further measures should be
taken to support you. But others... others did not share that view. A
compromise was reached. As part of that compromise, you came here.
Essentially, you became a research project.&rdquo;</p>
<p>The
woman hesitated. &ldquo;It&rsquo;s probably best if I show you.&rdquo;
She reached out and turned my head with one hand, her thumb
and fingers a couple inches apart, with no sign of exertion. The room
span by. And then I was looking at a gray box. At first I thought it
was a piece of medical equipment. It had tubes and wires, one of
which snaked toward me, and an attached monitor screen. I thought:
<em>What is that?</em> and words spilled across the screen: <em>WHAT IS
THAT</em>.</p>
<p>In the
screen&rsquo;s reflection, I could see the daffodils. Beside them,
sitting on the white benchtop, was a small black cylinder mounted on
a block of plastic. The cylinder had a dark lens at closest end and a
wire at the other. I was looking at myself.</p>
