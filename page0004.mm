<h5>CHAPTER  TWO</h5>
<p>One morning I woke
to find an extraordinarily beautiful woman sitting at the foot of my
hospital bed. She wore a green suit with large buttons. A thick black
folder lay in her lap. Her hair was long and straight and corporate.
She was a lawyer.</p>
<p>&ldquo;Hey, you,&rdquo;
she said. &ldquo;Feeling better?&rdquo;</p>
<p>&ldquo;Uh... yes.&rdquo;
I noticed she was holding a tiny black box, with a blinking red
light.</p>
<p>She followed my
gaze. &ldquo;I&rsquo;m recording our conversation, remember?&rdquo;</p>
<p>&ldquo;I just woke
up.&rdquo;</p>
<p>&ldquo;I&rsquo;m
pretty sure you were awake when I notified you. It was right before
you stated that you felt on the path to a full recovery.&rdquo;</p>
<p>&ldquo;Are you from
the company?&rdquo;</p>
<p>&ldquo;I&rsquo;m
Cassandra Tourney.&rdquo; She smiled. Her teeth were like needles. Or
maybe that was the drugs. I was on a lot of painkillers. &ldquo;I&rsquo;m
here to help.&rdquo;</p>
<p>&ldquo;I&rsquo;m
not going to sue.&rdquo;</p>
<p>Tourney glanced at
her black box. &ldquo;You just confirmed that you decline to take
legal action against Better Future, is that correct?&rdquo;</p>
<p>&ldquo;Wait.&rdquo;
I was starting to wake up. &ldquo;Now I&rsquo;m not sure.&rdquo;</p>
<p>Cassandra Tourney
rose from the bed. Her face was blank and terrible. She came closer
until her face blotted out my world and her hair touched my cheek.
Her breath smelled of betrayal and cinnamon. &ldquo;Are you trying to
blackmail me?&rdquo;</p>
<p>&ldquo;What? No.&rdquo;</p>
<p>&ldquo;Threatening
legal action following a commitment to desist from same is tantamount
to fraud,&rdquo; she breathed. &ldquo;Did you know that?&rdquo;</p>
<p>&ldquo;No.&rdquo;</p>
<p>&ldquo;Who&rsquo;s
advising you? Seddon?&rdquo;</p>
<p>&ldquo;I don&rsquo;t
know any Seddon.&rdquo;</p>
<p>She exhaled. It was
like a summer breeze. &ldquo;This isn&rsquo;t over.&rdquo; She
straightened, turned on her heel, and trotted out of the room. Her
skirt was very snug.</p>
<p>I realized I was
holding my breath. I felt shaky and excited and strangely close to
tears. I had never been in love before.</p>
