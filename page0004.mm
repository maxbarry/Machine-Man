<h5>CHAPTER  TWO</h5>
<p>One morning I woke
to find a short woman sitting at the foot of my hospital bed. She
wore a green suit with large buttons. A thick black folder lay in her
lap. Her hair was neat and straight and corporate. She was a lawyer.</p>
<p>&ldquo;Hey, you.&rdquo;
She smiled sympathetically. &ldquo;Feeling better?&rdquo;</p>
<p>&ldquo;Uh... yes.&rdquo;
I noticed a tiny black box in her hand, with a blinking red light.</p>
<p>&ldquo;I&rsquo;m
recording our conversation, remember?&rdquo;</p>
<p>&ldquo;I just woke
up.&rdquo;</p>
<p>&ldquo;You were
awake when I notified you, I&rsquo;m sure. It was right before you
said you felt on the path to a full recovery.&rdquo;</p>
<p>I sat up. &ldquo;Are
you from the company?&rdquo;</p>
<p>&ldquo;Yes. I&rsquo;m
Cassandra Cautery.&rdquo; She smiled. Her teeth were like needles. Or
maybe that was the painkillers. I was on a lot of drugs. &ldquo;I&rsquo;m
here to help.&rdquo;</p>
<p>&ldquo;I&rsquo;m
not going to sue.&rdquo;</p>
<p>She glanced at her
black box. &ldquo;You just confirmed that you decline to take legal
action against Better Future, is that correct?&rdquo;</p>
<p>&ldquo;Wait.&rdquo;
My brain was beginning to work. &ldquo;Now I&rsquo;m not sure.&rdquo;</p>
<p>Cassandra Cautery
rose from the bed. Her face was blank and terrible. She came closer
until her face blotted out the world and her hair dangled above my
cheek. Her breath smelled of cinnamon and betrayal. &ldquo;Are you
trying to threaten me?&rdquo;</p>
<p>&ldquo;What? No.&rdquo;</p>
<p>&ldquo;Hinting at
legal action following a commitment to desist from same is
actionable. Did you know that?&rdquo;</p>
<p>I didn&rsquo;t even
know what that meant. &ldquo;No.&rdquo;</p>
<p>&ldquo;Who&rsquo;s
advising you? Seddon?&rdquo;</p>
<p>&ldquo;I don&rsquo;t
know any Seddon.&rdquo;</p>
<p>She exhaled. It was
like a summer breeze, from somewhere evil. &ldquo;This isn&rsquo;t
over.&rdquo; She tucked her folder under her arm and walked out. Her
skirt was very snug.</p>
<p>I realized I was
holding my breath. I felt shaky and close to tears. I had never been
in love before.</p>
