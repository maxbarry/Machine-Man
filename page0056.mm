<p>The next day, Carl wheeled me through
the corridors of Research Division to the Repository. There was not
much talking. While waiting for an elevator, he said, &ldquo;Glad to
hear Miss Banks is recovering well,&rdquo; and I didn&rsquo;t say
anything, because I hated him. He seemed to get the message.</p>
<p>The Repository door was protected by an
electronic lock, like most doors in Better Future. Seriously, you
couldn&rsquo;t go to the bathroom without swiping yourself in first.
I think it had less to do with keeping unauthorized people out than
monitoring where authorized people went. &ldquo;I don&rsquo;t have my
pass,&rdquo; I said.</p>
<p>&ldquo;No need.&rdquo; Carl swiped his
own card, and the lock&rsquo;s display blinked a friendly green. I
was surprised, because as far as I knew, we did not grant lab access
to random security guards. Carl had been given special access.</p>
<p>He opened the door. Until this point, I
had not allowed myself to completely believe they were really going
to let me have my legs back: I had been half-bracing myself for
Cassandra Cautery to slide out from a doorway and look sympathetic
and say there was a last-minute problem. But no. Carl wheeled me
inside.</p>
<p>It was a large room, with many shelves.
On slabs of gleaming steel lay half a dozen sets of legs in various
stages of completion. My Contours were among them. To my right was a
smorgasboard of fingers. Next to them were hands. I was not really
happy with the hands. So far I&rsquo;d been unable to come up with
anything I could honestly say was a clear improvement over biology.
It was frustrating, because I had some really great arms in here.
Some really great arms.</p>
<p>Carl said, &ldquo;Would you like me to
fetch you a sweater?&rdquo;</p>
<p>I started. I had almost forgotten he
was here. &ldquo;What?&rdquo;</p>
<p>&ldquo;You&rsquo;re shivering.&rdquo;
He pointed to my arms. It was true.</p>
<p>&ldquo;Oh,&rdquo; I said. &ldquo;No,
thanks.&rdquo;</p>
