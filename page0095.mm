<p>I wasn&rsquo;t a
complete fool. I didn&rsquo;t approach from the front. I kicked down
a few fences and came at them from the side. There was a small
industrial plant adjacent to the main Better Future grounds, and its
vats provided good cover. As I ran between them, grabbing glimpses of
the sleek, gray Better Future building, I tried to estimate where
Lola&rsquo;s suite was. Because it had a balcony. And this was
critical, because the Contours were not good on stairs. Once I was
inside the building, I would find it difficult to move between
floors; in fact, given they would certainly shut down the elevators,
it might be impossible, . So this led to one inexorable conclusion: I
must enter the building on Lola&rsquo;s level.</p>
<p>Conceivably, I
could jump up there. Well, when I say <em>I</em>, my own input would be
minimal. I would just tell the Contours to do it, then find out if
they could. And, theoretically, if they tried and failed, they would
do so in a manner that wouldn&rsquo;t kill me. They were programmed
to never exceed my physical tolerances. But still, that was some leap
of faith.</p>
