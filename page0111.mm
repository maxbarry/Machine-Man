<p>Angelica arrived
home around six. By then I was not doing so well. Every muscle in my
body felt made of glass. My bowels were panicked and easily startled.
I had almost trod on tiny scampering dogs so many times that I was
thinking about doing it on purpose.</p>
<p>The dogs heard
Angelica&rsquo;s approach before I did and went into a frenzy:
barking, running laps of the hallway, pawing at the door. When
Angelica appeared, she dropped to her knees and scooped up as many as
she could hold, laughing as they squirmed and licked her face. I
found the whole scene kind of disgraceful. I mean, I understand it
would be nice to be greeted like this, but have some dignity. You
don&rsquo;t need to roll over and expose your genitals. I&rsquo;m
talking about the dogs. I feel that devotion that slavish can&rsquo;t
be taken seriously. It&rsquo;s not objective. I have a similar issue
with Christianity. Of course, I&rsquo;m a cat person.</p>
<p>Angelica had set
down a blue bag on the floorboards. This was one of those situations
where the social rules were so obvious even I could see them: I
should give Angelica a minute with her dogs before asking if that bag
had painkillers in it and if so could I please have some. So I did: I
waited silently at the end of the hall, not making anything of the
fact that every biological part of my body was silently screaming.
Finally, Angelica looked up. &ldquo;You need some meds?&rdquo;</p>
<p>The sweating and
grimacing must have given me away. I said, &ldquo;Yes, please.&rdquo;</p>
