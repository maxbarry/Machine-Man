<p>I hit the button for the elevator and
bounced up and down in the Contours while I waited. I was tempted to
take an express route, like leaping out a window and walking back in
through the lobby. Because I could do that. But that would be a bad
idea for the same reason I wasn&rsquo;t walking Lola Banks to the
labs: it would let everyone know that something was up.</p>
<p>Of course, I was being stupid. I was
assuming that Lola&rsquo;s suite was private. And this was clearly
not the case, because when the elevator finally arrived, it had
Cassandra Cautery in it. &ldquo;Charlie.&rdquo; She did not look
happy. &ldquo;Come here.&rdquo;</p>
