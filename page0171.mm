<p>&ldquo;Brains are
plastic,&rdquo; said Lola. &ldquo;That&rsquo;s what we tell people
when they come in with a foot missing, or a hand, or arm. Brains
adapt. When you lose a limb, the neurons that were firing its action
potentials, they won&rsquo;t just sit around. They&rsquo;ll start
looking for new jobs. They roam around your brain until they find
something useful to do. We tell people this because it&rsquo;s how
you can become just as mobile on an artificial leg as a real one.
When you lose a body part, you&rsquo;re literally freeing up a part
of your brain that can now do something else.</p>
<p>&ldquo;I&rsquo;ve
had amputees whose eyesight has improved. There was a woman we had
in, she swore she got better at math. There are people who
wind up being able to smell colors, or sense the personality of
letters: that&rsquo;s called synesthesia. These
people&rsquo;s neurons went looking for new jobs and found strange
ones. It&rsquo;s why we like to get people into prosthetics as
quickly as possible. To capture as many free-roaming neurons as
possible for motor function, before they decide to devote themselves
to something too funky.</p>
<p>&ldquo;Carl said
