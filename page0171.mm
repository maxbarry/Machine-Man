<p>&ldquo;Brains are
plastic,&rdquo; said Lola. &ldquo;That&rsquo;s what we tell people
when they come in with a foot missing, or a hand, or arm. Brains
adapt. When you lose a limb, the neurons that were in charge of it,
firing its action potentials, they don&rsquo;t stay there. Once they
figure out they&rsquo;re not needed, they start looking for new jobs.
They roam around your brain until they find something new to do. We
tell people this because it&rsquo;s how you can become just as mobile
on an artificial leg as a real one. Losing a body part literally
frees up resources in your brain to do something else.</p>
<p>&ldquo;We&rsquo;ve
had amputees whose eyesight has improved. There was a woman with a
