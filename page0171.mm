<p>&ldquo;Brains are
plastic,&rdquo; said Lola. &ldquo;That&rsquo;s what we tell people
who come in with a foot missing, or hand, or arm. Brains adapt. When
you lose a limb, the parts of your brain that were in charge of it,
the neurons, they don&rsquo;t just sit there. Once they figure out
they&rsquo;re not needed, they start roaming around, looking for new
jobs. We tell people this because it&rsquo;s how you can become as
mobile on an artificial leg as a real one. Losing a body part frees
up resources in your brain that you can apply to something new.</p>
<p>&ldquo;There
was a woman with a transtibial earlier this year, her eyesight
