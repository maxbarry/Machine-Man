<p>I hesitated.
Because, really, is anyone perfect? I don&rsquo;t think so. <em>Perfect</em>
has a strict definition. You can&rsquo;t be mostly perfect. You can&rsquo;t
be perfect some of the time. You&rsquo;re either perfect or not. And,
honestly, I don&rsquo;t think biology does perfect. Biology is about
<em>good enough</em>. Efficient approximation. A vacuum is perfect. Pi
is perfect. People are not.</p>
<p>But I knew this
would be a difficult sell to Angelica, as she stood there in the
bathroom with a syringe full of meds. Anyway, she wasn&rsquo;t really
asking if I thought Lola was perfect. She wanted to know if I thought
Lola was <em>good enough</em>. And the answer to that was yes. Lola far
exceeded my expectations, and not just because my expectations were
to die alone and friendless. She was amazing. I was too new to the
concept of love to feel completely comfortable throwing that term
around, but I knew Lola was about the most important thing in my
life. So I said: &ldquo;Yes. She is.&rdquo;</p>
<p>&ldquo;Too late,&rdquo;
said Angelica. &ldquo;You hesitated.&rdquo;</p>
<p>&ldquo;What? I was
thinking. I was giving your question due consideration.&rdquo;</p>
<p>&ldquo;What&rsquo;s
to think about? You either want to cut off parts of her or you
don&rsquo;t.&rdquo;</p>
<p>&ldquo;I really
object to your characterization. It&rsquo;s not about <em>cutting off
parts</em>. It&rsquo;s about enhancing functionality.&rdquo;</p>
<p>&ldquo;Does Lola
know you want to enhance her functionality?&rdquo;</p>
<p>&ldquo;I don&rsquo;t
want to enhance Lola!&rdquo;</p>
<p>&ldquo;I don&rsquo;t
believe you.&rdquo; Angelica raised the syringe. This time I was sure
she was going to smash it. My legs hiccuped forward of their own
accord.</p>
<p>I held out my
hands. &ldquo;Please. Wait. There&rsquo;s no problem. I promise.
Everything&rsquo;s going to be okay. Please, please, believe me.&rdquo;</p>
<p>Angelica eyed me.
&ldquo;Now that was sincere. But you&rsquo;re on notice. I&rsquo;m
watching you.&rdquo;</p>
<p>At first I was
confused. It took me a few moments to figure it out: Angelica thought
I&rsquo;d been talking to her.</p>
