<p>I hesitated.
Because, really, is anyone <em>perfect</em>? I don&rsquo;t think so.
That&rsquo;s not a criticism. It&rsquo;s that perfect has a strict
definition. You can&rsquo;t be mostly perfect. You can&rsquo;t be
perfect some of the time. A thing is either perfect or it is not.
And, frankly, I don&rsquo;t think biology does perfect. Biology is
about <em>good enough</em>. Its philosophy is efficient approximation.
Which, again, isn&rsquo;t a criticism. When you are trying to keep a
bundle of DNA strands alive long enough to reproduce in a hostile,
resource-constrained environment, that&rsquo;s an excellent strategy.
But it doesn&rsquo;t get you perfection. Crystals are perfect. Pi is
perfect. People are not.</p>
<p>But I could see
this would be a difficult sell to Angelica, who was standing here
waiting for my answer with burning eyes. She wasn&rsquo;t really
asking whether I thought Lola was perfect. She wanted to know if I
thought Lola was <em>good enough</em>. So I decided to interpret the
question this way, and said truthfully, &ldquo;Lola is perfect just
the way she is.&rdquo;</p>
<p>Angelica did not
look convinced. 
</p>
<p></p>
<p></p>
<p></p>
<p></p>
<p>Again, not to
criticize. But perfection is not in biology&rsquo;s philosophy. 
</p>
<p>It produces some
terrific outcomes, given available materials. 
</p>
<p></p>
<p></p>
<p></p>
<p></p>
<p></p>
<p>That was a lie, of
course. I didn&rsquo;t really think Lola was perfect. I know it&rsquo;s
not socially acceptable to say something like that, but come on. I&rsquo;m
a scientist. <em>Perfect</em> has a strict definition. Could one of
your eyelashes be straighter? Imperfect. You can not be <em>mostly
perfect</em>. You are either perfect or you are not. There is no
<em>perfect</em> in biology.</p>
<p>But I meant it in
spirit: I thought Lola was everything I needed. She far exceeded my
expectations. And not just because I had been becoming resigned to
the expectation of dying alone and friendless. 
</p>
