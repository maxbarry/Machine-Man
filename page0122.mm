<p>I found it useful
to talk to my parts as I worked. For example, I would pick up a
mirror plate and say, &ldquo;And what&rsquo;s your problem?&rdquo;
Or, when contemplating a radiation shield: &ldquo;You need an arc
welder. That&rsquo;s what you need.&rdquo; They didn&rsquo;t answer
me. I wasn&rsquo;t crazy. It was just a way to focus. But sometimes I
heard footsteps outside the door, and realized this probably wasn&rsquo;t
obvious to anyone else.</p>
<p>I regretted what
I&rsquo;d said to Lola. I regretted it a lot. It wasn&rsquo;t Lola&rsquo;s
fault. I told the Contours that. &ldquo;She tried to calm down,&rdquo;
I said. This was very late one night, after a particularly
frustrating few hours prying apart transistors. &ldquo;She did
everything she could. She didn&rsquo;t want you to die.&rdquo; Then I
wrapped my arms around my chest and cried a little, because I was
really tired.</p>
<p>The next day, as
soon as my eyes opened, I decided I would apologize to Lola. I would
make things right. The idea filled me with joy. I didn&rsquo;t want
to wallow here any longer, this filthy, stinking, grieving thing,
dragging itself around. I didn&rsquo;t want to be angry at Lola. I
didn&rsquo;t want to make her sad. But then I sat up and parts of the
Contours fell everywhere. I was sleeping with them to stave off the
phantom pain. Don&rsquo;t ask me why that worked. It just did. And as
parts tumbled to the carpet, I thought: <em>I&rsquo;ll just fix one
thing</em>. Because if I could do that, take one step toward restoring
functionality, the Contours wouldn&rsquo;t be dead. They would only
be temporarily disabled, like me. Everything would be getting better.</p>
<p>So I crawled off
the bed and began hunting for one fixable thing. Weeks passed.</p>
