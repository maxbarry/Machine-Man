<p>Cassandra Cautery pulled a chair right up to the edge of my hospital
bed and looked up at me with wet eyes. I won&rsquo;t lie. It was very
fetching. I know: she was duplicitous, and a lawyer, and didn&rsquo;t
care for me at all. But I had gone a long time without anyone looking
at me like that. A very long time. Look, all I&rsquo;m saying is it
was fetching. Give me a break.</p>
<p>&ldquo;I just want to make sure you&rsquo;re okay.&rdquo; She took my
hand. Her fingers were like ice. 
</p>
<p>&ldquo;I am okay.&rdquo;</p>
<p>&ldquo;Are you?&rdquo; She squeezed my hand.</p>
<p>&ldquo;Yes,&rdquo; I said. &ldquo;I&rsquo;m okay.&rdquo;</p>
<p>&ldquo;If something brought this on&mdash;say, you found something
out that upset you&mdash;I want you to know you can tell me. It will
stay between us, in the strictest confidence.&rdquo; She leaned
forward. &ldquo;It&rsquo;s Dick, isn&rsquo;t it?&rdquo;</p>
<p>&ldquo;Who?&rdquo;</p>
<p>&ldquo;Your boss.&rdquo;</p>
<p>&ldquo;Oh,&rdquo; I said. &ldquo;D. Peters. No, I just wanted to cut
off my leg.&rdquo; She looked confused, so I added, &ldquo;To build a
better one.&rdquo;</p>
<p>&ldquo;Oh,&rdquo; said Cassandra Cautery. She took back her hand.</p>
<p>&ldquo;I hope this won&rsquo;t interfere with my work. I&rsquo;ve had
a few new ideas this last week. Some aspects of my original design, I
don&rsquo;t know what I was thinking.&rdquo; I laughed.</p>
<p>Cassandra Cautery stood and gave me a look. It was an evaluative
look. She was scanning my body for trace emanations of deception.</p>
<p>&ldquo;Seriously,&rdquo; I said.</p>
<p>Something glinted in her eyes. I think it was greed. She looked like
a kid who had just stumbled into the living room on Christmas morning
and didn&rsquo;t dare let herself believe that the huge present with
the ribbon was for her. But I didn&rsquo;t understand where the
present was.</p>
<p>I said, &ldquo;Is something going on with D. Peters?&rdquo; A memory
was surfacing in my brain, vague and indistinct. &ldquo;Did he say,
<em>Don&rsquo;t do that thing</em>? What thing?&rdquo;</p>
<p>Cassandra Cautery kissed my forehead. &ldquo;Shhh. All I want you to
worry about, Charlie, is getting better.&rdquo;</p>
<p>I didn&rsquo;t say anything. I was confused by the kiss. I had gone
seven years without a kiss, and now I had two. It was the kind of
data event that implied a serious contamination of laboratory
conditions.</p>
<p>&ldquo;And getting back to work,&rdquo; she said.</p>
