<p>Page 68.</p>
<p>I grabbed a hand,
six fingers, and two thumbs, and hurried out of the Repository. None
of this made much sense. I was just carrying whatever I could. I
wished I could take some legs, but they weighed a ton. Two tons, in
the case of the Fragmenters. And here I was with weak, fleshy arms,
which could barely manage two bags of groceries. If I had my metal
arms&mdash;but I wasn&rsquo;t thinking about that. I wasn&rsquo;t
thinking about where my metal arms might be right now. Who they might
be attached to. I squeezed my multifunction fingers to my chest and
let the Contours bear me away.</p>
<p>The elevator was
empty. I was glad about this, because if the doors had slid open to
reveal Better Future guards, I don&rsquo;t know what would have
happened. There might have been some kicking. I was agitated and not
confident of making completely logical decisions. All I could think
about was gathering everything I valued in one place and defending
it.</p>
<p>So I went to Lola.
But she was not in her suite. She was not anywhere on that level; at
least, I didn&rsquo;t see her when I roamed the length of it, calling
her name. I saw a few managers, and quite a lot of personal
assistants. From their expressions, it became clear that I would soon
also be seeing some security guards, so I turned back to the stairs.</p>
<p>The Contours were
surprisingly bad here. Twice they stopped, and I had to soft-reset
them, by imagining raising my knee three times. I think they had
trouble identifying level surfaces. I would have to investigate this
later. It meant that by the time I emerged on the sub-level for
Lola&rsquo;s old medical recovery room, and kicked the stairwell door
off its hinges, the guards had already arrived.</p>
<p>&ldquo;Dr.
Neumann,&rdquo; said one. Not Carl, of course. A substitute, young
and sweaty. &ldquo;I would very much appreciate it if you could calm
down a second.&rdquo;</p>
<p>I weighed my odds
of getting past these guys on the Contours before anyone drew a
weapon. I thought they were pretty good. A temporary solution, but
still: better than letting them take me. Then Lola emerged from a
doorway and stared at me. &ldquo;Charlie? What are you doing?&rdquo;</p>
<p>&ldquo;What are <em>you</em>
doing? I&rsquo;ve been looking for you.&rdquo;</p>
<p>Lola glanced over
her shoulder. &ldquo;They asked me to help. There&rsquo;s a man,
who... well, they thought I could help.&rdquo;</p>
<p>&ldquo;What man?&rdquo;
This seemed to come out of my mouth very slowly, as if I were drunk:
<em>Whaaat maannn</em>.</p>
<p>&ldquo;It&rsquo;s,
um, that security man. His name is Carl.&rdquo;</p>
<p>&ldquo;Uck,&rdquo;
I said.</p>
<p>&ldquo;He doesn&rsquo;t
have any arms,&rdquo; Lola said, and in her eyes I saw a terrible
light, like love.</p>
