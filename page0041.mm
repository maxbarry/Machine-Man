<p>Lola rose from the table. She offered me her hand, and I took it. As
the Contours raised me back to my full height, her eyes held mine.
Sunlight bounced off a building across the street and played across
her cheeks; the stray hairs that had escaped her pony-tail glowed
orange. I felt my heart lifting out of my body. That&rsquo;s how it
felt: like I was leaving my physical body, becoming weightless and
untouchable.</p>
<p>A hard shard of light skipped across Lola&rsquo;s face, a reflection.
I turned, because I by now I&rsquo;d heard the motor, and recognized
it. A white van jumped the kerb and began to disgorge Better Future
security personnel.</p>
<p>&ldquo;I think&mdash;&rdquo; I said, but that was all I managed
before they burst into the cafe, and people began to scream and
overturn tables. Carl yelled at me to <em>let go of that woman, right
now</em>. Then things went bad.</p>
