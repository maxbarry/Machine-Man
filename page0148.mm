<p>With my excess arm
out of the way, I was able to clamp shut the artery. I&rsquo;ll spare
you the specifics. Let&rsquo;s just say it was a temporary solution
involving my fingers. I then spent the next forty minutes getting off
the table. It took this long because I had to do everything with my
mouth. First I pulled back the surgical cloth with my teeth. At this
point I gained a better appreciation of why Better Future test
subjects were having psychological problems. Because suddenly,
despite everything I&rsquo;d ever thought, I wanted to let go of my
artery and die.</p>
<p>My body ended at
the sternum. I couldn&rsquo;t tell exactly where. I was wrapped in a
flexible plastic-silicon bandage: above this was flesh and below was
metal. The abdomen unit I&rsquo;d inspected earlier was probably in
there, but if so it was encased in silver-black titanium. Etched into
the metal where my belly button should be was a circular design I
recognized even upside down as a Better Future logo. My hips were
multi-axis servo-magnetics. My legs were black Contour Threes.</p>
<p>I was going to
vomit. Some part of me that had been silent a long time shrieked out
a violent mental rejection: <em>this should not be</em>. I closed my
eyes and thought about Lola.</p>
<p>I was weakening. I
had to forget about who or what I was and concentrate on staying
alive. That meant getting off this table. I forced myself to look
again and eventually I spotted it: a thick plastic handle jutting out
of my metal side that didn&rsquo;t look like it belonged. What it
looked like was a damper: a safety switch that prevented the abdomen
from powering up.</p>
<p>I waited until I
was sure I had no better options. Then I let go of my artery, grabbed
at the handle, and pulled. It put up a fight, but I got it. Something
clicked. I heard a high-pitched whine, and simultaneously a deep hum.
I felt warmth. And I began to cry, because now I was going to live.</p>
