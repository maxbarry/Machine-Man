<p>In my
third week at the hospital I was visited by the prosthetics
specialist. She walked into my room with a bunch of artificial legs
under each arm, like some kind of Hindu goddess. &ldquo;Hi, Charlie.&rdquo;
She dumped the legs onto my bed and pushed her glasses up
her nose. Her hair was brown and frizzy and dragged into a merciless
pony-tail. Her white shirt was at least two sizes too big. There was
no longer any danger of confusing her with a deity. &ldquo;I hear you
got a transfemoral. May I?&rdquo; Before I could respond, she
lifted up my sheet. &ldquo;Oh, yeah. That&rsquo;s a nice stump. Can I
touch?&rdquo;</p>
<p>&ldquo;Uh...&rdquo;</p>
<p>Her fingers probed my bandages. &ldquo;Oh.
Yes. Yes.&rdquo; She dropped the sheet. &ldquo;I have to confess,
Charlie, I do prefer a transfemoral. We get a lot of
transtibials&mdash;that&rsquo;s below the knee&mdash;and, no offense
to those people, but it&rsquo;s like fitting shoes. There&rsquo;s no
art to it. This...&rdquo; She patted my leg, the part I had left.
&ldquo;This gives us options.&rdquo;</p>
<p>&ldquo;I like options,&rdquo; I said.</p>
<p>She smiled. &ldquo;You and me, Charlie.
We&rsquo;re going to be friends.&rdquo;</p>
