<p>Carl walked us to a
supply elevator I&rsquo;d never used before and swiped his ID card. I
found this interesting, because a moment ago, Carl&rsquo;s hands had
looked like blocks of stone. Now they&rsquo;d split into two thick
fingers, supple enough to grasp the tag around his neck. It seemed
they could split a few different ways, then come together to deliver
a punching force. That wasn&rsquo;t a bad idea, for security guard
hands. It&rsquo;s what I would have done, if I&rsquo;d designed them.
Which I hadn&rsquo;t.</p>
<p>&ldquo;Take it down
all the way,&rdquo; Carl said. &ldquo;It opens in the parking lot.
Then you&rsquo;re on your own.&rdquo;</p>
<p>Lola and I shuffled
inside. &ldquo;Thank you, Carl.&rdquo;</p>
<p>&ldquo;I&rsquo;m
not doing you a favor. I&rsquo;m just paying my debts.&rdquo;</p>
<p>&ldquo;Well...
thanks anyway. And I&rsquo;m sorry.&rdquo;</p>
<p>The elevators doors
began to slide closed, but Carl raised a hand to send them back. His
fist was a block of solid metal again. &ldquo;Sorry, Dr. Neumann?&rdquo;</p>
<p>&ldquo;Yes.&rdquo;</p>
<p>&ldquo;What are you
sorry for?&rdquo;</p>
<p>&ldquo;Well... you
know.&rdquo;</p>
<p>&ldquo;For having
them take away my prosthetics? That&rsquo;s what you mean?&rdquo;</p>
<p>&ldquo;Ah...&rdquo;</p>
<p>&ldquo;When I was
left unable to unzip my own fly, because you ordered that nobody
could use your arms but you? That&rsquo;s what you&rsquo;re talking
about?&rdquo;</p>
<p>&ldquo;Uh...&rdquo;</p>
<p>&ldquo;When I had
to be hidden out of your sight, because you wouldn&rsquo;t tolerate
me in the same building?&rdquo;</p>
<p>&ldquo;Carl&mdash;&rdquo;</p>
<p>&ldquo;That&rsquo;s
not a problem, Dr. Neumann.&rdquo; He lowered his arm, releasing the
elevator door. I heard the thin whine of servo-magnetics. &ldquo;I
have my own parts now.&rdquo;</p>
