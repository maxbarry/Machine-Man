<h5>CHAPTER 
TWENTY-ONE</h5>
<p>Carl looked
different. At first I couldn&rsquo;t put my finger on it. I was
distracted by other thoughts, like why was he even here. Cassandra
Cautery had said she&rsquo;d fired him. In fact, I&rsquo;d got the
impression she&rsquo;d maybe disposed of him permanently. But Carl
was not disposed of. He was right here, filling the doorway, blocking
the only exit to Lola&rsquo;s suite that didn&rsquo;t require falling
eighty feet.</p>
<p>&ldquo;Hi, Carl.&rdquo;
The light from the hallway behind him made it hard to see his face.
&ldquo;How are you?&rdquo;</p>
<p>Carl didn&rsquo;t
move. Lola peeked around me. &ldquo;Carl?&rdquo;</p>
<p>Still nothing. He
was wearing his security uniform, although something about that
looked different, too. &ldquo;Kind of a crazy day, huh?&rdquo; I
said. &ldquo;I mean... I don&rsquo;t know how up you are on current
events, but...&rdquo;</p>
<p>Carl stepped into
the room. Suddenly what was different about him became obvious. I
hadn&rsquo;t clicked earlier because Carl had always been a big guy.
But not this big. Not so big he had to turn sideways to fit through a
door.</p>
<p>His arms were
concealed by what must have been a custom-tailored uniform, but his
hands protruded from his sleeves: thick blocks of gray metal. They
looked like sledgehammers. I had not designed these.</p>
<p>&ldquo;Miss Banks,&rdquo;
Carl said. &ldquo;You were always very kind to me.&rdquo;</p>
<p>I felt a rush of
relief. I opened my mouth to express this, but Carl&rsquo;s eyes
flicked at me, and I shut up again. It was clear to me in that moment
that Carl knew I had tried to get him fired.</p>
<p>&ldquo;In
appreciation of that,&rdquo; he said, &ldquo;I&rsquo;ll give you a
head start.&rdquo;</p>
