<p>Lola was silent as
the elevator descended. I risked a glance at her: she was staring
straight ahead, her arms stiff. &ldquo;All right,&rdquo; I said.
&ldquo;Before we reach the parking lot, you should&mdash;&rdquo;</p>
<p>&ldquo;Did you make
them take away Carl&rsquo;s arms?&rdquo;</p>
<p>&ldquo;I&rsquo;m
not sure now is the best time to discuss this.&rdquo;</p>
<p>&ldquo;<em>Did you
make them take his arms?</em>&rdquo;</p>
<p>&ldquo;They weren&rsquo;t
his arms!&rdquo; I said. There was more to this argument, but Lola&rsquo;s
lips thinned to a dramatic white slash, and I decided it would be
counter-productive to advance it. &ldquo;Let&rsquo;s address this
later.&rdquo;</p>
<p>The elevator
thumped to a stop. The doors seemed to take a long time to open. &ldquo;I
want you to know, I am working extremely hard right now to not yell
at you.&rdquo;</p>
<p>&ldquo;Okay.
Noted.&rdquo; I held out my arms. &ldquo;Come here.&rdquo;</p>
<p>She glared at me.</p>
<p>&ldquo;There&rsquo;s
probably about to be some running,&rdquo; I said. &ldquo;And you need
to be on board for that.&rdquo;</p>
<p>The doors opened.
It took my eyes a moment to adjust to the contrast: the darkness of
the parking lot, and the sunlight pouring in the entry ramp. So I
first became aware of security by the shouting.</p>
<p>Lola jumped into my
arms. I wrapped her up tight. Then there was some running.</p>
