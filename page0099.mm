<h5>CHAPTER 
TWENTY-ONE</h5>
<p>Carl looked
different. At first I couldn&rsquo;t put my finger on it. I was
distracted by competing thoughts, like why was he even here. I was
sure Cassandra Cautery had said she&rsquo;d fired him. In fact, I&rsquo;d
got the impression she&rsquo;d maybe had him disposed of permanently.
But Carl was not disposed of. He was right here, filling the doorway.
Blocking the only exit to Lola&rsquo;s suite.</p>
<p>&ldquo;Oh,&rdquo; I
said. &ldquo;Hi, Carl.&rdquo; The light from the hallway behind him
made it hard to see his face. &ldquo;How are you?&rdquo;</p>
<p>He didn&rsquo;t
move. Lola peeked around me. &ldquo;Carl?&rdquo;</p>
<p>Still nothing. He
was wearing his security uniform, although something about that
looked different, too. &ldquo;Kind of a crazy day, huh?&rdquo; I
said. &ldquo;I mean... I don&rsquo;t know if you heard, but...&rdquo;</p>
<p>Carl stepped into
the room. And I suddenly realized what was different. It was obvious.
I just hadn&rsquo;t clicked because Carl had always been a big guy.
But not this big. Not so big he had to turn sideways to fit through a
door.</p>
<p>&ldquo;Miss Banks,&rdquo;
Carl said. &ldquo;You were always very kind to me.&rdquo;</p>
<p>I felt a rush of
relief, and opened my mouth to express this. But Carl&rsquo;s eyes
stabbed at me, and I shut it again. I think Carl knew I had tried to
get him fired.</p>
<p>&ldquo;So, in
appreciation of that,&rdquo; Carl said, &ldquo;I&rsquo;ll give you a
head start.&rdquo;</p>
