<h5>CHAPTER 
TWENTY-TWO</h5>
<p>I realized I could
no longer postpone medical treatment when I began to hallucinate.
Until now, I had remained functional despite a bullet wound to the
bicep thanks to the ocean of adrenaline sloshing around my veins, but
now Lola and I had put distance between ourselves and people who
wanted to kill us, this began to subside, leaving me raw and shaky.
It was just as well I&rsquo;d started the day with the usual generous
cocktail of painkillers. But the effects of that were not going to
last forever. As I stared into the darkening sky and saw a low-flying
plane morph into a pigeon and back again, I decided I needed a
hospital.</p>
<p>&ldquo;You can&rsquo;t
go to a hospital,&rdquo; said Lola. Night was falling; in the dusk
light, her face looked unreal, like something from a movie. Or maybe
that was the hallucinating. &ldquo;That&rsquo;s the first place
they&rsquo;ll look.&rdquo;</p>
<p>&ldquo;Who?&rdquo;</p>
<p>&ldquo;Better
Future. The people who want to cover up their not-so-legal human
trials of artificial body parts. Isn&rsquo;t that what you just told
me?&rdquo;</p>
<p>&ldquo;I need to
sit down.&rdquo; 
</p>
<p>&ldquo;You are
sitting down.&rdquo;</p>
<p>I looked at the
Contours. &ldquo;Sometimes I forget.&rdquo;</p>
<p>&ldquo;Charlie.
Stay with me.&rdquo;</p>
<p>&ldquo;I will,&rdquo;
I said, emotional.</p>
<p>Lola fell silent.
Somewhere, a dog barked. &ldquo;Okay. I think I know somewhere. A
friend of mine from the hospital, a doctor, she lives around here.
Maybe she can help us.&rdquo;</p>
<p>I watched Lola&rsquo;s
hair. Strands floated on the breeze: when they caught the
streetlight, they flared like stars.</p>
<p>&ldquo;Charlie?&rdquo;</p>
<p>&ldquo;What?&rdquo;</p>
<p>&ldquo;We need to
go.&rdquo;</p>
<p>&ldquo;I&rsquo;m
really tired.&rdquo;</p>
<p>&ldquo;Just a
little further, okay?&rdquo;</p>
<p>&ldquo;Okay,&rdquo;
I said.</p>
