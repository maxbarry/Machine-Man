<h5>CHAPTER 
TWENTY-TWO</h5>
<p>I realized I
couldn&rsquo;t postpone medical treatment any longer when I began to
hallucinate. Until now, I had remained functional despite carrying a
bullet wound to the bicep thanks largely to the ocean of adrenaline
sloshing around my veins, but now Lola and I had put distance between
ourselves and people who wanted to kill us, this began to subside,
leaving me feeling raw and fragile. It was just as well that I&rsquo;d
started the day, as usual, with a generous cocktail of painkillers.
But these were not going to last forever. I needed a hospital.</p>
<p>&ldquo;You can&rsquo;t
go to a hospital,&rdquo; said Lola. Night was falling; her face
looked yellow in the streetlight. &ldquo;That&rsquo;s the first place
they&rsquo;ll look.&rdquo;</p>
<p>&ldquo;Who?&rdquo;</p>
<p>&ldquo;Better
Future.&rdquo; She put her hands on her hips. &ldquo;The people who
want to cover up their not-so-legal human trials of artificial combat
augmentations. Isn&rsquo;t that what you just told me?&rdquo;</p>
<p>&ldquo;I need to
sit down.&rdquo; 
</p>
<p>&ldquo;You are
sitting down.&rdquo;</p>
<p>&ldquo;Oh yeah.&rdquo;
I looked at the Contours. &ldquo;Sometimes I forget.&rdquo;</p>
<p>&ldquo;Charlie.
Stay with me.&rdquo;</p>
<p>&ldquo;I will,&rdquo;
I said, emotional.</p>
<p>Lola fell silent.
Somewhere, a dog barked. &ldquo;Okay. I think I know somewhere. A
friend of mine from the hospital, a doctor, she lives around here.
Maybe she can do something about that arm.&rdquo;</p>
<p>I watched Lola&rsquo;s
hair. Strands floated on the breeze: when they caught the streelight,
they flared like stars.</p>
<p>&ldquo;Charlie?&rdquo;</p>
<p>&ldquo;What?&rdquo;</p>
<p>&ldquo;We need to
go.&rdquo;</p>
<p>&ldquo;I&rsquo;m
really tired.&rdquo;</p>
<p>&ldquo;Just a
little further, okay?&rdquo;</p>
<p>&ldquo;Okay,&rdquo;
I said.</p>
