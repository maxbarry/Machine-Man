<p>The nurses were not pleased to see me
back at the hospital. I detected resentment in the clatter of my meal
tray onto my bedside table, and the new roughness of the sponge.
Sometimes, when the painkillers were wearing off, I pushed the call
button and no-one came. At first this was a mystery to me. Then one
night, sweating and unable to stand the terrible crawling sensation
in my leg that was no longer there, I held down the call button for
as long as it took. Nine minutes later, a nurse appeared. She
snatched the call button out of my hand and stared down at me like I
was a stain. &ldquo;I&rsquo;m <em>so sorry</em> for the delay,&rdquo;
she said. &ldquo;I was busy with the patients who want to get
better.&rdquo;</p>
