<h5>CHAPTER 
EIGHTEEN</h5>
<p>Moments after I
stepped out of the elevator, a painfully beautiful girl appeared
around a corner, smiling brilliantly. &ldquo;You must be Charlie.&rdquo;
She looked at Cassandra Cautery. &ldquo;And Cassandra! How <em>are</em>
you?&rdquo;</p>
<p>&ldquo;Fine.&rdquo;
</p>
<p>&ldquo;That&rsquo;s
really wonderful,&rdquo; said the girl. &ldquo;What is that shirt, by
the way? I always mean to ask, but never remember.&rdquo;</p>
<p>&ldquo;I don&rsquo;t
remember,&rdquo; said Cassandra Cautery.</p>
<p>&ldquo;Well, I love
you in it.&rdquo; The girl put her hands on her hips. We could have
been old friends.</p>
<p>Cassandra Cautery
said, &ldquo;Is Walter ready?&rdquo;</p>
<p>The girl&rsquo;s
face turned very solemn. &ldquo;He&rsquo;ll be two minutes. But if
you&rsquo;ll come with me, I will get you completely set up with
whatever you need. Is that okay?&rdquo; When Cautery nodded, she
turned and sashayed down the corridor. The swaying of her skirt was a
little hynpnotic.</p>
<p>&ldquo;I hate that
bitch,&rdquo; said Cassandra Cautery. She had one thumbnail in her
mouth and was gnawing away at it. When she realized I was staring,
she pulled it out and started after the girl. &ldquo;Come on.&rdquo;</p>
