<h5>CHAPTER 
EIGHTEEN</h5>
<p>Moments after I
stepped out of the elevator, a painfully beautiful girl appeared,
smiling brilliantly. &ldquo;You must be Charlie. And Cassandra! How
<em>are</em> you?&rdquo;</p>
<p>&ldquo;Fine,&rdquo;
said Cassandra Cautery.</p>
<p>&ldquo;That&rsquo;s
really wonderful,&rdquo; said the girl. Next to her, the statue of
the Greek goddess looked very plain. &ldquo;What is that shirt, by
the way? I always mean to ask.&rdquo;</p>
<p>&ldquo;I don&rsquo;t
remember.&rdquo;</p>
<p>&ldquo;Well, I love
you in it.&rdquo; The girl put her hands on her hips. We could have
been old friends.</p>
<p>Cassandra Cautery
said, &ldquo;Is he ready?&rdquo;</p>
<p>The girl&rsquo;s
face turned solemn. &ldquo;He&rsquo;ll be two minutes. But if you&rsquo;ll
come with me, I will get you completely set up with whatever you
need. Is that okay?&rdquo; When Cautery nodded, she turned and
sashayed down the corridor. The swaying of her skirt was a little
hypnotic.</p>
<p>I looked at
Cassandra Cautery. She was staring after the girl with loathing. I
felt out of my depth, like a deep-sea fish hauled up to the surface.
I was not compatible with this environment. I did not possess the
parts necessary to survive in it. &ldquo;I&rsquo;m leaving,&rdquo; I
said.</p>
<p>&ldquo;Don&rsquo;t
be an idiot.&rdquo; She didn&rsquo;t even look at me. &ldquo;You
won&rsquo;t make it two floors.&rdquo; She started after the girl.
After a moment, so did I.</p>
