<p>I was left alone in
the medical room for a long time. There was nothing but me, a bed,
and a trash can. The door was locked. Well, I assumed it was. The
truth was, I couldn&rsquo;t bring myself to drag my body off the bed
and across the floor to check.</p>
<p>I was missing my
legs. I was <em>missing</em> my legs. I felt wrong without them. It was
a terrible injustice, that people could take them away from me.
Sitting in that room, half a man, I vowed I would never let them do
that again.</p>
