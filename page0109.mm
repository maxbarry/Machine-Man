<h5>CHAPTER 
TWENTY-THREE</h5>
<p>I woke happy. It
was dark. I didn&rsquo;t know where I was. But I had come from a
wonderful dream, in which I held hands with Lola and she smiled and
we were safe. I lay still, not wanting to disturb this bliss. But
even when reality began to filter in, bringing with it complications
like the fact that I was being pursued by a vengeful security guard
with multifunction sledgehammer arms, things didn&rsquo;t seem so
bad. That didn&rsquo;t seem more important than the miracle of Lola
telling Angelica, <em>I love him</em>. If I had Lola, everything else
was solvable: I felt that very strongly. She was my independent
variable. 
</p>
<p>On the ceiling
above my head was a poster of a dinosaur. I blinked and sat up. There
were dinosaur pictures everywhere. In one corner, stuffed toys
overflowed from a basket. This room belonged to a kid. In fact, now I
looked, this bed was awfully small. It was about five feet long.
Which meant&mdash;</p>
<p><em>I was not wearing my legs.</em></p>
<p>My toes went first.
They spasmed, locked. My foot curled back on itself. My calves turned
to screaming steel and the fact that none of this was real made any
difference. I threw back the covers and tried frantically to massage
the space where my shrieking muscles would be, but I knew it would do
no good without the Contours and was right. Tears streamed from my
eyes. <em>The painkillers have worn off</em>, my brain whispered. <em>It&rsquo;s
going to be much worse now.</em></p>
<p>&ldquo;My legs!&rdquo;
I screamed. &ldquo;<em>I need my legs!</em>&rdquo;</p>
