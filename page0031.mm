<p>Carl opened the limo door and I emerged into bright sunshine to two
dozen Better Future employees lining the path to the lobby. When they
saw me, they began to clap. Carl lifted me into a waiting wheelchair;
there were cheers. As he pushed me past the crowd, they put out hands
for high-fives. I saw a woman from Human Resources who had never made
eye contact with me before, not once, and as I passed she whispered,
&ldquo;You&rsquo;re an inspiration.&rdquo; I didn&rsquo;t understand
what was happening.</p>
<p>Inside the lobby, Cassandra Cautery waited with folded arms and wet
eyes. &ldquo;Welcome back.&rdquo; Her voice was choked with emotion,
like I was her son, returned from war.</p>
<p>&ldquo;Thanks,&rdquo; I said.</p>
<p>She walked alongside as Carl steered me through security. The
wheelchair didn&rsquo;t set off any alarms, which was odd. Or maybe
it did, but the guards had been instructed to let me through anyway.
&ldquo;I&rsquo;ve taken the liberty of expanding your staff. What do
you think of that Jason Huang? I left him, in case he&rsquo;s a
favorite, but really, his metrics are average.&rdquo;</p>
<p>&ldquo;Jason&rsquo;s okay.&rdquo;</p>
<p>She put a hand on my shoulder. Carl took the hint and stopped
pushing. Cassandra Cautery looked into my eyes. She was so short, we
were almost level. &ldquo;It would be no problem to get rid of him.
Permanently.&rdquo;</p>
<p>&ldquo;Jason&rsquo;s fine,&rdquo; I said.</p>
<p>Cassandra Cautery nodded. &ldquo;I just don&rsquo;t want anything to
slow you down.&rdquo;</p>
<p>&ldquo;From making prosthetic legs.&rdquo; I felt it was important to
clarify.</p>
<p>&ldquo;Yes,&rdquo; she said. &ldquo;Or furthering your project in any
way that makes sense to your beautiful, brilliant mind.&rdquo;</p>
