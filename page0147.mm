<p>One day at the
mall, I saw a guy demonstrating an electric carving knife. It was a
white thing with a long handle and a gleaming steel blade, and he had
a steaming roast chicken on a plate. Beneath his little table was a
__ bag, filled, I guess, with more steaming roast chickens. As I
walked by, he was demonstrating to a middle-aged couple how the
electric carving whirred easily through the meat, slicing off
delicious strips with a noise like: <em>vrrrreeeee</em>.</p>
<p>For some reason I
expected this to sound different.</p>
