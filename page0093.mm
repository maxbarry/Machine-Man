<p>My legs slowed. We
came to a halt not far from a small, elderly woman struggling up some
steps with bags of groceries. She came to a halt, too, when she saw
my hooves. Her eyes bulged.</p>
<p>Lola Banks was back
at Better Future. Probably in her suite, waiting for me to return
with the scanner. Her fingers maybe touching her chest, then pulling
away; trying not to wonder what it was the company had put in her
chest. Trying to concentrate on what she knew: that I was coming
back.</p>
<p>&ldquo;<em>Diablo,</em>&rdquo;
whispered the woman.</p>
<p>On the other hand,
Better Future had guards. Guards with guns. Guns they weren&rsquo;t
shy about using. What was I, an idiot? I couldn&rsquo;t go back
there. They would shoot me dead.</p>
<p>&ldquo;<em>Diablo!</em>&rdquo;</p>
<p>I imagined
Cassandra Cautery knocking on Lola&rsquo;s door. Smiling as Lola
opened it. <em>Charlie sent me to fetch you. He wants you to come down
to the lab</em>. Because  Cautery would have to assume that when I
came back, I&rsquo;d be bringing the government. That she had a small
window of time in which eradicate all evidence of wrongdoing. I saw
Cautery placing a hand in the small of Lola&rsquo;s back, steering
her toward the elevators.</p>
<p>&ldquo;<em>Diablo!</em>&rdquo;
</p>
<p>&ldquo;Quiet,&rdquo;
I said, because that was making it really hard to think. Plus my
wounded bicep was beginning to transition from comfortably numb to
distractingly sore with hints of impending agony. Concentrate. Maybe
there was a back entrance. A door not protected by armed security
guards on high alert. I scanned floor plans in my mind. No. There
wasn&rsquo;t.</p>
<p>But Lola. But Lola.</p>
<p>&ldquo;<em>Diablo!</em>&rdquo;
the woman shrieked. She dropped her groceries to alternately point at
me and clap her hands to her cheeks. &ldquo;<em>Diablo!</em>&rdquo;</p>
<p>&ldquo;Then why am
I going back?&rdquo; I shouted. I wasn&rsquo;t really angry with her.
I was just emotional about my own likely death. The Contours began to
hammer the sidewalk, bearing me back to Better Future.</p>
