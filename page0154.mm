<p>I said, &ldquo;No.&rdquo;</p>
<p>&ldquo;So far we&rsquo;ve
been able to keep the authorities out of this.&rdquo; Cassandra
Cautery adjusted her grip on the towel she was pressing to her head.
The way she did this implied it was hurting quite a lot. &ldquo;But
that required calling in a lot of favors, and that&rsquo;s not
sustainable. I don&rsquo;t want to bore you with politics, but there
are some things you can&rsquo;t ask a government official to do no
matter how much you contribute to their campaign. And one of those
things is ignoring a mentally unstable robot-man wandering around
breaking things.&rdquo;</p>
<p>&ldquo;No. <em>No.</em>&rdquo;</p>
<p>&ldquo;All we need
to do is take Carl down. Then this whole situation, it was a little
internal product test that didn&rsquo;t go quite right. We have
those. We put out a statement, making the right kind of soothing
noises, and it&rsquo;s no big deal, there&rsquo;s no story, no
congressional investigations. You see? You see how close we are to
making this okay?&rdquo;</p>
<p>&ldquo;Stop. No. I
can&rsquo;t say this strongly enough. I am not going to <em>shoot at
Carl</em>. I am not going around with a <em>gun</em> in my arm.&rdquo; I
heard panic in my voice, because when two people disagree about
something obvious, it means one of them knows something the other
doesn&rsquo;t.</p>
<p>&ldquo;Online,&rdquo;
said one of the men. I felt a distant prickling, like somebody
telling me a story about my childhood. At first I couldn&rsquo;t tell
where it was. As it grew sharper, I realized it was in my arm, the
one I didn&rsquo;t have a second ago.</p>
<p>&ldquo;Ow,&rdquo; I
said. &ldquo;<em>Ow</em>.&rdquo; I gripped the metal bicep with my
organic hand, trying to choke off the line of electronic ants I could
feel marching there.</p>
<p>&ldquo;That&rsquo;s
your brain adapting to the nerve signals,&rdquo; said Jason. &ldquo;It&rsquo;ll
feel weird for about six days, but then you&rsquo;ll get used to it.&rdquo;</p>
<p>&ldquo;Take it
off!&rdquo; At this point I must have gestured, because the gun-arm
swung in an arc and nearly decapitated one of the men who had
attached it. &ldquo;I want it off!&rdquo; It made a rapid clicking
noise, like a nine-year-old riding a bike with playing cards stuck in
the spokes. I had seen a boy cruising down a hill like that once,
when I was a kid. I thought it was the coolest thing ever.</p>
<p>&ldquo;He&rsquo;s
firing!&rdquo;</p>
<p>Jason said, &ldquo;Dr.
Neumann! Please stop doing that. You&rsquo;ll damage the hammers.&rdquo;</p>
<p>&ldquo;I don&rsquo;t
want this! You hear me? I&rsquo;m not a super-soldier! I&rsquo;m not
doing this! I&rsquo;m not!&rdquo; The gun-arm rattled, stopped,
rattled, in spasms.</p>
<p>&ldquo;Dr.
Neumann&mdash;&rdquo;</p>
<p>&ldquo;<em>Take it
off!</em>&rdquo;</p>
<p>&ldquo;<em>Charlie!</em>&rdquo;
shouted Cassandra Cautery. I jumped, because a woman opening her
throat and letting rip: I didn&rsquo;t hear that very often. A
trickle of blood had escaped from her towel and was slowly following
the curve of her jaw. &ldquo;He took Lola.&rdquo; Her voice softened.
She almost sounded sympathetic. &ldquo;Carl took Lola.&rdquo;</p>
