<p>Carl the security guard sat in the back of the limo, on the seat
opposite me. His eyes were obscured by black sunglasses, so I
couldn&rsquo;t tell if he was staring. But it felt like he was.</p>
<p>&ldquo;You lied,&rdquo; Carl said. I jumped, because I had never
heard Carl speak before. It was like hearing a voice from a
refrigerator. &ldquo;You told that nurse you had prosthetics, plural.
You don&rsquo;t. You have one.&rdquo;</p>
<p>I felt uncomfortable. I tried to cross my legs, then remembered.</p>
<p>&ldquo;You&rsquo;re meant to be smart,&rdquo; said Carl. &ldquo;Why
didn&rsquo;t you wait until you had two new legs before you cut off
your other one?&rdquo;</p>
<p>I looked out the window. Later, I came to regret this. I should have
told him. But like I said, I&rsquo;m socially awkward. I couldn&rsquo;t
think how to phrase the answer, which was shock. You can only inflict
so much trauma on the human body before important parts of it
overload and stop working. Or, more accurately, so much trauma <em>at
once</em>. You need to stage it, with plenty of recovery time in
between. And, from a scheduling point of view, whenever you&rsquo;re
looking at a series of long, sequential tasks, you want to commence
the first one as soon as possible. That&rsquo;s not rocket science.
That&rsquo;s project management.</p>
<p>But at the time, I couldn&rsquo;t find the words. I said, &ldquo;No
reason.&rdquo;</p>
