<p>I had to bathe
three times, one after the other. At first, the water turned black as
soon I entered it. &ldquo;Sorry,&rdquo; I told Lola. &ldquo;I guess I
really stink.&rdquo;</p>
<p>&ldquo;I&rsquo;m
more impressed by the layer of grease.&rdquo; She pointed at the
water. &ldquo;Look at that. It&rsquo;s like an oil spill.&rdquo;</p>
<p>She held my hand to
help me out of the bath. She refilled the tub and scrubbed my back
with a cloth. When I was finally clean, she dried me with a fluffy
towel. &ldquo;Wait here,&rdquo; she said. When she came back, she was
carrying two prosthetic legs. &ldquo;These are nothing special...&rdquo;
She set them down, watching my face.</p>
<p>She was right. They
were the entry-level models she had shown me the first day we met:
two thin buckets on crutch-like poles. They were the kind of
prosthetics you saw on Vietnam veterans who had been abandoned by
their government.</p>
<p>&ldquo;They&rsquo;re
just temporary,&rdquo; said Lola. &ldquo;We can&mdash;&rdquo;</p>
