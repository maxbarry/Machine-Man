<h5>CHAPTER 
TWENTY-SIX</h5>
<p>I had to bathe
three times, one after the other. The first time, the water turned
black as soon I entered it. &ldquo;Sorry,&rdquo; I told Lola. &ldquo;I
guess I stink.&rdquo;</p>
<p>&ldquo;Actually,
I&rsquo;m more impressed by the layer of grease.&rdquo; She pointed
at the water. &ldquo;Look at that. It&rsquo;s like an oil spill.&rdquo;</p>
<p>I laughed, and we
looked at each other, and what I still could not really believe was
that her eyes held no vindictiveness. I had not known this about
love: that you did not have to deserve it. I&rsquo;d always thought I
was missing something, something essential, that left me unqualified.
Confidence, or a tan; something like that. And I was okay with that,
because at least I was being me. I wasn&rsquo;t pretending to be
someone else just to be loved. But here I was, the same pasty mess of
social incompetence as always, and here was Lola.</p>
<p>In retrospect, I
wonder if this could have been a turning point. It seems possible. I
could have emerged from that bath, scrubbed clean to my core, and
everything might have turned out differently. Lola and I could have
run away, left behind Better Future and artificial augmentation and
the arc welder in the garage, built new lives in a tiny Canadian snow
town, become a quirky couple, the woman and the man with a prosthetic
hand and prosthetic legs and he used to be a scientist once, did you
know? Lola could have baked pies. I could have repaired things. The
townspeople could have called me <em>Doc</em>.</p>
<p>No. I guess not.</p>
