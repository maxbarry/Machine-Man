bench were many very delicate hand-fabricated metal parts. They
didn&rsquo;t look like anything much at the moment. But this was my
hobby, on which I&rsquo;d worked in between my project work over the
last four weeks. As far as anyone else knew, it was project work. But
it wasn&rsquo;t. It was an idea I&rsquo;d had. A present. I was
