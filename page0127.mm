<p>But the morning was
better. When Lola padded off to the shower, clad in a borrowed
T-shirt that said <em>DINO-ROAR!</em>, I even tried the war veteran
legs by myself. My plan was to impress Lola. I got myself strapped in
and staggered into the corridor, taking huge, toddler-like steps and
bouncing off the walls. There were no dogs around. I should have
noticed that.</p>
<p>&ldquo;He <em>has</em>.&rdquo;
That was Lola. She had detoured to the living room, for some reason.
&ldquo;You&rsquo;ll see.&rdquo;</p>
<p>&ldquo;You
shouldn&rsquo;t have bought the welder. You&rsquo;re enabling him.&rdquo;
Angelica, of course. &ldquo;I swear to God, Lola. This ends with you
in tiny pieces.&rdquo;</p>
<p>&ldquo;Give him a
chance. You&rsquo;ll see. He&rsquo;s trying.&rdquo;</p>
<p>&ldquo;Trying to
get to the garage, I bet.&rdquo;</p>
<p>&ldquo;He&rsquo;s
