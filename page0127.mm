changing.&rdquo;</p>
<p>&ldquo;Tss,&rdquo;
Angelica said, or something similar. &ldquo;Believe that when I see
it.&rdquo;</p>
<p>At this point I
smiled, because I had formed a smug little plan. I would totter out
there on my pole legs. Angelica would be surprised. Lola would glance
at her like: <em>See?</em> And I would be all: <em>What?</em></p>
<p>I almost pulled it
off, too. I got one pole in front of the other. As I reached the end
of the corridor, I attained balance: I entered the living room
walking, actually walking, albeit somewhat zombie-like, my arms out
in front. Lola and Angelica turned toward me, their faces matching
portraits of surprise. It was perfect. Then Dog struck.</p>
