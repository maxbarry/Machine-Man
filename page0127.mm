<p>But the morning was
better. When Lola wrapped herself in a towel and went off to shower,
I even got up the courage to try the war veteran legs by myself. The
plan was to impress Lola. I got myself strapped in and staggered out
into the corridor, taking huge, exagerated steps, grabbing at the
wall for balance. There were no dogs around. I should have noticed
that.</p>
<p>&ldquo;He has.&rdquo;
I caught Lola&rsquo;s voice. She did not seem to be in the bathroom
after all. She was in the kitchen. &ldquo;You&rsquo;ll see.&rdquo;</p>
<p>&ldquo;You
shouldn&rsquo;t have bought the welder. You&rsquo;re enabling him.&rdquo;
That was Angelica, of course. &ldquo;I swear to God, Lola. This ends
with you in tiny pieces.&rdquo;</p>
<p>&ldquo;Give him a
chance. You&rsquo;ll see. He&rsquo;s trying.&rdquo;</p>
<p>&ldquo;Trying to
get to the garage.&rdquo;</p>
<p>&ldquo;He&rsquo;s
trying to walk on those, those <em>stupid</em> prosthetics. He really
is.&rdquo;</p>
<p>&ldquo;I&rsquo;ll
believe it when I see it,&rdquo; said Angelica.</p>
<p>At this point I
smiled, because I had a smug little plan. I would totter out there on
my pole legs. Angelica would be surprised. Lola would look at her
like: <em>See? Look how hard Charlie is working!</em> And I would be
all: <em>What?</em></p>
<p>I almost pulled it
off. I got one pole in front of the other. As I reached the end of
the corridor, I attained balance: I entered the kitchen walking,
actually walking, albeit somewhat zombie-like, my arms out in front.
Lola and Angelica turned toward me, their faces matching portraits of
surprise. It was perfect. My enemy sensed this, I think. Because in
this moment, Dog struck.</p>
