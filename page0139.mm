<h5>CHAPTER 
TWENTY-NINE</h5>
<p>I have always had a
strong focus. When something gets my attention, I forget about
everything else, like who I was just talking to or where I was
supposed to be soon. When I was six years old, I got distracted at my
own birthday party by the washing machine, which was new, and sat in
the laundry watching it tick through cycles until my mother found me
and asked what was I doing, everybody was going home. In high school
I was crossing the road when a beautiful girl walked by in the other
direction, and I didn&rsquo;t realize I was holding up traffic until
she turned around to see what all the honking was about. The look she
gave me makes my ears burn even thinking about it today.</p>
<p>It&rsquo;s useful,
of course. I&rsquo;m not sure I could have advanced this far in
engineering without it. But it&rsquo;s not always appropriate, is the
thing. And sometimes this doesn&rsquo;t bother me, because that
washing machine really was more interesting than the birthday party,
but other times I wish in retrospect that the shutters hadn&rsquo;t
closed. I wish I had retained enough self-awareness to realize I was
standing in the middle of the road gaping at a girl like an idiot. I
wish that when we cruised to a halt beside the elevator in the Better
Future parking lot, my brain left some room for thoughts besides <em>Will
I get parts now</em> and <em>I wonder if they made more Contours</em>
and <em>What did that mean, she&rsquo;s hoping to blow me away?</em>
Because the door opened and I saw a lab assistant with a wheelchair,
ready to bear me inside, and I totally forgot about Lola.</p>
