<p>When I got back to my bunk room, I
found a message taped to the door. It said: CAN I SEE YOU? LOLA.</p>
<p>I peeled the message off and slipped it
into a pocket. I had a bad feeling about this. The feeling was I was
about to get into trouble over Carl.</p>
<p>I knocked on the door to her suite.
After a moment, I realized I was holding my breath. Then the door
cracked open, and she smiled up at me. &ldquo;Hey, big guy!&rdquo;</p>
<p>&ldquo;Hi.&rdquo;</p>
<p>&ldquo;What&rsquo;s the matter? You
look upset.&rdquo;</p>
<p>&ldquo;Do I?&rdquo; I really needed
that helmet. &ldquo;I guess I&rsquo;m worried something&rsquo;s
wrong. Because of your note.&rdquo;</p>
<p>She blinked. &ldquo;What about my note
gave you the idea something&rsquo;s wrong?&rdquo;</p>
<p>&ldquo;You said you had to see me about
something.&rdquo;</p>
<p>&ldquo;No, I didn&rsquo;t. I just said
I wanted to see you.&rdquo;</p>
<p>&ldquo;Oh. Oh.&rdquo; I was so
relieved, I laughed a little. &ldquo;Well, that&rsquo;s okay.&rdquo;</p>
<p>Lola shifted from one foot to the
other. &ldquo;I want to see you, that means something must be wrong?&rdquo;</p>
<p>&ldquo;No, of course not. But if you
want to see me, you can drop by any time.&rdquo;</p>
<p>&ldquo;I do drop by. I drop by, like,
three times a day. But you&rsquo;re always at work. Or sleeping.&rdquo;</p>
<p>&ldquo;Really?&rdquo;</p>
<p>&ldquo;And I know you&rsquo;re busy.
You&rsquo;re doing really important stuff. But, well...&rdquo; Her
voice lowered. &ldquo;I want to see you, Charlie. That&rsquo;s why
I&rsquo;m here.&rdquo;</p>
<p>&ldquo;Is it?&rdquo;</p>
<p>&ldquo;Trust me,&rdquo; Lola said.
&ldquo;It&rsquo;s not for the free shirts.&rdquo;</p>
<p>&ldquo;Well,&rdquo; I said. &ldquo;I
want to see you.&rdquo;</p>
<p>&ldquo;Then come in.&rdquo; Her hand
closed on my shirt. And I grinned, because Lola liked me more than I
thought, and that was an intoxicating feeling. That was early-stage
romantic love, right there: the delirious discovery of your own
unexpected worth, through the eyes of another. I should map that, for
the helmet.</p>
