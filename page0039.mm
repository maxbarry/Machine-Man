<h5>CHAPTER 
NINE</h5>
<p>The place Lola Banks had arranged to meet turned out to be an
upmarket cafe. It had smoked glass and a deep green awning and
wrought iron furniture out on the sidewalk. I felt a twinge of doubt
about my ability to navigate this place without creating a scene, but
then I saw Lola through the glass, sitting at a table with her hair
in a pony-tail, and my heart jumped, and so did my legs. They jogged
up the step and inside almost before I could duck beneath the
doorway. A few people turned toward me, pasta dangling from their
forks, and I stopped, feeling self-conscious. I looked like a
vagrant, you may recall. A six-foot six, dust-covered vagrant with
titanium hooves. But Lola turned toward me and I stopped caring what
other people thought, because she was radiating joy.</p>
