<p>I wheeled back into Lola&rsquo;s room. No change: she was still lying
on her side, staring blankly at the window. No, not the window: the
wall near the window. That was even worse. I wheeled myself around in
front of her empty stare. &ldquo;Lola?&rdquo; Nothing. I moved
closer. I felt a little tense. Like she might leap up and bite me. I
think I was still shaky from the encounter with Cassandra Cautery,
where she had cried in the hallway. That had been a horror movie.</p>
<p>I
reached out and stroked Lola&rsquo;s hair. Still no reaction. &ldquo;Lola,
it&rsquo;s okay,&rdquo; I said. &ldquo;Everything&rsquo;s okay.&rdquo;
I began to relax. I stroked her hair for a while. Sometimes I said,
<em>Everything&rsquo;s
okay</em>.
I was soothing myself.</p>
<p>Lola&rsquo;s hand came up and closed over mine. And I looked down and
it was her again. Of course I knew her. What had I been thinking? I
felt closer to Lola than anyone I knew.</p>
<p>She started crying. It had been a hell of a day for tears. I felt
like joining in. But I held firm.</p>
