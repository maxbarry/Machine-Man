<p>I wheeled back into Lola&rsquo;s room. No change: she was still lying
on her side, staring at the window. Actually, not the window: the
wall near the window. That was even worse. I wheeled myself around in
front of her. &ldquo;Lola?&rdquo; Nothing. I moved closer. I felt
tense, like she might leap up and bite me. I was still shaky from
watching Cassandra Cautery cry in the hallway. That had been a horror
movie.</p>
<p>I
reached out and touched Lola&rsquo;s shoulder. Still no reaction.
&ldquo;Lola, it&rsquo;s okay,&rdquo; I said. &ldquo;Everything&rsquo;s
okay.&rdquo; I began to relax. I stroked her hair for a while.
Sometimes I repeated: &ldquo;Everything&rsquo;s okay.&rdquo; I was
soothing myself.</p>
<p>Just as I was drifting off, Lola&rsquo;s hand closed over mine. Our
eyes met. She was the person I knew again. Before I could even think
to bring it up, she said, &ldquo;I need to tell you about my heart.&rdquo;</p>
