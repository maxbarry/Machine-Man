<h5>CHAPTER 
ELEVEN</h5>
<p>I was by Lola&rsquo;s side when she woke. She took me by surprise,
even though I&rsquo;d been waiting for it: her eyelids fluttered and
two dozen tiny muscles contracted and suddenly her face looked
completely different. It was a little disconcerting, to be honest. I
hadn&rsquo;t watched many people sleep before. Any, now I think about
it. Maybe a couple of dogs. And they had not prepared me for this:
this sudden infusion of consciousness, where a second ago there had
been none. Not to insult dogs. But it was different.</p>
<p>&ldquo;Hey,&rdquo; I said softly. Lola reached for her nose, because
there were a couple tubes in there. I guided her hand away. &ldquo;You&rsquo;ve
had surgery. They had to fix your heart.&rdquo;</p>
<p>Her eyes widened. Her fingers moved down to her chest, touched the
bulge beneath her gown. &ldquo;Bah.&rdquo;</p>
<p>&ldquo;It&rsquo;s okay. Just try to relax.&rdquo;</p>
<p>&ldquo;Bah.&rdquo;</p>
<p>I leaned closer. &ldquo;What?&rdquo;</p>
<p>&ldquo;Bah.&rdquo; Her fingers closed around my shirt. &ldquo;Bah.&rdquo;</p>
<p>&ldquo;Don&rsquo;t exert yourself,&rdquo; I said, alarmed. &ldquo;You&rsquo;re
not supposed to raise your blood pressure.&rdquo;</p>
<p>She pulled. I went with it, because I feared that if I resisted, she
would pop her stitches. Her lips brushed my ear. &ldquo;<em>Back</em>,&rdquo;
she said. &ldquo;Put it back.&rdquo;</p>
