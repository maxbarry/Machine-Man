<p>Page 23.</p>
<p>Days passed and Lola Banks the
prosthetic specialist did not come. In this respect it was just like
my first time in the hospital: no-one visited. The difference was now
I wanted them to. Well, not <em>them</em>.
Her.</p>
<p>I was badly tempted
to ask the nurses. But I couldn&rsquo;t: since they had turned
hostile, letting them know I wanted something was a strategic
mistake. My meals were proof enough of that. But I couldn&rsquo;t
wait, either. On the fifth day, just as I was formulating a plan
involving dragging myself across the floor to the phone in the hall,
Lola appeared. She was not carrying any prosthetic limbs. It was just
her, in her too-big shirt and brown glasses. She hung in the doorway
like she was afraid to enter.</p>
<p>&ldquo;Hi,&rdquo; I
said.</p>
<p>She chewed her lip,
but didn&rsquo;t answer. Then she made a decision, and marched toward
me, but stopped two feet short of my bed. &ldquo;You crushed your own
leg.&rdquo;</p>
<p>&ldquo;Yeah.&rdquo;</p>
<p>She said, &ldquo;Why.&rdquo;
It wasn&rsquo;t a question. The word came out of her mouth like it
was heavy. It slid soundlessly to the floor and lay there.</p>
<p>I shrugged. It
seemed obvious. Lola had seen my prototype. She had touched it.</p>
<p>&ldquo;They think
you&rsquo;re suicidal,&rdquo; said Lola. &ldquo;They say you like
hurting yourself.&rdquo;</p>
<p>&ldquo;What?&rdquo;</p>
<p>She put her hands
on her hips. &ldquo;Well? What else are people meant to think?&rdquo;</p>
<p>&ldquo;That
I&rsquo;m improving myself! I&rsquo;m <em>augmenting</em>.
Do people have their vision laser-corrected because they like hurting
themselves? Is that why they pierce their ears, or... pluck their
eyebrows? What about people who hit the gym every day, enduring
short-term suffering in order to make their bodies to work better? Is
there anything wrong with that? The only difference is I have access
to better technology.&rdquo; Lola sucked in her breath, but I was too
riled up to stop. &ldquo;And incidentally, the reason I&rsquo;m in
pain isn&rsquo;t <em>my</em>
fault. It&rsquo;s because the biological design of the human body is
so conceptually flawed that the only way to make significant
improvements is to scrap what&rsquo;s there and start over.&rdquo;</p>
<p>I realized from
Lola&rsquo;s expression that I had gone too far. She began to move.
&ldquo;Wait,&rdquo; I said, but she was leaning closer, and before I
realized what was happening, she kissed me.</p>
