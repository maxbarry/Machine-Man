<p>Days passed and no-one visited. In this
respect it was just like my first time in the hospital. The
difference was now I wanted them to. Well, not <em>them</em>.
Her. I wanted to see Lola Banks barge in through the door, her arms
full of legs.</p>
<p>I was tempted to
ask the nurses to send for her. But I couldn&rsquo;t: since they had
turned hostile, letting them know I wanted something was a strategic
mistake. My meals were proof enough of that. But I couldn&rsquo;t
wait, either. On the fifth day, just as I was formulating a plan
involving dragging myself across the floor to the phone in the hall,
Lola appeared. She was not carrying any prosthetic limbs. It was just
her, in a too-big shirt and crooked glasses. She hung in the doorway
like she was afraid to enter.</p>
<p>&ldquo;Hi,&rdquo; I
said.</p>
<p>She chewed her lip.
Then she marched toward me, but stopped two feet short of my bed.
&ldquo;You crushed your own leg.&rdquo;</p>
<p>&ldquo;Yeah.&rdquo;</p>
<p>She said, &ldquo;Why.&rdquo;
The word came out of her mouth like it was heavy. It slid bonelessly
to the floor and lay there.</p>
<p>I shrugged. It
seemed obvious. Lola had seen my prototype.</p>
<p>&ldquo;They think
you&rsquo;re suicidal,&rdquo; she said. &ldquo;They think you like
hurting yourself.&rdquo;</p>
<p>&ldquo;<em>What?</em>&rdquo;</p>
<p>&ldquo;Well?&rdquo;
she demanded. &ldquo;What else are people meant to think?&rdquo;</p>
<p>I was
flabergasted. &ldquo;Do people get their vision laser-corrected
because they like hurting themselves? Is that why they pierce their
ears? People who work out at the gym, are they mentally ill because
they&rsquo;re prepared to endure a little short-term suffering in
order to make their bodies work better? The only difference between
them and me is I have access to better technology.&rdquo; Lola sucked
in breath, but I was too riled up to stop. &ldquo;And I don&rsquo;t
have pain because it&rsquo;s my <em>goal</em>.
I have pain because it&rsquo;s an unavoidable side-effect of the
human body being so conceptually flawed that the only way to
implement significant improvements is to scrap what&rsquo;s there and
start over.&rdquo;</p>
<p>From Lola&rsquo;s
expression, I realized I had gone too far. She began to move. &ldquo;Wait,&rdquo;
I said. &ldquo;Let me rephrase.&rdquo; But she was leaning closer.
Before I realized what was happening, she kissed me.</p>
