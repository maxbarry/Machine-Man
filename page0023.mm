<p>Days passed and Lola Banks the
prosthetic specialist did not come. In this respect it was just like
my first time in the hospital: no-one visited. The difference was now
I wanted them to. Well, not <em>them</em>.
Her.</p>
<p>I was
badly tempted to ask the nurses. But I couldn&rsquo;t: since they had
turned hostile, letting them know I wanted something was a strategic
mistake. My meals were proof enough of that. But I couldn&rsquo;t
wait, either. On the fifth day, just as I was formulating a plan
involving dragging myself across the floor to the phone in the hall,
Lola appeared. She was not carrying any prosthetic limbs. It was just
her, in her too-big shirt and brown glasses. She hung in the doorway
like she was afraid to enter.</p>
<p>&ldquo;Hi,&rdquo;
I said.</p>
<p>She
chewed her lip, but didn&rsquo;t answer. Then she made a decision,
and marched toward me, but stopped two feet short of my bed. &ldquo;You
crushed your own leg.&rdquo;</p>
<p>&ldquo;Yeah.&rdquo;</p>
<p>She
said, &ldquo;Why.&rdquo; It wasn&rsquo;t a question. The word came
out of her mouth like it was heavy. It slid soundlessly to the floor
and lay there.</p>
<p>I
shrugged. It seemed obvious. And I was pretty sure she already knew.
She had seen my prototype. She had touched it.</p>
<p>&ldquo;They
think you&rsquo;re suicidal,&rdquo; said Lola. &ldquo;They think you
like hurting yourself.&rdquo;</p>
<p>My
mouth dropped open. &ldquo;What?&rdquo;</p>
<p>&ldquo;What
else are people meant to think?&rdquo; There was anger in her eyes.</p>
<p>&ldquo;I&rsquo;m
<em>improving</em> myself. I&rsquo;m
<em>augmenting</em>. Do people
have their vision laser-corrected because they like hurting
themselves? Is that why people pierce their ears, or... pluck their
eyebrows? What about people who hit treadmills or do weights, are
they weird for wanting their bodies to work better? The only
difference between me and them is that I have access to better
technology.&rdquo; Lola sucked in her breath, but I was too riled up
to stop. &ldquo;And I&rsquo;d point out that the reason I&rsquo;m in
pain is not <em>my</em> fault.
It&rsquo;s because of the biological design of the human body, a
design so conceptually flawed that the only way to significantly
enhance parts of it is to scrap them and start from scratch.&rdquo;</p>
<p>I
realized from Lola&rsquo;s expression that I had gone too far. She
began to move; to leave, I thought. &ldquo;Wait,&rdquo; I said, but
she was leaning closer, and before I realized what was happening, she
kissed me.</p>
