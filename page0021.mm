<p>When I was fifteen, I was almost killed
by a shirtless man in a Dodge Viper. He came around the corner of the
suburban street I was crossing and accelerated toward me. I think he
expected me to scurry out of the way. But I didn&rsquo;t, because I
was fifteen, and it was more important to appear tough to strangers
than live. I think the shirtless man shared this philosophy, because
his car jagged toward me. I was sure I was about to die. I probably
would have, if the vehicle had been anything less than a triumph of
engineering. But at the last possible second&mdash;a second too late,
in a different car&mdash;the Viper slid to a smoking halt before me.</p>
<p>The driver leaned out the window and
screamed abuse. This was when I saw he was shirtless. He wore mirror
shades and chunky jewelery, which flew around as he gesticulated. He
stabbed fingers in my direction to emphasize points I couldn&rsquo;t
hear over the torrent of perfectly reproduced music pouring from his
stereo.</p>
<p>Eventually he finished, and the Viper
swerved around me. I watched it slingshot around the next corner. It
was lucky nobody was coming the other way.</p>
<p>I hefted my schoolbag and continued
walking. It occurred to me that we had, as a civilization, reached a
milestone. We had begun making better machines than people.</p>
