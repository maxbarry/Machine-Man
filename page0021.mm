<p>When I was fifteen, I was almost killed
by a shirtless man in an Audi X3. He came around the corner of the
suburban street I was crossing and accelerated toward me. I think he
expected me to scurry out of the way. But I didn&rsquo;t, because I
was fifteen, and it was more important to appear tough to strangers
than live. I think the shirtless man shared this philosophy, because
his car jagged toward me. I was sure I would die. If the vehicle had
been anything less than a triumph of modern engineering, I would
have. But at the last possible second&mdash;a second too late, in a
different car&mdash;the Audi came to a smoking halt before me.</p>
<p>The driver leaned out the window and
screamed abuse. This was when I saw he was shirtless. He wore mirror
shades and chunky jewellery, which flew around as he gesticulated. He
stabbed fingers in my direction to emphasize points I couldn&rsquo;t
hear over the torrent of perfectly reproduced music pouring from his
stereo.</p>
<p>Eventually the shirtless man finished
shouting and the Audi swerved around me. I watched it slingshot
around the next corner. If anyone had been coming, he would have
killed them.</p>
<p>As I hefted my schoolbag, I realized
that we had reached an important milestone. We had begun making
better machines than people.</p>
