<p>Then I had an
epiphany. I was on my stomach, straining to reach some titanium
pieces that had somehow wound up under the bed, and I thought: <em>These
are just metal</em>. I guess that doesn&rsquo;t sound very revelatory.
But it was. I stared at these pieces, which had once been the core of
my fingers, and they did not look like part of me.</p>
<p>I sat up. I would
never put the Contours back together: I knew that now. Previously,
this fear had paralyzed me, but now I mostly just felt relief. A part
of me still wanted to fix them, to try just one more time, but it was
a small part, receding, and I could ignore it. I looked down and
thought: <em>Man, I&rsquo;m a mess</em>.</p>
<p>I thought about
Lola. I had done so much damage there. I wasn&rsquo;t sure it was
repairable. A sickening thought struck me: maybe Lola was not even
around any more. Someone had been bringing me food, and listening
outside the door as I talked to my parts, but that could have been
Angelica. I wouldn&rsquo;t blame Lola for leaving me. It was the
rational thing to do.</p>
<p>Eventually, I
opened the door. Fresh air swept hit me like a slap. I saw a piece of
paper on the corridor floor: no, two pieces. One was a clipping from
a trade supplies catalogue. It featured an arc welder. The other was
a note, in Lola&rsquo;s handwriting. It said: <em>It&rsquo;s in the
garage.</em></p>
<p>I didn&rsquo;t
realize she was there until she spoke. &ldquo;You said...&rdquo; She
shifted from one foot to the other. &ldquo;I heard you needed one.&rdquo;</p>
<p>I opened my mouth.
My jaw worked. The things I wanted to say couldn&rsquo;t fit through
my throat. I tried to say, <em>I&rsquo;m sorry</em>, but all I did was
shake.</p>
<p>&ldquo;It&rsquo;s
all right,&rdquo; Lola said. &ldquo;It&rsquo;s okay, Charlie.&rdquo;</p>
