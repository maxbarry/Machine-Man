<p>Then I had an
epiphany. I was on my stomach, straining to reach some titanium
pieces that had somehow wound up under the bed, and I thought: <em>These
are just metal</em>. I guess that doesn&rsquo;t sound very revelatory.
But it was. I stared at these pieces, which had once been the core of
my fingers, and they did not look like part of me.</p>
<p>I sat up. I would
never put the Contours back together: I knew that now. Previously,
this fear had paralyzed me, but I mostly just felt relief. A part of
me still wanted to fix them, to try just one more time, but it was a
small part, receding, and I could ignore it. I looked down and
thought: <em>I&rsquo;m a mess</em>.</p>
<p>I thought about
Lola. I had done so much damage there. I wasn&rsquo;t sure it was
repairable. A thought struck me: maybe Lola was not even around any
more. Someone had been bringing me food, and listening outside the
door as I talked to my parts, but that could have been Angelica. Lola
could have left days ago. It would have been the rational thing to
do.</p>
<p>I pulled open the
door. Fresh air swept hit me like a slap. A piece of paper lay in the
corridor: a clipping from a trade supplies catalog. It was
advertising an arc welder. On it was a note, in Lola&rsquo;s
handwriting: <em>For you, in the garage.</em></p>
<p>I was still there
when she appeared at the top of the corridor. &ldquo;Oh,&rdquo; she
said. &ldquo;You... well, I heard you needed one.&rdquo;</p>
<p>I opened my mouth.
My jaw worked. But I couldn&rsquo;t find any words.</p>
<p>&ldquo;It took me a
while to get.&rdquo; She shifted from one foot to the other. &ldquo;I
hope it&rsquo;s the right kind.&rdquo;</p>
<p>I croaked: a low,
pathetic sound. I didn&rsquo;t know where to begin. &ldquo;I&rsquo;m
sorry.&rdquo;</p>
<p>&ldquo;It&rsquo;s
all right,&rdquo; Lola said. &ldquo;It&rsquo;s okay, Charlie.&rdquo;</p>
