<p>Finally I had an
epiphany. I was on my stomach, straining to reach some titanium
pieces that had somehow wound up under the bed, and I thought: <em>These
are just metal</em>. I guess that doesn&rsquo;t sound very revelatory.
But it was. I stared at these pieces, which had once comprised the
core of my fingers, and they did not look like part of me.</p>
<p>I sat up. I would
never put the Contours back together: I knew that now. Previously,
this idea had paralyzed me with fear, but now I just felt relieved.
Not completely. A part of me wailed, wanted me to try to fix them
just one more time, but it was a small part, and I ignored it. I
looked down and thought: <em>Man, I&rsquo;m a mess</em>.</p>
<p>I pushed my parts
into a heap and thought about Lola. I had done some damage there. I
wasn&rsquo;t sure it was repairable. In fact, I wasn&rsquo;t even
sure Lola was still around. Someone was bringing me food, and
sometimes listening outside the door as I talked to my parts, but
that could have been Angelica. I wouldn&rsquo;t blame Lola if she had
left. It was the rational thing to do.</p>
<p>Eventually, I
opened the door. Fresh air swept into the room. I saw a piece of
paper on the corridor floor: no, two pieces. One was a clipping from
a trade supplies catalogue. It featured an arc welder. The other page
was a note, in Lola&rsquo;s handwriting. It said: <em>Garage</em>.</p>
<p>I didn&rsquo;t hear
Lola&rsquo;s footsteps, because I was crying. She said: &ldquo;I
heard you say you needed one.&rdquo;</p>
<p>I opened my mouth.
My jaw worked. But the things I wanted to say couldn&rsquo;t fit in
my brain. All I could do was shake.</p>
<p>&ldquo;It&rsquo;s
all right,&rdquo; Lola said. &ldquo;It&rsquo;s okay, Charlie.&rdquo;</p>
