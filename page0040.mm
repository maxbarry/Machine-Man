<p>I made it to Lola&rsquo;s table without crushing anything. &ldquo;Hi,&rdquo;
I said. I was very happy to see her again. She was right: I <em>had</em>
been at the lab too long. I had forgotten what it felt like to
interact with people for the pleasure of it.</p>
<p>&ldquo;Hi.&rdquo; Her cheeks reddened. Cafe patrons were staring.
&ldquo;Have a seat.&rdquo;</p>
<p>&ldquo;I&rsquo;m already in one.&rdquo; I concentrated. The Contours
settled; pistons retracted. This still left me towering above the
table, but not as much.</p>
<p>Lola&rsquo;s mouth formed an O. I felt proud, to have caused that O.
&ldquo;Did you... have any trouble getting here?&rdquo;</p>
<p>&ldquo;No. Well.&rdquo; I reconsidered. &ldquo;Actually, I almost
killed some people. It&rsquo;s the first time the Contours have been
in a real-world environment. It&rsquo;s highlighted a few issues.&rdquo;</p>
<p>&ldquo;Contours?&rdquo;</p>
<p>&ldquo;This model leg I&rsquo;m wearing. I have others.&rdquo; Lola
inhaled. &ldquo;Well&mdash;when I say <em>legs</em>, I mean transports.
Most are tracks. One is a kind of drill.&rdquo;</p>
<p>&ldquo;Why is one a kind&mdash;&rdquo;</p>
<p>&ldquo;I don&rsquo;t know.&rdquo; I laughed. I felt giddy. &ldquo;The
company gave me carte blanche and I got carried away.&rdquo;</p>
<p>Lola&rsquo;s eyes stopped on my fingers. I had meant to keep that
hand out of sight. But I was nervous and it had started fiddling with
a salt shaker. My index finger was curled around the shaker three
times. It was made of rubber. Actually, it was a black hydrocarbon
polymer over titanium, silicon, and more copper than you might
expect. But to Lola, it would have looked like a long, flexible,
black rubber sausage.</p>
<p>I put my hand back under the table. Lola looked at me through her
eyelashes. When she spoke, her voice was low and throaty. &ldquo;Charlie...
what have you done to your fingers?&rdquo;</p>
<p>&ldquo;Well,&rdquo; I said. &ldquo;You know.&rdquo;</p>
<p>&ldquo;Show me.&rdquo;</p>
<p>I glanced around, but the nearby customers had returned to their
food, or pretended to. I placed my hand on the table.</p>
<p>Lola Banks sat rigid. She didn&rsquo;t seem to be breathing. &ldquo;Could
you show me your legs?&rdquo;</p>
<p>&ldquo;Yes, of course. Come back to Better Future and I&rsquo;ll show
you the collection.&rdquo; But I could see from her expression that
this was not what she meant.</p>
<p>&ldquo;Privately,&rdquo; she said.</p>
