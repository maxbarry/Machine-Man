<p>I made it to Lola&rsquo;s table without crushing anything. &ldquo;Hi,&rdquo;
I said. I was very happy to see her again. She was right: I <em>had</em>
been at the lab too long. I had forgotten what it felt like to
interact with people just for the pleasure of it.</p>
<p>&ldquo;Hi.&rdquo; Her cheeks reddened. Everyone in the cafe was
staring at us. She bit her lip. &ldquo;Take a seat.&rdquo;</p>
<p>&ldquo;No need.&rdquo; I concentrated, and the Contours began to
retract. When they finished, I was four foot tall. &ldquo;I&rsquo;m
already in one.&rdquo;</p>
<p>&ldquo;Did you...&rdquo; She swallowed. &ldquo;Did you make it here
okay?&rdquo;</p>
<p>&ldquo;Yes. No problems. Well.&rdquo; I reconsidered. &ldquo;Actually,
I almost killed someone. It&rsquo;s the first time I&rsquo;ve worn
Contours in a real-world environment. It&rsquo;s highlighted a few
issues.&rdquo;</p>
<p>&ldquo;Con... Contours?&rdquo;</p>
<p>&ldquo;This model of legs I&rsquo;m wearing. I have others.&rdquo;
Lola sucked in her breath. &ldquo;Well&mdash;when I say <em>legs</em>,
I mean transports. Most are more like tracks. One is a kind of
drill.&rdquo;</p>
<p>&ldquo;Why is one a kind&mdash;&rdquo;</p>
<p>&ldquo;I don&rsquo;t know.&rdquo; I laughed, giddy. &ldquo;The
company gave me carte blanche and I got carried away.&rdquo;</p>
<p>Lola&rsquo;s eyes stopped on my fingers. I had meant to keep that
hand out of sight. But I was nervous, and it had started fiddling
with a salt shaker. My index finger was curled around the salt shaker
three times. It was made of rubber. Actually, it was a black
hydrocarbon polymer stretched over titanium, silicon, and quite a lot
of copper. But to Lola, my fingers would have looked rubber. Like
long, thin, highly flexible black sausages.</p>
<p>Lola Banks stared at them. I became embarrassed and moved them under
the table. She looked at me through her eyelashes. When she spoke,
her voice was low and throaty. &ldquo;Charlie... what have you done
to your fingers?&rdquo;</p>
<p>&ldquo;Well,&rdquo; I said. &ldquo;You know.&rdquo;</p>
<p>&ldquo;Show me.&rdquo;</p>
<p>I glanced around, but the people eating at nearby tables were
ignoring us, or pretending to. I placed my hand on the table.</p>
<p>Lola Banks sat rigid for a long time. It was like she wanted to touch
my fingers, but didn&rsquo;t dare. &ldquo;Could you show me your
legs?&rdquo;</p>
<p>&ldquo;Of course. I&rsquo;d love to. Come back to Better Future and
I&rsquo;ll...&rdquo; I could see from her expression that this was
not what she meant.</p>
<p>&ldquo;Privately,&rdquo; she said.</p>
