<p>&ldquo;Lie down on the floor!&rdquo; Carl said. He looked nervous. I
didn&rsquo;t like Carl nervous. Not when he had a gun pointed at me.
&ldquo;Let her go and lie down!&rdquo;</p>
<p>Lola Banks squeezed my hand. She didn&rsquo;t look like she wanted to
be let go. There was fear in her eyes, which made me sad, because a
second ago she had looked so happy. She had been radiant, and Carl
had ruined it.</p>
<p>&ldquo;<em>Lie down!</em>&rdquo;</p>
<p>In retrospect, it was foolish to have imagined Better Future wouldn&rsquo;t
track me to the cafe. I had fled their premises wearing about eight
million dollars worth of equipment. I hadn&rsquo;t thought about how
that would look, from their perspective.</p>
<p>They didn&rsquo;t approach us, though. They hovered at a radius of
twenty feet, bristling with guns. It occurred to me that as far as
Carl knew, my legs were armed. Conceivably, I could have built in
some firepower. And in this moment, I wished I had. I wanted to sweep
them all away and carry Lola out of here.</p>
<p>&ldquo;Don&rsquo;t let them take you,&rdquo; Lola whispered. &ldquo;Don&rsquo;t
go back there, Charlie.&rdquo;</p>
<p>I heard a noise behind me, and turned my head. I was wrong about the
security radius: two were sneaking up on me.</p>
<p>&ldquo;Grab him!&rdquo; Carl said. &ldquo;Pull him out of the legs!&rdquo;</p>
<p>&ldquo;No!&rdquo; shouted Lola. She jumped forward and threw out her
arms. It was as if she was going to fly toward Carl and wrench his
gun away, or call down the wrath of the gods, or something. I don&rsquo;t
know. All I know is Carl pivoted and shot Lola twice in the heart.</p>
