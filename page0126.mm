<p>The night was
tough. I won&rsquo;t recount the whole sweating, groaning ordeal.
I&rsquo;ll just say it was one of the most horrible experiences of my
life. I speak as someone who has had both legs crushed in an
industrial clamp. These new prosthetics, the war veteran legs,
weren&rsquo;t realistic enough to fool my brain. They were poles.
When I waved them around, when I massaged them with shaking fingers,
my brain did not buy it. It had bought the Contours. It had bought
the Contours even when they were in pieces.</p>
<p>Around four, with
Lola was rubbing my back and telling me I could get through this, I
realized the pain was different. At Better Future, before I&rsquo;d
figured out to wear the Contours at night, the phantom pain had felt
distinctly like muscle cramp. Like toes I didn&rsquo;t have were
curling into shrieking clumps. This was a kind of pressure ache&mdash;a
lack of pressure ache, as if they were stretching out, inflating like
balloons. The problem here wasn&rsquo;t that I was missing my
biological legs. I had phantom Contours.</p>
