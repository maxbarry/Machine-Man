<p>In the 1970s, Better Future developed
depleted-uranium ordnance. This was of interest to several world
militaries and underpinned our expansion into other fields. We made
some amphibious tanks in the 80s but they didn&rsquo;t really work
out. In the 90s, we developed techniques for non-fatally removing
much more of a person&rsquo;s body than had previously been possible,
which was useful for treating various organ-specific afflictions,
including many types of cancer. More recently, we have had promising
results from labs engaged in neuro-stimulus response mapping,
lightweight metals fabrication, and sonics-based non-lethal weaponry.
We also sponsor a local softball team.</p>
