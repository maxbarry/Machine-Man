<p>At some point we
stopped. It wasn&rsquo;t until Lola climbed down off me that I could
see where: a leafy, suburban street. It took me a moment to recognize
it, and then I laughed. I had lived here, as a boy. I had thought
<em>away, make me safe</em>, and the Contours had taken me back to my
childhood. I felt touched.</p>
<p>Lola was looking at
me oddly. &ldquo;I used to live here,&rdquo; I explained. &ldquo;When
I was a boy.&rdquo;</p>
<p>She looked away.
&ldquo;You have blood on your chin.&rdquo;</p>
<p>&ldquo;Oh.&rdquo; I
tried to wipe it with my sleeve, but that had blood on it, too. This
struck me as funny. I had just outrun a whole bunch of security
guards. In fact, I had done that twice. And in between, I had jumped
eighty feet into the air and kicked a car. I started laughing again,
and then couldn&rsquo;t stop. When I wiped my eyes, Lola was staring
at me.</p>
<p>&ldquo;You&rsquo;re
different,&rdquo; she said.</p>
<p>I blinked.
&ldquo;Different how?&rdquo;</p>
<p>She shrugged,
looking off into the distance. &ldquo;Confident.&rdquo;</p>
<p>I felt confused.
&ldquo;I&rsquo;m more confident?&rdquo;</p>
<p>&ldquo;Yeah.&rdquo;</p>
<p>&ldquo;Because of
the legs?&rdquo;</p>
<p>&ldquo;I guess,&rdquo;
said Lola. &ldquo;I guess when you go leaping all over the place and
running at a hundred miles an hour, I guess you get confident.&rdquo;</p>
<p>I felt lost.
&ldquo;Confidence is a good thing, right?&rdquo;</p>
<p>&ldquo;I don&rsquo;t
know,&rdquo; said Lola. &ldquo;I liked you before.&rdquo;</p>
