<p>Her name was Lola Banks. She was
twenty-four and studying prosthetics part-time. I learned this as she
took me through her selection of artificial legs. &ldquo;Now, this is
entry-level,&rdquo; Lola said. &ldquo;I&rsquo;m showing you this just
so you know what&rsquo;s out there.&rdquo;</p>
<p>The lower section was a pole, exactly
like a crutch. The top was a metal bucket.</p>
<p>&ldquo;That&rsquo;s the socket,&rdquo;
Lola said. &ldquo;Don&rsquo;t underestimate the socket. Ninety
percent of your satisfaction with the prosthetic will derive from how
well you fit the socket.&rdquo;</p>
<p>I looked at her. Most people would have
said: <em>how well the socket fits you</em>.</p>
<p>&ldquo;But you
don&rsquo;t want that.&rdquo; She pulled it from my hands. &ldquo;Check
this out.&rdquo;</p>
<p>It resembled a real
leg, if you squinted. There was a band of metal around the knee and a
foot with odd, flat toes.</p>
<p>&ldquo;You can put a shoe on it. And I
can tell from your face that you don&rsquo;t think it&rsquo;s very
attractive, but imagine it under long pants. See the fullness? That
gives you a more natural look. You get practiced in this baby, most
people won&rsquo;t know you&rsquo;re packing plastic until you take
off your pants.&rdquo;</p>
<p>I returned the leg. &ldquo;I don&rsquo;t
really care about a natural look.&rdquo;</p>
<p>Lola&rsquo;s breath caught. &ldquo;<em>Really</em>.
Well, let me... just a minute.&rdquo; She came up with something
black and lightweight that I vaguely recognized from the Paralympic
Games. The foot was two arched prongs, like skis. &ldquo;This is an
Endolite Echelon on a microprocessor-controlled adaptive knee. Gives
you multi-axis rotation, polycentric swing, maximum energy return.
That heel, that&rsquo;s carbon polymer. It&rsquo;s the same model the
Olympics banned because they deemed it held an unfair advantage over
biological legs.&rdquo; Her eyes searched mine. &ldquo;You don&rsquo;t
seem excited.&rdquo;</p>
<p>&ldquo;It&rsquo;s nice. It&rsquo;s
just... I was hoping for something a little more...&rdquo; I searched
for the word. &ldquo;Cutting-edge.&rdquo;</p>
<p>&ldquo;Ha!&rdquo; Lola said, her eyes
lighting up. I realized I had made a pun. I have noticed that people
in the medical profession have dark senses of humor. To them, a joke
is never complete until it includes a defiled corpse, or spray of
blood. &ldquo;No, seriously. This is the best we have.&rdquo;</p>
<p>&ldquo;Oh,&rdquo; I said. &ldquo;Can I
think about it?&rdquo;</p>
<p>&ldquo;Sure,&rdquo; said Lola Banks.
She began gathering up her legs. When she left, I didn&rsquo;t
notice. I was already thinking about the lab.</p>
