<p>Her name was Lola Banks. She was
twenty-four and studying part-time. I learned this as she took me
through artificial legs. &ldquo;This is entry-level,&rdquo; Lola
said. &ldquo;I&rsquo;m showing you this just so you know what&rsquo;s
out there.&rdquo;</p>
<p>The lower section of the leg was a
pole, like a crutch. The top was a metal bucket.</p>
<p>&ldquo;That&rsquo;s the socket,&rdquo;
Lola said. &ldquo;Don&rsquo;t underestimate the socket. Ninety
percent of your satisfaction with the prosthetic will derive from how
well you fit the socket.&rdquo;</p>
<p>Her choice of words interested me. Most
people would have said <em>how well the socket fits you</em>.</p>
<p>&ldquo;But you
don&rsquo;t want this.&rdquo; She took it out of my hands. &ldquo;Look
at this instead.&rdquo;</p>
<p>It resembled a real
leg, if you squinted, maybe. There was a band of metal around the
knee and a foot with odd, flat toes.</p>
<p>&ldquo;That&rsquo;s so you can put a
shoe on it. And I can tell from your face that you don&rsquo;t think
it&rsquo;s very attractive, but bear in mind, if you wear long pants,
the fullness of this device gives you a more natural look than a
thinner unit.&rdquo;</p>
<p>I gave it back to her. &ldquo;I&rsquo;m
not especially interested in looking natural.&rdquo;</p>
<p>Lola&rsquo;s breath caught. &ldquo;<em>Really</em>,&rdquo;
she said. &ldquo;Well, let me... just a minute.&rdquo; She came up
with something black and lightweight that I vaguely recognized from
the Paralympic Games. The foot was two arched prongs, like skis.
&ldquo;This is the Endolite Echelon on a microprocessor-controlled
adaptive knee, for multi-axis rotation, polycentric swing, and
maximum energy return. That heel, that&rsquo;s carbon polymer. This
baby, this is the model the Olympics banned because it gave an unfair
advantage to prosthetic wearers over athletes with biological legs.&rdquo;
Her eyes searched mine. &ldquo;You don&rsquo;t seem excited.&rdquo;</p>
<p>&ldquo;No, it&rsquo;s nice. It&rsquo;s
just... I guess I was hoping for something a little more...&rdquo; I
searched for the word. &ldquo;Cutting-edge.&rdquo;</p>
<p>&ldquo;Ha,&rdquo; Lola said, her eyes
lighting up. I realized I had made a pun. I have noticed this about
people in the medical profession: they have dark senses of humor. To
them, a joke is never complete until it includes a defiled corpse or
spray of blood. &ldquo;No, seriously. This is the best we have. Which
model do you like best?&rdquo;</p>
<p>&ldquo;Can I have some time?&rdquo; I
was thinking about the lab.</p>
<p>&ldquo;Sure,&rdquo; said Lola Banks.</p>
