<p>Lola was silent as
the elevator descended. I risked a glance at her: she was staring
straight ahead, her arms stiff. &ldquo;Well,&rdquo; I said. &ldquo;Do
you think this elevator really takes us to the parking lot, or is it
a trap?&rdquo;</p>
<p>Lola said nothing.</p>
<p>I cleared my
throat. &ldquo;When the doors open&mdash;&rdquo;</p>
<p>&ldquo;Did you take
away Carl&rsquo;s arms?&rdquo;</p>
<p>&ldquo;I don&rsquo;t
think now is the best time to discuss this.&rdquo;</p>
<p>&ldquo;<em>Did you
make them take his arms?</em>&rdquo;</p>
<p>&ldquo;They weren&rsquo;t
his arms!&rdquo; I had more to this argument, but Lola&rsquo;s lips
thinned to a dramatic slash, and I decided it would be
counter-productive to advance any further. &ldquo;Let&rsquo;s talk
about this later.&rdquo;</p>
<p>The elevator
thumped to a stop. The doors seemed to take a long time to open. Lola
said, &ldquo;I&rsquo;m very disappointed, Charlie.&rdquo;</p>
<p>&ldquo;Okay.&rdquo;
I held out my arms. &ldquo;Come here.&rdquo; Her expression became
alarming, so I added, &ldquo;There&rsquo;s probably going to be some
running. I need to carry you.&rdquo;</p>
<p>The doors slid
apart. It took my eyes a moment to adjust to the contrast: the
darkness of the parking lot against the rectangle of briliant
sunlight pouring down the ramp. The shouting was clear enough,
though: phrases like <em>there he is</em> and <em>take him</em>.</p>
<p>Lola jumped into my
arms. Then there was some running.</p>
