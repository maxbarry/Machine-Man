<p>It was a good hospital; very exclusive.
The company spared no expense. Technically they were covered for
workplace accidents, but these things are never, well, <em>clean cut</em>.
A hint of neglience and they&rsquo;d be out two million bucks. Not to
mention the publicity, which, doing what we did, they were never keen
on. So I had a private room with a view over the Gardens. At dusk the
glass scrapers on the far side of the park radiated orange fire. At
ten each morning, my leg was bathed by beautiful nurses. There were
all the painkillers I could want. Nobody visited me. It was very
pleasant.</p>
