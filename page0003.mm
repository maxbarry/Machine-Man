<p>It was a good hospital; very exclusive.
The company spared no expense. Workplace accidents were always a big
deal at Better Future&mdash;or, rather, it was a big deal that nobody
make them into a big deal. Doing what we did, our public image was
tightly managed. The last thing the company wanted was a journalist
sniffing around, asking about our peculiar death and injury rate.</p>
<p>Also, I think they were worried I might
sue. I wasn&rsquo;t going to. It was my own fault. But I guess these
things are never... well, clean cut.</p>
<p>So I got a private
room with a view over the Gardens. At dusk the glass skyscrapers on
the far side of the park radiated orange fire. At ten each morning, I
was bathed by a beautiful nurse. There were all the painkillers I
could want. Nobody visited me. It was very pleasant.</p>
