<p>I said, &ldquo;Lola.&rdquo;</p>
<p>She looked
terrified, her skin gray and waxy. I felt faint stirrings of an
invisible force, and I realized something. I connected a few data
points. When this happened, when Lola turned magnetic, that was her
heart beating just below the EMP trigger. The first time it had
happened, at Better Future, Lola and I had been getting intimate. The
second time, I had leapt in via her balcony window to rescue her, and
taken her hand. At Angelica&rsquo;s, we had kissed: that time she had
actually detonated. And the final data point: she had triggered the
EMP while driving blind, fleeing from Better Future, and sideswiping
a parked car. Afterward, I thought she&rsquo;d said: <em>I thought we
hit a car</em>. But unless that was outlier, she had actually said: <em>I
thought we hit Carl</em>.</p>
<p>&ldquo;Lola.&rdquo;
I reached for her, with the arm that still listened to me.</p>
<p>If it were me&mdash;well,
if it were me when I still had an organic heart&mdash;I would no
doubt peak during moments of extreme personal danger. But Lola didn&rsquo;t
seem to do that. Her heart seemed to beat fastest when she feared for
someone she cared about. When I touched her.</p>
<p>&ldquo;Lola,&rdquo;
I said. &ldquo;Lola, kiss me.&rdquo;</p>
