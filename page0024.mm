<h5>CHAPTER  SIX</h5>
<p>I woke from dreams of being crushed to the sound of a baby crying.
This wasn&rsquo;t unusual, because there was some kind of intensive
care ward for newborns down the hall from my room. I had glanced in
one day, while cruising the hallways in my hospital-issue wheelchair,
to see rows of glowing pink- and brown- skinned babies, little limbs
clutching and flailing within plastic incubators. Sometimes
worried-looking couples sat in the chairs outside, clutching each
other. I stopped going that way, though, because one man looked at me
like: <em>Well, that puts things into perspective</em>.</p>
<p>The baby this morning sounded frightened. Then another joined it, and
another. Each seemed louder than the last, until I realized they were
just closer.</p>
<p>Cassandra Cautery appeared in my doorway. She was wearing a snug gray
jacket over a pinstripe shirt with a big collar, and a little skirt.
It was a schoolgirl-meets-Wall-Street look. Her eyelashes flashed,
like she was on the verge of tears. Her lips trembled with sympathy.</p>
<p>&ldquo;Oh,
Charlie,&rdquo; said Cassandra Cautery. &ldquo;You poor thing, look
at you.&rdquo; I could hardly hear her, over the babies&rsquo;
screaming.</p>
