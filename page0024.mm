<h5>CHAPTER  SIX</h5>
<p>I woke from dreams of being crushed to the sound of a baby crying.
This wasn&rsquo;t unusual, because there was some kind of intensive
care ward for newborns down the hall from my room. I had glanced in
one day, while cruising the hallways in my hospital-issue wheelchair,
to see rows of glowing pink- and brown- skinned babies, little limbs
clutching and flailing within yellow plastic incubators. Sometimes
couples filled the chairs outside, clutching each other. I stopped
going that way, though, when one man looked at me like: <em>Well, that
puts things into perspective</em>.</p>
<p>The baby this morning sounded frightened. Another joined it, then
another. Each crying baby seemed louder than the last, until I
realized they were just closer.</p>
<p>Cassandra Cautery appeared in my doorway. She was wearing a snug gray
jacket over a pinstripe shirt with a big collar, and a little skirt.
It was a schoolgirl-meets-Wall-Street look. Her eyes were huge and
glistened, as if she was on the verge of tears. Her lips trembled
with sympathy.</p>
<p>&ldquo;Oh, Charlie!&rdquo; she said. &ldquo;You poor thing.&rdquo; I
could hardly hear her over the wailing of the babies.</p>
