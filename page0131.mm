<p>Lola gave a shriek.
The Better Future van&rsquo;s rear doors banged open. Carl would be
in there. I knew it. We would try to squeeze past and Carl&rsquo;s
metal arm would shoot out and grab our bumper. Our little hybrid
wheels would smoke, the engine would shriek, and I would turn to see
vengeance burning in his eyes.</p>
<p>&ldquo;Go. Drive.&rdquo;
I braced myself against the impending acceleration. But there was no
acceleration. I looked at Lola. Her eyes were closed. &ldquo;Lola?&rdquo;</p>
<p>&ldquo;Zero, one,
one, two, three. Five. Eight. Thirteen.&rdquo;</p>
<p>&ldquo;Are you
reciting Fibonacci?&rdquo;</p>
<p>&ldquo;Twenty-one.
Thirty-four. Fifty-five.&rdquo;</p>
<p>&ldquo;Oh. Focusing
on calculations to avoid thinking about the stressful situation we
find ourselves in.&rdquo; Guards popped out of the van, grim-faced
and armed. &ldquo;That&rsquo;s good, I guess.&rdquo;</p>
<p>&ldquo;Eighty-nine.
One hundred thirty-four.&rdquo;</p>
<p>&ldquo;One hundred
<em>forty</em>-four.&rdquo;</p>
<p>&ldquo;<em>Shut up!</em>&rdquo;
Her eyes popped open, took in the guards, then squeezed shut again.
&ldquo;<em>Oh God!</em>&rdquo;</p>
<p>&ldquo;Sorry.
Sorry. It&rsquo;s just, if you&rsquo;re going to do Fibonacci...&rdquo;
I forced myself to stop this sentence. &ldquo;Okay. You recite
arbitrary numbers. I&rsquo;ll figure out how we operate a motor
vehicle when one of us can&rsquo;t see and the other can&rsquo;t
reach the pedals.&rdquo; Five guards, now. But still no Carl.</p>
<p>&ldquo;Tibialis
anterior. Extensor hallucis longus. Extensor digitorum longus.
Fibularis tertius.&rdquo;</p>
<p>I glanced at her,
because I suspected she was now reciting muscles I didn&rsquo;t have.
But this helped me solve our problem. I shouldn&rsquo;t think of us
as two people. It was more useful to consider us a collection of body
parts. Combined, we were one pair of eyes, two feet, three hands, two
brains; everything we needed. It was just a matter of resource
allocation.</p>
<p>I took hold of the
steering wheel. &ldquo;This is how we&rsquo;ll do it. I steer. You
keep your eyes closed and work the pedals when I tell you.&rdquo;</p>
<p>&ldquo;Triceps
surae!&rdquo;</p>
<p>&ldquo;I know. But
please depress the accelerator as far as it will go.&rdquo; 
</p>
<p>The guards began to
close in. &ldquo;Plantaris,&rdquo; said Lola, and stepped on the gas.</p>
