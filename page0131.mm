<p>Lola gave a shriek.
The van slewed to a halt diagonally across the driveway. Its rear
doors banged open and guards began to spill out of the van. I didn&rsquo;t
see Carl yet. But he would be there. I knew it. I had a vision of us
steering past the van and Carl&rsquo;s metal arm shooting out and
grabbing the bumper. Our little hybrid wheels smoking, our engine
shrieking, going nowhere. I would turn and look out the rear window
and he would be looking at me with those accusing eyes. &ldquo;Go!&rdquo;
I yelped. &ldquo;Drive!&rdquo;</p>
<p>I grabbed the side
door with my hand, bracing against the acceleration. But there was no
acceleration. I looked at Lola and she was sitting ramrod straight,
eyes closed.</p>
<p>&ldquo;Lola?&rdquo;</p>
<p>&ldquo;Just...
don&rsquo;t talk to me for a second.&rdquo; She took a shaky breath.
&ldquo;I think... I am just going to a calm, quiet place for a
minute. Because... we don&rsquo;t want me to kill the car. Right?&rdquo;</p>
<p>&ldquo;Um...&rdquo;
The guards fanned out in front of us, drawing guns. &ldquo;Right. But
we do need you to drive. Fairly soon.&rdquo;</p>
<p>Inhale, exhale.
&ldquo;Please don&rsquo;t say things like that, Charlie. Because...
right now... my heart is beating... really fast.&rdquo;</p>
<p>The Better Future
began to advance on the car, guns aimed at us. I wasn&rsquo;t sure I
wanted Lola seeing that. Which made it a puzzle: how to operate a
motor vehicle when one person had no legs and the other couldn&rsquo;t
see. As soon as I thought about it in those terms, the solution
became obvious. The trick was not to look at us like two people, but
a collection of resources: two eyes, three hands, four arms, two
hearts (one weaponized), and so on. I twisted around and retrieved my
war veteran legs from the rear. &ldquo;Okay. I think what we&rsquo;ll
do in a minute is swap. But right now, we just need to get out of
here, so keep your eyes closed and steer when I tell you.&rdquo;</p>
<p>&ldquo;Steer
when... what?&rdquo;</p>
<p>I angled one of the
leg poles through Lola&rsquo;s legs until it touched the accelerator.
Then I pushed it down, hard. The car leaped forward. &ldquo;<em>Left!
Steer left!</em>&rdquo;</p>
