<p>The streetscape
beyond the town car&rsquo;s tinted windows grew more familiar. We
passed an auto repair shop where I had once taken my car and learned
interesting things about diesel engines from a man who despaired at
the disposability of modern technology. A small bakery I had visited
twice a week for years, even though they were rude, because they made
a spectacular apple danish. The sidewalk I had fled down on the
Contours after inadvertently terminating the Manager.</p>
<p>&ldquo;The key word
now,&rdquo; said Cassandra Cautery, &ldquo;is clean-up.&rdquo; She
had been looking out the opposite window for a while, talking on a
cellphone too small to see, but now she smiled at me brilliantly.
Cassandra Cautery was a lot happier now we were getting closer to
Better Future. &ldquo;Or is that two words?&rdquo;</p>
<p>It occurred to me
that whatever plan Cassandra Cautery had, it was probably not in my
best interest. Which I guess should have been obvious, but I&rsquo;m
na&iuml;ve that way. When someone says they want to help me, I always
believe them. I have no siblings.</p>
<p>&ldquo;Once we&rsquo;re
inside,&rdquo; she said, &ldquo;I want you to start getting prepped
right away.&rdquo;</p>
<p>The Better Future
complex rose ahead. They had installed searchlights on the roof: I
could see them glinting in the sun.</p>
<p>&ldquo;This is
exciting. Don&rsquo;t you think? I can&rsquo;t wait to show you what
we&rsquo;ve done, Charlie. The progress we&rsquo;ve made.&rdquo; The
driver turned the car down the ramp toward the parking lot. The
opening gaped like a mouth. &ldquo;I&rsquo;m really hoping to blow
you away.&rdquo;</p>
<p>The parking lot
swallowed us. Yellow lights flicked past the windows. The drumbeat in
my chest that had begun the moment I saw the Better Future van, and
grown with every mile we drew nearer this building, became almost
painful: a thumping, like: <em>parts. Parts. Parts.</em></p>
<p>&ldquo;Welcome
home,&rdquo; said Cassandra Cautery. I didn&rsquo;t look at her,
because I didn&rsquo;t want her to see I was glad.</p>
