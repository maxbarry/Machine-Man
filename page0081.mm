<p>&ldquo;Charlie!&rdquo; She sounded
happy to hear from me. &ldquo;I&rsquo;m glad you called. I have your
parts.&rdquo;</p>
<p>&ldquo;What parts?&rdquo;</p>
<p>She laughed. I had never heard
Cassandra Cautery laugh before. It was high and girlish. I shivered.
&ldquo;Your arms. I got them back from Carl.&rdquo;</p>
<p>&ldquo;Oh. Thank you.&rdquo;</p>
<p>&ldquo;About before,&rdquo; she said.
&ldquo;I&rsquo;m sorry if I bit your head off. It&rsquo;s just, I
don&rsquo;t like surprises. I don&rsquo;t like to think I understand
a person, then have that person run off and do something completely
unexpected. It&rsquo;s a management thing. Well. It&rsquo;s a control
thing. You know what I&rsquo;m talking about. You have to be able to
control your parts.&rdquo;</p>
<p>&ldquo;Did I...&rdquo; I was
momentarily derailed by the thought of being one of Cassandra
Cautery&rsquo;s parts. &ldquo;Did I do something unexpected?&rdquo;</p>
<p>&ldquo;Not at all. So you want to take
orders from Lola. That&rsquo;s no problem. I just need to do a little
recalibration.&rdquo;</p>
<p>&ldquo;Orders?&rdquo;</p>
<p>&ldquo;Instructions. Requests. Notes.
Whatever. Let&rsquo;s not get into the nitty-gritty of who did what,
Charlie. The point is, it&rsquo;s your life, and you can live it how
you choose. It&rsquo;s not up to me to say which of your choices are
smart and which just create problems.&rdquo;</p>
<p>&ldquo;Am I creating problems?&rdquo;</p>
<p>&ldquo;No! Charlie, really, you need to
just calm down. Take some deep breaths. Relax. Know that I will solve
any and all problems before you even become aware of them.&rdquo;</p>
<p>&ldquo;Is Lola a problem?&rdquo;</p>
<p>Her voice dropped. &ldquo;You tell me.&rdquo;</p>
<p>&ldquo;No. Not for me.&rdquo;</p>
<p>&ldquo;All right.&rdquo; She took a
breath. &ldquo;So be it.&rdquo;</p>
