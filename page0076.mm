<p>I lay still while the fMRI machine
thrummed around me. At first this was unsettling, because you have to
lie back and poke your head into a small hole in a large machine. The
issue is the hole looks like a mouth. Then there&rsquo;s the noise,
and it&rsquo;s hard to forget the unit is generating enough magnetic
force to pull a pin clean through your body. But after a while, the
<em>whoomp-whoomp-whoomp</em> becomes comfortingly rhythmic. I felt
like I was in a womb.</p>
<p>&ldquo;That&rsquo;s good,&rdquo; said a
disembodied voice. It was Jason, my lab assistant. &ldquo;Now regret.
Something you wish you could change.&rdquo;</p>
<p>I thought a moment. &ldquo;An uncle of
mine died from colon cancer. I was about twelve. I remember thinking
how ridiculous it was, a failure in one small body part killing the
whole thing.&rdquo;</p>
<p>I had figured that as long as I was
here, I might as well map the whole brain. That would come in handy
later. Also, if things went like I planned, it would soon be
