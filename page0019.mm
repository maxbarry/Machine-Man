<h5>CHAPTER 
FIVE</h5>
<p>Kathline and
Jason, two of my lab assistants, arrived in the Glass Room around
eight. They were holding coffees and talking about something that was
apparently very amusing. Then they saw me through the perspex, and
froze.</p>
<p><em>Dr. Crux?</em>
said Kathline. I inferred this from her lip movements. I couldn&rsquo;t
hear because she hadn&rsquo;t toggled the intercom. I waited for her
to figure this out. &ldquo;Dr. Crux? What&rsquo;s in the syringe?&rdquo;</p>
<p>&ldquo;Morphine.&rdquo; This came out muffled because I was holding
my shirt sleeve in my teeth, but I think she understood. I completed
the injection and let my sleeve drop. &ldquo;For the pain.&rdquo;</p>
<p>Through the perspex, I saw Kathline and Jason share a glance. Jason
leaned toward the microphone. &ldquo;What pain is that, Dr. Crux?&rdquo;</p>
<p>I felt exasperated. These two were supposed to be amongst the
brightest of their generation. Yet here I was standing in the Clamp
with a syringe of morphine and they couldn&rsquo;t figure it out. &ldquo;I
think that will become obvious.&rdquo;</p>
<p>On the wall of the Glass Room was the Big Red Button. If you flipped
up the clear plastic panel and pushed it, everything lost power.
Once, long ago, it had been labeled EMERGENCIES ONLY, but now a
sticker said, DO! NOT!!! PUSH!, because lab assistants are curious.
Jason&rsquo;s eyes flicked toward the button.</p>
<p>&ldquo;Please call Medical,&rdquo; I said. &ldquo;Right away.&rdquo;</p>
<p>To his credit, Jason made it look like he was going for the phone. He
leaned in that direction, even picked up the handset. Then he dropped
it and lunged at the Big Red Button.</p>
<p>But my button was closer. It was in my hand. The Clamp was
powered-up, humming on standby. Its steel plates were positioned
about&mdash;well, about a foot apart, I guess. I was sitting on the
edge of a plate. My right leg, the biological one, dangled between
them.</p>
<p>It was just as well I took care of all this in advance, because the
morphine was already seeping into my neurons, fogging my synapses. If
I hadn&rsquo;t been well-prepared, Jason would have reached the Big
Red Button before I could activate the Clamp and crush my leg to
paste. But I was, and he didn&rsquo;t, and I did.</p>
