<p>They sent me out
through the underground parking lot, to avoid being seen by the many
emergency services people who were apparently above ground. At first
I didn&rsquo;t understand how the parking lot made any difference,
since it exited in the same general area, but I was no longer
worrying about details. I was prepared to assume Better Future would
take care of things while I focused on the big-picture goal of
running until I found Carl and then shooting him.</p>
<p>The parking lot was
lit by a portable generator and a series of halogen lights, which
made everything either blindingly bright or lost in impenetrable
shadow. Better Future vans and Hummers idled in the dark, their
chrome reflecting like supernovas, their tailpipes belching fumes.
&ldquo;Hold here a second, please,&rdquo; said Jason, and people
swarmed around me, performing final checks. Someone wrapped me in a
top, a security guard jacket with one arm cut off. Until that moment
I didn&rsquo;t realize I was technically naked.</p>
<p>My legs hiccupped
forward. &ldquo;Only another minute,&rdquo; said Jason. He thought it
was me. It wasn&rsquo;t. I could feel my legs&rsquo; impatience. Soon
they were going to start running whether these people had finished
their checks or not.</p>
<p>&ldquo;All clear,&rdquo;
said a lab tech. They began backing away. People climbed into vans;
doors slammed.</p>
<p>&ldquo;Wait.&rdquo;
This was Cassandra Cautery. She walked up to me. Her head was
bandaged. &ldquo;Before you go.&rdquo; Then she simply stood there. I
did not want to tell her to hurry this along, but unless she moved, I
was going to run through her. &ldquo;I feel like the old woman who
