<h5>CHAPTER 
THIRTY-THREE</h5>
<p>I clomped through
the Better Future corridors escorted by a group of guards, doctors,
and lab techs. Sometimes we passed other guards, doctors, and lab
techs, who stopped what they were doing to stare at me. From their
expressions, I was either an awe-inspiring technological miracle or
the worst thing they had ever seen. I wasn&rsquo;t sure myself. But
as we filed down the stairs, and the Contour Threes cantilevered
expertly on each step, I found myself coming around to the former.
There is something moving about a mechanical system that works
exactly like it&rsquo;s supposed to. I&rsquo;m not sure if everyone
feels that way. Maybe it&rsquo;s an engineering thing. But descending
those stairs with my hooves locking onto each step, I think it got
me.</p>
<p>So when we passed a
guy on the stairs who might or might not have been trying to hide his
revulsion, I stopped in front of him. Hands pulled at me, trying
futilely to tug me onward. I said, &ldquo;What?&rdquo;</p>
<p>&ldquo;Nothing.&rdquo;
A young guy in a dusty suit and tie: he could have been from any
department, except one that did real work. He looked everywhere but
at me.</p>
<p>&ldquo;Look at me.&rdquo;
I waited until he dragged his eyes to my face. Disgust, definitely.
&ldquo;You prick,&rdquo; I said.</p>
<p>Jason put a hand on
my metal forearm, as if he had any chance of influencing where I
chose to move. &ldquo;Dr. Neumann, please.&rdquo;</p>
<p>&ldquo;I&rsquo;m
going out there to save this company&rsquo;s ass. I&rsquo;m risking
my life to clean up the mess you guys made.&rdquo;</p>
<p>&ldquo;Shut him
down,&rdquo; someone said, and someone else said, &ldquo;No, wait.&rdquo;</p>
<p>&ldquo;Dr.
Neumann.&rdquo; Jason forced himself between me and the suit guy.
&ldquo;Please. This way.&rdquo;</p>
<p>&ldquo;I want some
respect.&rdquo; I felt myself choking up. &ldquo;I at least want some
<em>respect</em>.&rdquo;</p>
<p>&ldquo;We all
respect you very much,&rdquo; said Jason. &ldquo;Please. Please.
Let&rsquo;s go.&rdquo;</p>
