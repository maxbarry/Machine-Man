<h5>CHAPTER 
TWELVE</h5>
<p>&ldquo;The company didn&rsquo;t wise up
to what he was doing for a long time,&rdquo; said Lola. &ldquo;I
guess they couldn&rsquo;t believe anyone would do that to themselves.
Lose parts. He was down four toes, a thumb, an ear, and that hand
before they sent someone around asking questions. Like: Did my father
seem depressed? Had he talked about killing himself? You see, even
then, they had it all wrong. Dad had never been happier. When he
looked at me, I could see our secret twinkling in his eye. He never
had to tell me what he was doing. I just figured it out. I knew he
was doing it for me.</p>
<p>&ldquo;One night he was tucking me into
bed, and I said, &lsquo;Thank you.&rsquo; He looked at me and nodded
and his eyes were full of love, and I felt so special. Nobody else&rsquo;s
dad would do this for them. Nobody&rsquo;s.</p>
<p>&ldquo;We began to plan his accidents.
We had a little notebook. He showed me how he&rsquo;d been silly with
the hand, losing it all at once. He said, &lsquo;See, Lola, the sum
of the parts is greater than the whole.&rsquo; It was true. The
payout for death was a hundred thousand dollars, but if you added up
all the individual body parts, it came to a lot more. Even the hand,
if you lost it all at once, that was fifty thousand, but each finger
alone was worth between ten and fifteen, plus the thumb was up to
twenty. You could play the numbers to maximize your return.&rdquo;
She smiled a little at the memory. &ldquo;&lsquo;The sum of the parts
is greater than the whole.&rsquo; That&rsquo;s a good principle. I&rsquo;ve
held onto that, in prosthetics.</p>
<p>&ldquo;Anyway,&rdquo; Lola said. &ldquo;Mom
busted us. She must have been listening at the door. She burst in and
started screaming, grabbed the notebook. I ran out of the house. When
I came back, Mom sat me down and gave me a long talk about how my dad
was very ill. In the head, she meant. That made me angry. We were
never really the same, afterward. I couldn&rsquo;t stand her saying
that my dad only loved me this much because he was <em>sick.</em></p>
<p>&ldquo;She reported him to the company.
But he was crafty. He knew the factory&mdash;where the cameras were,
how the safeties worked. He managed to get in a few more claims. A
foot, an arm. Then the lower leg. But I think he knew it couldn&rsquo;t
go on. They were trying to transfer him, fire him, get him committed,
anything. They assigned a guy to follow him around.</p>
<p>&ldquo;So one night, the last night I
saw him alive, he comes into my room and sits on the bed. He says,
&lsquo;Lola, I love you so much.&rsquo; He leans down and kisses me
on the forehead, and it&rsquo;s longer than usual, like he&rsquo;s
going somewhere. Then he says, &lsquo;When you need a new heart,
you&rsquo;re going to have the best one there is.&rsquo;&rdquo;</p>
<p>Lola burst into tears. &ldquo;He was
crushed under an engine block.&rdquo; She cried quietly for a while,
and I waited, not daring to disturb her. &ldquo;By the time I turned
eighteen, the money, with that last hundred thousand dollars, it had
grown it to six hundred and twenty-two thousand dollars. And I told
the doctors I wanted something that would last forever, because my
dad had given everything for this. I wanted something steel. I wanted
my dad to live on in my heart for the rest of my life.&rdquo; Her
face contorted. Her fingers clutched at her chest. &ldquo;And they
took it out.&rdquo;</p>
