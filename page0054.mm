<h5>CHAPTER 
TWELVE</h5>
<p>&ldquo;The company didn&rsquo;t realize
what Dad was doing for a long time,&rdquo; said Lola. &ldquo;I guess
they couldn&rsquo;t believe anyone would do that to themselves. Lose
parts. He was down four toes, a thumb, an ear, and that hand before
they sent someone to the house, asking questions. Like: Did my father
seem depressed? Had he ever talked about killing himself? You see,
they still had it all wrong. Dad had never been happier. When he
looked at me, his eyes shone.</p>
<p>&ldquo;That was how I figured it out.
He was just so happy. One night he was tucking me into bed, and I
said, &lsquo;Thank you.&rsquo; He nodded, and his head was funny on
one side because of the missing ear, and his eyes were full of love.
I felt so special. I knew nobody else&rsquo;s dad would do this for
them. Nobody&rsquo;s.</p>
<p>&ldquo;It became our secret. He let me
help plan his accidents. We had a little notebook. He&rsquo;d been
silly with the hand, he showed me, losing it all at once. He said,
&lsquo;See, Lola, the sum of the parts is greater than the whole.&rsquo;
It was true. The payout for death was a hundred thousand dollars, but
if you added up all the individual body parts, it came to a lot more
than that. Even the hand, if you lost it all at once, that was fifty
thousand, but each finger alone was worth between ten and fifteen,
and the thumb was up to twenty. You could play the numbers. Maximize
your return.</p>
<p>&ldquo;It&rsquo;s a good principle.
&lsquo;The sum of the parts is greater than the whole.&rsquo; I&rsquo;ve
held onto that, in prosthetics.&rdquo;</p>
<p>Lola sighed. &ldquo;Then Mom busted us.
She must have been listening at the door. She burst in, screaming,
and grabbed the notebook. I ran out of the house. When I came back,
Mom sat me down and gave me a long talk about how my dad was ill. In
the head. That made me angry. I couldn&rsquo;t stand her saying that
my dad only loved me so much because he was <em>sick.</em></p>
<p>&ldquo;She reported him to the company.
But he was crafty. He knew the factory&mdash;where the cameras were,
how the safeties worked. He managed three more claims. A foot. His
arm. Then the leg. He was going bigger because he knew it couldn&rsquo;t
last. The company was trying to transfer him, fire him, get him
committed, anything. They assigned a guy to follow him around every
second of the day.</p>
<p>&ldquo;One night, the last night I ever
saw him, he came into my room on his crutch. He only had one, because
he only had one arm, so it was hard for him. And I felt scared,
seeing him hobbling like that, because it didn&rsquo;t seem like a
game any more. The fingers, the ear, even the foot, they hadn&rsquo;t
really slowed him down. They had seemed funny. But now it didn&rsquo;t.</p>
<p>&ldquo;He leaned down and kissed me on
the forehead. And it was longer than usual. So I knew that tomorrow
was another accident. I said, &lsquo;Dad, don&rsquo;t.&rsquo; He
smiled and said, &lsquo;It&rsquo;s okay.&rsquo; He said, &lsquo;Last
one. After tomorrow, when you need a new heart, you can have the best
there is.&rsquo;&rdquo;</p>
<p>Lola burst into tears. &ldquo;He was
crushed under an engine block.&rdquo; She cried quietly. &ldquo;He
didn&rsquo;t mean it. He just couldn&rsquo;t move fast enough. I
know, because I read the notebook. He was only trying to lose the
shoulder.</p>
<p>&ldquo;The company gave us one hundred
thousand dollars. The death payout. The whole, which was less than
the sum of the parts. Mom put it with the rest, and I guess she
invested wisely, because by the time I turned eighteen there was six
hundred thousand. I told the doctor I wanted something that would
last forever, because my dad had given everything for it. I wanted
something steel. I wanted my dad to live on in my heart for the rest
of my life.&rdquo; Her face contorted. Her fingers clutched at her
chest. &ldquo;And they took it out.&rdquo;</p>
