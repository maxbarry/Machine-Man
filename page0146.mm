<p>Panic swept through
me. It was not a good time to panic. It was time for some of that
clinical detachment. But tides of blood were gushing from a gaping
canyon in my flesh and my brain screamed, <em>That&rsquo;s fatal,
you&rsquo;re going to lose consciousness any second now</em>. I lifted
my right arm&mdash;I still had a right arm&mdash;without any real
plan for what to do with it and it was bright red. <em>They&rsquo;ve
skinned it!</em> my brain shrieked. And it did look like raw tendon
and muscle, but it wasn&rsquo;t, it was only soaked in blood. I was
lying in a shallow red bath. Blood ran over the sides to spatter on
the tiles below, like a water feature. And possibly this looked worse
because of the added volume of water falling from the ceiling, but
still: there was way too much blood here. I should already be dead.</p>
<p>I looked down. My
legs weren&rsquo;t right. By which I mean, I had some. I was lying
beneath saturated green surgical cloth, and this was, well,
contoured, in a definite legs-and-torso shape. Tubes ran from between
layers in the cloth to devices nearby: a large black box on a
trolley, two different drips. The box was making the slooshing sound
I&rsquo;d heard earlier. With every sloosh, the tubes connected to it
bucked a little, dark fluid moving through them.</p>
<p>The second I
decided this was keeping me alive, the box stopped slooshing and
started sounding like an enthusiastic six-year-old chasing the last
of his milkshake. As I watched, beige spots appeared around where the
tube was connected to the box, and rapidly elsewhere along it, racing
toward my body.</p>
<p>I grabbed at my
mostly-severed arm and tried to mash it back on. Now, I&rsquo;m not
particularly squeamish, but seriously, this was disgusting. I had no
feeling there; it was like handling a steak. And the sounds&mdash;it
wasn&rsquo;t the squishing that got me, the sucking or squelching. It
was the rasping. I almost couldn&rsquo;t do it. But I didn&rsquo;t
want to die. So I did.</p>
<p>Blood squirted from
the wound. I couldn&rsquo;t seal it properly; of course I couldn&rsquo;t.
&ldquo;Help,&rdquo; I said, just in case whoever I&rsquo;d heard
coughing before wanted to step in. But no-one came. This was it, I
realized: I was actually about to cease living. I felt terror and
dizziness and regret. I felt cheated and angry at someone or
something. I looked around one last time, in case there was something
useful nearby I hadn&rsquo;t noticed earlier, like maybe a surgeon,
and my eyes fell again on the electric saw.</p>
