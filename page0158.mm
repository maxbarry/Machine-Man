<p>One time in
university, I failed to buy a pack of Doritos from a vending machine.
I fed my bills into the slot, pressed C then 4, and nothing happened.
So I pressed the buttons again, more firmly, then CANCEL, then as
many buttons as I could press at the same time. Then I cursed and
slapped it, because I was 19 and there were people watching. I said,
&ldquo;This <em>stupid</em> machine.&rdquo;</p>
<p>Hours later, I was
walking by to check my mail, and another guy was there. I opened my
mouth to tell him the machine was busted, but he&rsquo;d already
inserted his money. He slapped the side of the machine, just as I
had, and said: &ldquo;This <em>stupid</em> machine.&rdquo;</p>
<p>That bothered me. I
guess it&rsquo;s always a shock to discover you are not quite as
unique and unpredictable as you thought. But what stuck out was not
only my similarity to this guy, but to the vending machine. I found
it difficult to justify to myself why I should be considered the
independent entity in this situation, exerting my free will to elicit
mechanical reactions from a vending machine, and not the other way
around. To a truly objective observor, wasn&rsquo;t it equally likely
that the vending machine was choosing to withhold snacks in order to
extract the predictable human response of a slap and a curse?</p>
<p>Once I tried to
raise this with a Philosophy major, during a floor party. &ldquo;You&rsquo;re
a determinist.&rdquo; Her tone implied this was kind of cute, like
believing in the Tooth Fairy. &ldquo;Shock. A determinist science
geek.&rdquo; We argued a while, which was frustrating because people
from the Humanities only want to play word games, and then she went
to make out with a guy I didn&rsquo;t know. I left the party and went
down to the vending machine. I sat on the floor for about an hour,
just watching. I was a little drunk. I wasn&rsquo;t sure what I hoped
to see. I just felt we had a lot in common.</p>
