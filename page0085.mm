non-lethal? Of course you don&rsquo;t. Because he managed it.&rdquo;</p>
<p>As a general rule,
I did not pay attention to company communications. They only made me
aware of inconvenient obligations. But now Cassandra Cautery
mentioned it, <em>The Manager</em> did sound familiar. I think his name
had come up in a few of Jason&rsquo;s lurid stories, the ones he told
to impress other lab assistants. They ended like: <em>and nobody ever
saw them again</em>. &ldquo;The Manager.&rdquo;</p>
<p>&ldquo;Exactly.&rdquo;
She slipped one thumbnail into her mouth and began gnawing at it.
&ldquo;The Manager.&rdquo;</p>
