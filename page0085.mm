<p>&ldquo;That&rsquo;s
his title?&rdquo;</p>
<p>&ldquo;Of course
not.&rdquo; She began pacing around the chairs. &ldquo;His title is
Chief Executive Officer. But everyone calls him the Manager. Because
that&rsquo;s what he does. He manages. You know when Congress wanted
