<p>The beautiful girl
led Cassandra Cautery and I to what I can only call a sitting room. I
say this as someone who is not totally sure what a sitting room is.
But this room was straight from an eighteenth Century Victorian
mansion: heavy drapes, busy wallpaper, and chairs with curving,
detailed legs. Turned, I think is the word. I felt elegant just being
here.</p>
<p>&ldquo;You know who
we&rsquo;re meeting,&rdquo; said Cassandra Cautery, once the girl had
