how to get you there. That&rsquo;s basic encapsulation.&rdquo;</p>
<p>Lola looked back
at the leg. I don&rsquo;t think she really understood encapsulation.
But she was impressed with my leg. She knelt on the floor before it.
The leg was under a lab spotlight, looking a little like a steel
vase. It bulged at the bottom, because the foot was the size of a
backpack, and tapered to a slimmer thigh. My lab assistants had
performed excellent work in metals fab this week. I had never seen
them so motivated.</p>
<p>Lola reached out
and touched the leg. I cleared my throat. &ldquo;What do you think?&rdquo;</p>
<p>Her fingers ran
down the metal. &ldquo;It&rsquo;s the most beautiful thing I&rsquo;ve
ever seen.&rdquo;</p>
<p>It was a moment
before I could speak. &ldquo;Thanks,&rdquo; I said.</p>
