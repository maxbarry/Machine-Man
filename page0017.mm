<p>&ldquo;Oh,&rdquo;
Lola said. &ldquo;I see.&rdquo;</p>
<p>She walked in a
circle around my prototype leg three times. She stopped beside the
Clamp, the big pre-fab die stamp that had crushed my old leg,
although she didn&rsquo;t know that. The Clamp was gleaming clean.
You didn&rsquo;t leave machinery of that caliber idle just because
some idiot managed to lose a limb in it. It was expensive as all
get-out.</p>
<p>&ldquo;It looks
heavy.&rdquo;</p>
<p>&ldquo;It&rsquo;s
about 180 pounds,&rdquo; I admitted. &ldquo;It tends to dent the
floor.&rdquo;</p>
<p>&ldquo;How do
you lift it?&rdquo;</p>
<p>&ldquo;I don&rsquo;t.
It walks by itself.&rdquo;</p>
<p>Lola looked at
me.</p>
<p>&ldquo;The foot
rotates. It&rsquo;s essentially an orbital wheel, on a shifting
multi-dimensional axis.&rdquo; I wanted to tell her its top speed,
but didn&rsquo;t, because she&rsquo;d think I was boasting.</p>
<p>&ldquo;And
what&rsquo;s this?&rdquo; She pointed at a squat black box, which
protruded from one side.</p>
<p>&ldquo;Oh. The
