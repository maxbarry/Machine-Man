<h5>CHAPTER 
TWENTY-FOUR</h5>
<p>&ldquo;They asked
if I&rsquo;d heard from you.&rdquo; Angelica reached across the table
and speared a potato.</p>
<p>Lola froze, a
forkful of lettuce halfway to her mouth. &ldquo;Who?&rdquo;</p>
<p>&ldquo;Steve
Molonovich.&rdquo;</p>
<p>&ldquo;Who&rsquo;s
Steve Molonovich?&rdquo;</p>
<p>&ldquo;He&rsquo;s
in Client Services.&rdquo;</p>
<p>&ldquo;Never heard
of him.&rdquo;</p>
<p>&ldquo;Well,&rdquo;
Angelica said, &ldquo;Steve Molonovich is going around the hospital
asking if anyone&rsquo;s heard from you.&rdquo;</p>
<p><em>Hello?</em> I
said to my legs. <em>Can you hear me?</em></p>
<p>Lola chewed her
salad. &ldquo;Because of Better Future?&rdquo;</p>
<p>&ldquo;Probably,&rdquo;
said Angelica. &ldquo;He had this look on his face like he gets when
a corporate client really wants something. Like he&rsquo;s halfway
through a bad hot dog.&rdquo;</p>
<p>&ldquo;What did you
tell him?&rdquo;</p>
<p>&ldquo;I said I
hadn&rsquo;t heard from you.&rdquo;</p>
<p>&ldquo;He believed
you?&rdquo;</p>
<p>Angelica shrugged.
&ldquo;I don&rsquo;t know.&rdquo;</p>
<p><em>I think you can</em>,
I told them.</p>
<p>&ldquo;I don&rsquo;t
like that.&rdquo; Lola glanced at me. I remembered I was supposed to
be eating, and stabbed a carrot. This entailed bending down a little,
because even with the Contours lowered as far as possible, I sat a
good foot higher than everyone else at the table. &ldquo;I don&rsquo;t
know what we should do.&rdquo;</p>
<p>&ldquo;I&rsquo;ve
told you what you should do.&rdquo; Angelica bent down and scooped up
a dog that had been whining at her feet. Its bright eyes fixed on me
across the table cloth. &ldquo;Go to the Feds. Report that Better
Future has built itself an unlicensed operating theater, and they&rsquo;ll
be raided within 24 hours, I guarantee it.&rdquo;</p>
<p><em>Give me a sign. A twitch. A stretch.</em></p>
<p>&ldquo;I bet half
the stuff they&rsquo;re doing in there is illegal.&rdquo; Angelica
stroked the dog&rsquo;s ears. &ldquo;The Feds get wind of it, I bet
they seize a whole bunch of stuff nobody has permits for.&rdquo; Her
eyes flicked at me, at my Contours.</p>
<p>My right knee
bounced up and hit the underside of the table. The entire setting
jumped an inch into the air. Before it clattered back down, the dog
bolted from Angelica&rsquo;s lap. From the hallway it stared back at
me, quivering.</p>
<p>&ldquo;Charlie?&rdquo;
Lola reached for my hand. &ldquo;You okay?&rdquo;</p>
<p>&ldquo;Yes. Sorry,&rdquo;
I said. &ldquo;I&rsquo;m fine.&rdquo;</p>
