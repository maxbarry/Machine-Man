<p>I felt very
excited after Lola Banks left the lab. She was the first person I&rsquo;d
shown my prototype leg, and her reaction exceeded my wildest hopes.
To be honest, I had been bracing myself for her to be grossed-out.
And maybe most people would have been&mdash;would still be. But not
Lola. She had knelt beside the leg. She had touched it. Gently.
Reverentially. Almost affectionately. I thought about that for a long
time.</p>
<p>It was getting
late. My three lab assistants had left a long time ago. I was
probably the last person down here. But I didn&rsquo;t feel tired. I
looked down at my leg, the good one. Well. &ldquo;Good&rdquo; was the
wrong word. I mean the one I&rsquo;d had since birth. I rolled up my
pants and turned it this way and that. It looked fat, and weak, and
ordinary. The more I looked at it, the more it bugged me.</p>
