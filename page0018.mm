<p>After Lola left,
I sat alone in the lab, thinking. I remembered how her hands had run
over the prototype&rsquo;s metal; how they had caressed it. I was
very pleased that Lola liked my new leg, but I felt unsettled, too.
For a while I thought it was just that I was almost finished, because
I always get a little despondent when a project is coming to a close.
I tend to look around for ways to tinker, add a few last-minute
features. To draw it out.</p>
<p>But that wasn&rsquo;t
it. What gnawed at me was how Lola&rsquo;s eyes had fixed on the
prototype. She had practically forgotten I was in the room. And you
couldn&rsquo;t expect any different, I guess, from someone confronted
with a 180-pound nuclear-powered polycarbonate prosthetic. But still.
She had looked at it like a device. Like a product, to be fitted to
whoever needed it. But that&rsquo;s not how I had intended it. I had
built it to be part of me.</p>
<p>It was getting
late. I was probably the only person still in the labs. But I didn&rsquo;t
feel tired. I looked down at my right leg, the one I&rsquo;d had
since birth. I rolled up my pant leg, and turned it this way and
that. The more I studied it, the more it bugged me.</p>
