<p>After a couple
hours, the door to my little cell opened and there was Carl. At
first, neither of us spoke. It was an unusual social situation. In
the last six hours, I had fled him on artificial legs and he had shot
Lola Banks in the heart. I didn&rsquo;t know what you said after
something like that.</p>
<p>&ldquo;You can see
her now,&rdquo; Carl said. &ldquo;If you want.&rdquo; In the hallway,
a wheelchair awaited. &ldquo;She&rsquo;s in surgery. But you can
watch.&rdquo;</p>
<p>I opened my mouth
to say, <em>Okay</em>, but nothing came out. I felt dizzy. All
Cassandra Cautery had said before leaving was that Lola was receiving
the best medical care the company had to offer. That could have meant
they were zipping her into a body bag.</p>
<p>Carl came forward,
his arms out to scoop me up. I tried to push him away, because I
wasn&rsquo;t ready for Carl to touch me. It was going to take a lot
before that would be okay. But he had arms like tree trunks, and I
was weak and shaky and missing five fingers. He lifted me off the
bed. Against Carl&rsquo;s rock-hard pectorals, I began to cry. It was
a post-traumatic reaction. I had been through a lot.</p>
<p>&ldquo;She&rsquo;s
going to be okay, I think,&rdquo; Carl said.</p>
<p>The sympathy in his
voice unmade me. Suddenly I was blubbering helplessly. Maybe Carl was
a good man. A good man, in a tough job. Maybe he had never hated me.
Maybe he had, but only because I had walked past him a thousand times
and never said hello, treating him like a piece of furniture. Maybe
this was a new beginning for us. Maybe we would be best friends.</p>
<p>&ldquo;It was only
NLA,&rdquo; Carl said. &ldquo;Non-lethal ammunition. I wouldn&rsquo;t
have fired otherwise.&rdquo;</p>
<p>I stopped crying. I
had a passing familiarity with our company&rsquo;s munitions line, so
knew we preferred a strict definition of <em>non-lethal</em>. The term
we used for weaponry that left the target not merely alive but with a
solid chance of regaining full quality of life was <em>non-crippling</em>.
I hated Carl. I hated him so much.</p>
