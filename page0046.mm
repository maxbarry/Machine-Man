<p>After a couple
hours, the door to my little cell of a medical room opened and there
was Carl. At first, neither of us spoke. It was an unusual social
situation. In the last six hours, I had fled him on artificial legs
and he had shot Lola Banks in the heart; I didn&rsquo;t know what you
said after something like that.</p>
<p>&ldquo;You can see
her,&rdquo; Carl said finally. &ldquo;If you want.&rdquo; I noticed a
wheelchair in the hallway. &ldquo;She&rsquo;s in surgery. But you can
watch.&rdquo;</p>
<p>I opened my mouth
to say, <em>I do</em>, but nothing came out. I felt dizzy. All
Cassandra Cautery had told me was that Lola was receiving the best
medical care the company had to offer. That could have meant they
were zipping her into a body bag.</p>
<p>Carl came toward
me, his arms out to scoop me up. I tried to push him away, because I
wasn&rsquo;t ready for Carl to touch me. It was going to take a lot
before that would be okay. But he had arms like tree trunks, and I
was weak and shaky and missing five fingers, and he wrapped me up and
lifted me off the bed. Against Carl&rsquo;s rock-hard pectorals, I
began to cry. It was a post-traumatic reaction. I had been through a
lot.</p>
<p>&ldquo;She&rsquo;s
going to be okay,&rdquo; Carl said, as he carried me to the
wheelchair. &ldquo;It was NLA. Non-lethal ammunition. I was never
going to kill her.&rdquo;</p>
<p>I had a passing
familiarity with our company&rsquo;s munitions line, and knew we
preferred a strict definition of <em>non-lethal</em>. The term we used
for weaponry that left the target not just alive but with a solid
chance of regaining full quality of life was <em>non-crippling</em>.
But I didn&rsquo;t say anything. I was just so happy she was still
alive.</p>
