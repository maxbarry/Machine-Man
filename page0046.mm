<p>After a couple
hours, the door to my little cell of a medical room opened and there
was Carl. At first, neither of us spoke. It was an unusual social
situation. In the last six hours, I had fled him on artificial legs
and he had shot Lola Banks in the heart. I didn&rsquo;t know what you
said after something like that.</p>
<p>&ldquo;You can see
her,&rdquo; Carl said finally. &ldquo;If you want.&rdquo; I noticed a
wheelchair waiting in the hallway. &ldquo;She&rsquo;s in surgery. But
you can watch.&rdquo;</p>
<p>I opened my mouth
to say, <em>I do</em>, but nothing came out. I felt dizzy. All
Cassandra Cautery had told me was that Lola was receiving the best
medical care the company had to offer. That could have meant they
were zipping her into a body bag.</p>
<p>Carl came forward,
his arms out to scoop me up. I tried to push him away, because I
wasn&rsquo;t ready for Carl to touch me. It was going to take a lot
before that would be okay. But he had arms like tree trunks, and I
was weak and shaky and missing five fingers, and he wrapped me up and
lifted me off the bed. Against Carl&rsquo;s rock-hard pectorals, I
began to cry. It was a post-traumatic reaction. I had been through a
lot.</p>
<p>&ldquo;She&rsquo;s
going to be okay,&rdquo; Carl said. &ldquo;She&rsquo;ll be just
fine.&rdquo;</p>
<p>The sympathy in his
voice unmanned me. I blubbered helplessly. Maybe Carl had never hated
me. Maybe he had, but only because I had walked past him a thousand
times and never said hello, as if he were a piece of furniture. Maybe
Carl and I would come out of this with a whole new understanding.
Maybe we would be best friends.</p>
<p>&ldquo;It was NLA,&rdquo;
Carl said. &ldquo;Non-lethal ammunition. I was never going to kill
her.&rdquo;</p>
<p>I stopped crying. I
had a passing familiarity with our company&rsquo;s munitions line, so
knew we preferred a strict definition of <em>non-lethal</em>. The term
we used for weaponry that left the target not merely alive but with a
solid chance of regaining full quality of life was <em>non-crippling</em>.
I hated Carl. I hated him so much.</p>
