<p>First I headed for
the cafeteria. This was a decoy, to throw off surveillance. I really
wanted to check out that operating theater, to see if Carl was in it,
or had been recently. But going there directly from Cassandra
Cautery&rsquo;s office might have aroused suspicion. So I caught an
elevator to ground floor, swiped my way through the doors, and stood
in line to buy a muffin.</p>
<p>There were three
people ahead of me, one a young blond guy with glasses who kept
turning around to look at me. Finally he blurted: &ldquo;Dr. Neumann,
I just want to say you&rsquo;re my idol.&rdquo;</p>
<p>&ldquo;What?&rdquo;
I said. Because I thought I must have misheard <em>idol</em>.</p>
<p>&ldquo;You and
Isaac Newton,&rdquo; the kid said. &ldquo;And Barry Marshall. And the
Curies. You guys who put yourselves on the line. Become your own test
subjects. I salute you.&rdquo;</p>
<p>&ldquo;Oh,&rdquo; I
said. &ldquo;Thanks.&rdquo;</p>
<p>&ldquo;Compared to
you, Kevin Warwick is a <em>pussy</em>.&rdquo; Behind the counter, the
cafeteria girl waited for the kid to realize it was his turn, but I
couldn&rsquo;t see that happening without intervention. &ldquo;He
should be <em>embarrassed</em> with himself.&rdquo;</p>
<p>&ldquo;You can go
now.&rdquo; He looked confused, so I gestured to the counter.</p>
<p>&ldquo;Oh. Sure.&rdquo;
He was hurt. &ldquo;I just wanted to say, it&rsquo;s an honor to work
in the same building as someone like you.&rdquo;</p>
<p>I waited while the
kid bought a sandwich. I should have been gracious, somehow. But I
didn&rsquo;t know how to respond to that kind of flattery. After a
while I decided I should have offered him a position on my team, but
by then he was already walking away.</p>
<p>&ldquo;The Curies
died,&rdquo; said the girl behind the counter. &ldquo;From radiation
poisoning.&rdquo;</p>
<p>&ldquo;I know,&rdquo;
I said. &ldquo;One chocolate muffin, please.&rdquo;</p>
