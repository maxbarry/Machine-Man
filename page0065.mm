<p>First I headed for
the cafeteria. This was a decoy, to throw off surveillance. I really
wanted to check out that operating theater, to see if Carl was in it,
or had been recently. But going there directly from Cassandra
Cautery&rsquo;s office might have looked suspicious. So I caught an
elevator to ground floor, swiped my way through the doors, and stood
in line to buy a muffin.</p>
<p>There were three
people in line ahead of me, one a young blond guy with glasses who
twice turned around to look at me. I was preoccupied with thoughts of
Carl stealing my limbs, but finally this guy summoned enough courage
to blurt: &ldquo;Dr. Neumann, I just want to say you&rsquo;re my
idol.&rdquo;</p>
<p>&ldquo;What?&rdquo;
I said. Because I thought I must have misheard <em>idol</em>.</p>
<p>&ldquo;You and
Isaac Newton,&rdquo; the kid said. &ldquo;And Barry Marshall. And the
Curies. You guys who put yourselves on the line for your research.
Become your own test subjects. I salute you.&rdquo;</p>
<p>&ldquo;Oh,&rdquo; I
said. &ldquo;Thanks.&rdquo;</p>
<p>&ldquo;Compared to
you, Kevin Warwick is a <em>pussy</em>.&rdquo; There was a kind of
religious fervor in the kid&rsquo;s eyes. The girl behind the counter
was waiting for him to realize it was his turn, but I couldn&rsquo;t
see that happening without intervention. &ldquo;He should be
<em>embarrassed</em> with himself.&rdquo;</p>
<p>&ldquo;You can go
now.&rdquo; The kid looked confused, so I gestured to the counter.</p>
<p>&ldquo;Oh,&rdquo;
he said. &ldquo;Sure, yeah. I just wanted to say, it&rsquo;s an honor
to work in an organization with someone like you.&rdquo;</p>
<p>I waited while the
kid bought a sandwich. I didn&rsquo;t know how to respond to
something like that. I should have been gracious, somehow. Maybe I
still could. I could offer the kid a position on my team, or
something. But before I could figure this out, he had walked away.</p>
<p>&ldquo;The Curies
died,&rdquo; said the girl behind the counter. &ldquo;From radiation
poisoning.&rdquo;</p>
<p>&ldquo;Yes, I
know,&rdquo; I said. &ldquo;One chocolate muffin, please.&rdquo;</p>
