<p>One of the most
interesting parts of this project was the comments: they kept me in
touch with what readers thought, threw up ideas, and became a kind of
meta-work: &ldquo;The Annotated Machine Man,&rdquo; perhaps. Thank
you to everyone who contributed a comment. And special thanks to
those who contributed many, many comments, the most prolific of whom
were: Pev (still interesting!), gStein, CrystalR, Toby O,
Electrichead, David, Ben, fredzfrog, Stygian Emperor, Mapuche,
Chemical Rascal (haiku on demand), coolpillows, Alex, Ian Manka,
Felix, C Leffelman, SilverKnight, Yannick, dabbeljuh, Abgrund, Alan
Westbrook, SexCpotatoes, regtiangha, Neville, Adam, tim, Katie Ellert
(&ldquo;Where&rsquo;s Lola? WHERE&rsquo;S LOLA?&rdquo;), Ajna, Isaac,
Joe M., Justin, towr, Morlok8k, Ballotonia, Sander, and Robert
Bissonnette. Many times I clicked through to the previous day&rsquo;s
comments with a sense of dread, the growing suriety that everybody
had surely hated it, but instead found cheers, jokes, and crazy
spin-off ideas that buouyed me forward. Before I began, I was tempted
to put a warning on the comments page, something like: &ldquo;Just so
you know, being too critical of this thing while I&rsquo;m still
writing it may cause me to lock up completely.&rdquo; I didn&rsquo;t
do that, and didn&rsquo;t have to. You were all far nicer to me than
I deserved.</p>
<p>Thank
you to everyone who tossed me an idea: that was much appreciated even
if I didn&rsquo;t use it, because it helped me clarify the boundaries
of my story&rsquo;s world. The idea I
liked the most was from Meredith Course, who educated me about
brain plasticity and free-roaming neurons: that one propelled many
pages.</p>
<p>I also want to
mention Michael Ian Minter again. Now, I don&rsquo;t want to
encourage people to go around goading authors. Particularly not when
that author is me. But Minter&rsquo;s kick in the pants essentially
decided me on doing this, and since I fully would have sheeted home
the blame to him if it had all gone spectacularly wrong, it&rsquo;s
only fair I thank him now.</p>
<p>Thank you to Jen
for her patience with me on those days I struggled with my deadline
