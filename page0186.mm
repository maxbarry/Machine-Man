good idea to begin with. Ditto for my agent, Luke Janklow, who also
made sure this fun experiment in real-time fiction didn&rsquo;t break
me financially, by finding fans in Zachary Wagman of Vintage Books
and, with Brian Siberrel, Cathy Schulman of Mandalay Pictures.</p>
