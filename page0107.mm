<p>I got two steps
into the hallway before Angelica gasped. &ldquo;Is that&mdash;&rdquo;</p>
<p>&ldquo;The guy with
the legs,&rdquo; said Lola. &ldquo;Yeah.&rdquo;</p>
<p>I turned and
squinted at Angelica. Now I remembered her. She had been my doctor,
back in the hospital. She had called me <em>unstable</em>. That seemed
pretty rich, now I knew she kept a house full of tiny, dressed dogs.</p>
<p>&ldquo;He
shouldn&rsquo;t&mdash;&rdquo;</p>
<p>&ldquo;I know. But
close the door, okay?&rdquo;</p>
<p>Angelica came
inside and shut out the night. The dog in her arms stared at me. I
didn&rsquo;t know what it was thinking. But it was something.</p>
<p>&ldquo;We&rsquo;re
in trouble,&rdquo; Lola said. &ldquo;We need help.&rdquo;</p>
<p>I closed my eyes
and let Lola talk. I was glad she was taking the lead, because I was
done. I closed my eyes and let their conversation swirl around me. I
saw the Manager, flying backwards out the window. His eyes stayed on
me until he dropped out of sight.</p>
<p>&ldquo;Charlie.&rdquo;</p>
<p>I opened my eyes.
Cassandra Cautery was there, standing shoulder-to-shoulder with Lola.
&ldquo;I&rsquo;m sorry,&rdquo; I croaked. &ldquo;I didn&rsquo;t mean
to kill him.&rdquo;</p>
<p>&ldquo;He goes in
and out,&rdquo; said Lola. 
</p>
<p>Cassandra Cautery
nodded. &ldquo;You did the right thing. I&rsquo;ll take care of him.&rdquo;
She came closer. &ldquo;Let me take a look at that arm.&rdquo;</p>
<p>&ldquo;Don&rsquo;t
take my arms.&rdquo; Then I realized it wasn&rsquo;t Cassandra
Cautery. It was Angelica. She had deep brown eyes, like the dog she
was carrying. In fact, they had very similar expressions. I knew what
that dog was thinking now. It was: <em>This guy is trouble</em>.</p>
<p>&ldquo;Just relax,
Charlie,&rdquo; said Lola, and it seemed like a good idea. I passed
out.</p>
