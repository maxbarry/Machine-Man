<p>The car leaped
forward. I aimed for a guard to the left of the van: an older guy
with a mustache. It was nothing personal. I mean, true, I&rsquo;m no
fan of mustaches. But that wasn&rsquo;t why I pointed a car at him.
He just happened to be in front of the van, and I wanted to avoid the
rear, in case Carl was about to leap out of there.</p>
<p>The guard stepped
professionally out of the way. He did not look at all scared, which
was a little disappointing. Although it was hard to tell. It was a
pretty bushy mustache. He unloaded his pistol into our tires as we
passed. The car rang with flat impacts, as if we were being attacked
by a baseball bat-wielding gang. This would have been an improvement
on reality, now I think about it, so maybe I should have tried to
sell that to Lola. All she knew was there were gunshots. But I was
not really thinking about Lola: I was concentrating on crushing rose
bushes while not getting killed.</p>
<p>We bumped onto the
road, and I hauled on the wheel, which was not easy to do with one
hand from the passenger seat. &ldquo;Less acceleration!&rdquo; I
said, but not quickly enough, and we thumped into a parked wagon. I
was thrown against Lola. Her head rebounded from the side window. She
said something that sounded like <em>Guk</em> but probably wasn&rsquo;t.
I got my hand back on the wheel. &ldquo;More acceleration!&rdquo;</p>
<p>Lola&rsquo;s eyes
fluttered open. She looked short of breath for some reason, her skin
waxy. &ldquo;Oh. We hit... we hit a car.&rdquo;</p>
<p>&ldquo;My fault.&rdquo;
I threw a glance out the rear window. Better Future guards were
jogging after us. Still no Carl! I couldn&rsquo;t think where he
could be. But we weren&rsquo;t moving: that was more urgent. &ldquo;Let&rsquo;s
go!&rdquo;</p>
<p>&ldquo;Maybe...
that was a mistake. Closing my eyes.&rdquo;</p>
<p>The car dash was
dark. There was a smell in the air: something acrid.</p>
<p>&ldquo;My teachers
always said...&rdquo; She leaned forward until her forehead touched
the steering wheel. &ldquo;I had an active imagination.&rdquo;</p>
<p>&ldquo;Did you...&rdquo;
I couldn&rsquo;t think of a word for it. &ldquo;Discharge?&rdquo;</p>
<p>&ldquo;I
thought...&rdquo;</p>
<p>&ldquo;<em>Put your
hands on the dash! Do it now!</em>&rdquo;</p>
<p>&ldquo;... we
hit...&rdquo; Then she exhaled, like: <em>Cah</em>. At least, that&rsquo;s
what I thought. It wasn&rsquo;t until much later that I realized she
had said: <em>Carl</em>.</p>
