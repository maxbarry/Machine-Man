<p>Some time later, when I finally dragged
myself out of Lola&rsquo;s suite, I came around a corner to find
Cassandra Cautery standing outside the Glass Room, her arms folded.
It gave me a nasty shock, because I had been thinking very pleasant,
Lola-related thoughts.</p>
<p>&ldquo;Look who&rsquo;s finally showing
up for work,&rdquo; said Cassandra Cautery. &ldquo;It&rsquo;s almost
noon.&rdquo;</p>
<p>I stopped. I never knew whether I
should lower the Contours when in conversation with Cassandra
Cautery. It felt wrong to tower over her, but condescending to
descend. &ldquo;Does that matter?&rdquo;</p>
<p>&ldquo;I don&rsquo;t know, Charlie.&rdquo;
She put her hands on her hips. That couldn&rsquo;t be good. &ldquo;Does
it?&rdquo;</p>
<p>&ldquo;I was just&mdash;&rdquo;</p>
<p>&ldquo;I know what you were just. And I
have to tell you, Charlie, I did not authorize a six hundred thousand
dollar operation for that woman so she could steal you away from your
work. I did not.&rdquo;</p>
<p>I felt too confused to speak. Because
if I had had to characterize Cassandra Cautery&rsquo;s behavior, I
would have said <em>jealous</em>. But
that made no sense. I was sure she had no romantic feelings for me. I
would have bet my life on that. But I didn&rsquo;t know what else
this could be.</p>
<p>She walked forward like she was going
to blow by me, then stopped. Very softly, she said: &ldquo;This
relationship only works if we trust each other, Charlie. That&rsquo;s
the only way.&rdquo; Then she walked away.</p>
