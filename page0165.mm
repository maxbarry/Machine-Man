<p>I raised my
gun-arm. Carl&rsquo;s head turned toward me. Light fell across his
face and I saw his eyes lock onto my arm, widen as he realized what
it was. I was going to obliterate him. We both knew it. He was going
to die in little pieces and I would weep beside them over whatever he
had done to Lola&rsquo;s body.</p>
<p>Then Carl flicked
his forearm and an object hurtled toward my body. Even before I saw
what it was, this was the moment I realized I had seriously misread
the situation. Because it didn&rsquo;t matter if I was stronger than
Carl, and faster and smarter and better armed, if he could surprise
me by throwing things. I had never fought anyone in my entire life. I
had almost hit a girl in elementary school, but before I could decide
to do it, she pushed me over. That was the extent of my hand-to-hand
combat experience. Carl was trained. And he had a car battery.</p>
<p>It tumbled toward
me and I flinched away. My gun-arm spat bullets into the ceiling,
tearing out concrete. The car battery ricocheted off my metal hip,
and I thought, <em>Wait a minute, that didn&rsquo;t even hurt</em>,
then the second hit me square in the back and I screamed, because
some of that was flesh. I staggered in a circle, and then Carl was on
top of me, his sledgehammer arms coming at me like buildings, and I
had already lost.</p>
