<p>Something was
burning. I looked down at my shoulder and saw the bandages there
smoking. It said something that I smelt this rather than felt it: it
said I was still enjoying the benefits of Better Future&rsquo;s
industrial-grade drugs. I lowered the gun-arm. <em>Is he dead?</em> my
parts wanted to know, and I said: <em>I don&rsquo;t know</em>. We
walked toward the garage. As the concrete dust began to dissipate,
the effects of the weapon became clear. The parking garage looked
gnawed, as if attacked by giant rats. So much of it had slid into the
alley that the Contours were able to walk almost up to the level I
had fallen from on a shifting hill of rubble and broken concrete
slabs. Occasionally chunks cracked and fell away beneath my hooves,
but I felt calm. The Contours would not let me fall.</p>
<p>Sirens.</p>
<p>I climbed the last
section, propping my gun-arm against the ground and letting the
Contours hop me up. It was dark, and I stood there a moment,
listening. Then I heard his breathing. It was harsh, rattling, the
breathing of a man concentrating on not dying, and I relaxed. <em>I
think we got him</em>, I told the parts, and they said: <em>Let&rsquo;s
make sure</em>.</p>
<p>I moved through the
clouds of dust. &ldquo;Carl?&rdquo; I stopped, because I&rsquo;d
momentarily lost my bearing on him. &ldquo;Come on, Carl. Let&rsquo;s
get this over with.&rdquo;</p>
<p>His legs were
poking out from behind the elevator block. I circled around them,
keeping my distance, because there were to be no last-minute tricks
from Carl. He would not brain me with a chunk of concrete as I rushed
around the corner. He would not open one metal hand to reveal any
kind of explosive device he had swiped from Better Future. The way
this was going to work was I would maintain a safe distance from Carl
at all times. I would tell him a few things about Lola. And then I
would shoot him.</p>
<p>I stepped around a
concrete pillar. He didn&rsquo;t look right. It was the moment I&rsquo;d
first spotted him in here all over again: he was there, but his
posture was wrong and I couldn&rsquo;t work out why. <em>Shoot</em>,
said the parts. <em>Shoot shoot shoot</em>. My gun-arm whirred, like a
suggestion.</p>
<p>The part of Carl&rsquo;s
body that didn&rsquo;t make sense turned. It was Lola. She was
kneeling before him. Even from this distance, I could tell she was
crying. &ldquo;Stop,&rdquo; she said. &ldquo;Please, Charlie, stop.&rdquo;</p>
