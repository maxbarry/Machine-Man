<h5>CHAPTER  EIGHT</h5>
<p>I first realized that meeting Lola Banks was not going to be easy
when I exited the Better Future elevator and a security guard fell
into step beside me, muttering into his lapel mike. By the time I
reached the lobby, there were four of them, and another two moving to
block the glass doors. Carl was there, of course. Carl was
everywhere. &ldquo;Dr. Neumann? Can I help you?&rdquo;</p>
<p>My Contour legs were already detouring around him, and it was a
moment before I could get them to stop. They were a little fiesty.
But eventually I came to a juddering halt. The receptionist had
vanished, I noticed. In fact, the entire lobby was empty, except for
me and the guards. &ldquo;I&rsquo;m going out.&rdquo;</p>
<p>&ldquo;Where would you like to go, sir? We can escort you.&rdquo;</p>
<p>&ldquo;That&rsquo;s kind of you, Carl,&rdquo; I said. &ldquo;But I
have my own transport, thanks.&rdquo;</p>
<p>His eyes slipped to my legs. The Contours were underneath my jeans,
but they were longer than my old legs, and I hadn&rsquo;t been able
to quite pull my socks over the six-inch strip of gleaming titanium.
On the plus side, I was really tall. &ldquo;I&rsquo;m afraid I must
insist.&rdquo;</p>
<p>What happened next was I thought I really should just walk out of
there. Because it wasn&rsquo;t like they would be able to stop me. I
honestly didn&rsquo;t express this as a mental instruction, of the
kind the Contours were supposed to respond to. But, like I mentioned,
they were fiesty. All of a sudden, they were moving so quickly I had
to grip the sides of the bucket seat just to stay upright. &ldquo;Whoa,&rdquo;
I said, and Carl tried to grab me. The Contours stepped around him.
They ran at the glass doors too fast for them to part, which caused
the collision-detection function to kick in and stop me so suddenly
that my forehead cracked against the smoked glass. That really hurt.
I would have to fix that. Then the doors slid open and I felt fresh
air on my face and the Contours took off again. No-one shot at us.
That was good.</p>
