<h5>CHAPTER 
THIRTY-SIX</h5>
<p>&ldquo;I love you,
Charlie,&rdquo; Lola said. &ldquo;But I&rsquo;m not sure how much you
is in there.&rdquo;</p>
<p>I stared at Carl.
This didn&rsquo;t seem to be a trick. His chest did not rise or fall.
He was genuinely non-living.</p>
<p>&ldquo;I think you
took a bad turn,&rdquo; she said. &ldquo;Because at first you really
were making yourself better. You were taking charge of your body. But
then it started to go the other way. I noticed that a while ago. And
when at Angelica&rsquo;s your legs stopped working, I thought... I
thought, maybe that&rsquo;s not such a bad thing. Because you might
start to get in charge again.&rdquo;</p>
<p>Carl was off. He
was non-functional. He was disabled. He was dead.</p>
<p>&ldquo;I&rsquo;m
not against self-improvement,&rdquo; she said. &ldquo;I just think
you&rsquo;ve started going backward.&rdquo;</p>
<p>The sirens were
very loud now. Then they stopped. Somewhere below us, I heard
engines. Voices. When I turned to Lola, reflected blue light
flickered across her face.</p>
<p>&ldquo;Charlie,
you&rsquo;re the most amazing person I&rsquo;ve ever met. But not
because of your body.&rdquo;</p>
<p>I felt a bubble of
grief rising within me. Because she was very lovely, standing here
telling me to shed myself: she was a girl who loved me and they did
not come along every day; they had not come along in my lifetime
prior to this. &ldquo;Lola,&rdquo; I said, and my parts said: <em>Shoot
her</em>. Which kind of proved Lola&rsquo;s point. My grief sharpened
and I realized it wasn&rsquo;t for her. It was for them. &ldquo;I
love you,&rdquo; I said, and I wasn&rsquo;t sure who I was talking
to, but my parts said: <em>I love you too</em>.</p>
<p>Then my arm died.</p>
