<h5>CHAPTER 
THIRTY-SIX</h5>
<p>&ldquo;I love you,
Charlie,&rdquo; Lola said. &ldquo;But I&rsquo;m not sure how much you
is in there any more.&rdquo;</p>
<p>I stared at Carl.
This didn&rsquo;t seem to be a trick. His chest did not rise or fall.
He genuinely appeared to be non-living.</p>
<p>&ldquo;I think you
took a bad turn,&rdquo; she said. &ldquo;Because at first you were
making yourself better. But then it went wrong. It started to take
you over. I realized that a while ago, Charlie, at Angelica&rsquo;s.
When your legs stopped working, I thought, well, maybe that&rsquo;s
not such a bad thing. Maybe it would let you be you again. But...
well, look at you.&rdquo;</p>
<p>Carl was off. He
was non-functional. He was disabled. He was dead.</p>
<p>&ldquo;I just want
to go back to how things were before. I mean... can&rsquo;t we do
that?&rdquo;</p>
<p>The sirens were
very loud now. Then they stopped. Somewhere below us, I heard
engines, voices. I turned to face Lola, and reflected blue light was
flickering across her face.</p>
<p>&ldquo;I&rsquo;m
not against self-improvement,&rdquo; she said. &ldquo;I just think
you&rsquo;ve gone backward.&rdquo;</p>
<p>I felt a bubble of
grief rising within me. Because she seemed very lovely, standing here
telling me to shed myself: she was a girl who loved me and they did
not come along every day; they had not come along in my lifetime
prior to this. &ldquo;Lola,&rdquo; I said, and my parts said: <em>Shoot
her</em>. Which kind of proved Lola&rsquo;s point, and the feeling of
grief sharpened, and I realized it wasn&rsquo;t for her. It was for
them. &ldquo;I love you,&rdquo; I said, and I wasn&rsquo;t sure who I
was talking to, but my parts said: <em>I love you too</em>.</p>
<p>Then my arm died.</p>
