<h5>CHAPTER 
THIRTY</h5>
<p>I became aware of
smoke. I felt alarmed, in some small, sectioned-off part of my body.
Smoke is rarely good. In my line of work, it usually signals
stupidity: failure to adequately anticipate logical consquences, or
check a tolerance, or convert from imperial. This smoke was curling
along the ceiling above me. I wasn&rsquo;t sure what it signaled. I
wasn&rsquo;t sure where I was. I just knew there was smoke.</p>
<p><em>Get up</em>,
said the part of me that was worried about the smoke. But some other
part said: <em>Lie here a little longer</em>,
and that felt more persuasive. I was doped. I was relaxed. I would
never feel this peaceful again, not without chemical assistance. I
had to savor it.</p>
<p>Something
was slooshing. It sounded soothing. Something else went: <em>Sssssss</em>,
like an old man easing into a favorite chair. I felt safe. It was
like being in the womb. Except for the smoke, which was becoming
quite thick. But I could ignore that. I closed my eyes.</p>
<p>Someone
coughed. I opened my eyes again, because that was a disconcerting
sound to hear in a womb. Especially this particular cough, which
sounded wet. I waited, hoping it would go away, but it didn&rsquo;t.
<em>Cark. Cark</em>. It was a
perfunctory cough. Like the owner didn&rsquo;t expect it to do much
good.</p>
<p>This I
couldn&rsquo;t ignore. I felt the dream-state dissolving and felt
sad, but also better, because this was me, coming back. My thoughts
began to organize themselves. I raised my head.</p>
