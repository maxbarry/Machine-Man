<p>Page 90.</p>
<p>&ldquo;You&rsquo;ve
killed him.&rdquo; I didn&rsquo;t realized Cassandra Cautery was
beside me until she spoke. She braced herself carefully against what
was left of the window frame and peered out. The drapes flapped
around her. &ldquo;Look at him. He&rsquo;s just lying there.&rdquo;</p>
<p>&ldquo;I didn&rsquo;t...&rdquo;
The rest of this sentence was: <em>mean to</em>. But I couldn&rsquo;t
get it out. My chest was compressing my lungs.</p>
<p>&ldquo;The Manager
is dead.&rdquo; There was a touch of awe in her voice. &ldquo;He is
definitely dead.&rdquo;</p>
<p>I looked again,
then wished I hadn&rsquo;t. Occupying most of the space between the
Better Future building and the road was a wide, healthy lawn.
Bisecting this was a narrow concrete path, and on this the Manager
was sprawled. I am tempted to say this was extraordinary bad luck,
but given his position, I can&rsquo;t honestly believe he would have
survived if he&rsquo;d landed on the grass, either. I couldn&rsquo;t
see his head.</p>
<p>My legs took a step
backward. Whatever part of me was in charge at this time, I will give
it this: it analyzed the situation faster than I did.</p>
<p>Cassandra Cautery
looked at me. &ldquo;Oh, Charlie. You are in so very much trouble.&rdquo;</p>
<p>My legs were
backing me out of the room. I finally reached the conclusion they
had, and turned and ran.</p>
