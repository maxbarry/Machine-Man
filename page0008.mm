<p>I asked a nurse for a sketch book and
began drawing up specs. Fleshing out
the concept, you might say. I thought about what I really wanted from
a leg. It seemed to me, as I lay in my hospital bed, that the
designer of biological legs had not been particularly ambitious. I
mean, I don&rsquo;t mean to criticize. Legs are perfectly adequate at
conveying a small biological mass from A to B, so long as A and B are
not particularly far apart, and you&rsquo;re in no hurry. That&rsquo;s
not bad, when you&rsquo;re limited to raw materials you can grow
yourself. But I had to think that if anyone was designing a leg
today, they would build in a lot more features.</p>
