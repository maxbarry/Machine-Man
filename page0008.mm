<p>I asked a nurse for a sketch book and
began drawing up specs. <em>Fleshing out the </em><em>concept</em>,
Lola Banks might have said. I thought about what I really wanted from
a leg. It seemed to me, as I lay in my hospital bed, that the
designer of biological legs had not been particularly ambitious. I
mean, I don&rsquo;t mean to criticize. Given the available materials,
legs were perfectly adequate at conveying a small biological mass
from A to B at fairly low speeds, so long as A and B were not
particularly far apart. But I had to think that if anyone today was
designing a leg from scratch, they would build in a lot more
features.</p>
