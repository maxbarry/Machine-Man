<p>The door handle
clacked open and in walked the Manager. I was a little disappointed.
The way Cassandra Cautery was acting, I expected a man with lightning
crackling around the shoulders of his tailored suit, trailing weeping
virgins. And he was in a suit, and I guess it was probably tailored,
but otherwise he looked very normal. If I had been buying a car, and
this guy walked out of the salesroom, I would not have been
surprised.</p>
<p>&ldquo;Dr.
Neumann.&rdquo; He came at me with his hand outstretched and his
teeth exposed. His hair was very neat. I wouldn&rsquo;t have thought
you could get hair to sit like that, not without some kind of
industrial solvent. &ldquo;Can I get you anything? Water? Coffee?
Something to eat?&rdquo;</p>
<p>&ldquo;No, thanks.&rdquo;
I shook his hand. This lasted a while, and he smiled at me the whole
time. I felt nervous.</p>
<p>He looked at my
legs. &ldquo;Now, what&rsquo;s the protocol here? Do I offer you a
seat?&rdquo;</p>
<p>&ldquo;No. I&rsquo;m
comfortable.&rdquo;</p>
<p>&ldquo;Of course
you are. You know what? Let&rsquo;s all stand.&rdquo; Cassandra
Cautery, who had popped out of her chair when the Manager walked in
and was now in the process of lowering herself again, arrested her
descent. &ldquo;What do you think, Cassie?&rdquo;</p>
<p>&ldquo;I love to
stand,&rdquo; she said. Cassie. I would never look at her the same
again.</p>
<p>The Manager walked
to the window and drew back the drapes. I squinted against the sudden
glare. I could barely make out his face. &ldquo;I have to tell you,
Dr. Neumann, it&rsquo;s a thrill to finally meet you. I&rsquo;ve been
across your project from the beginning. And while we obviously have
our fingers in many pies, a large number of very speculative pies,
yours, Charlie, is the one that captures my imagination. You know
what I see? I see a man taking a stand. A man who refuses to let
himself be defined by anyone or anything but himself. Nature dealt
you a hand, Charlie, and you, for the first time in history, you
tossed it back. You said no. You said: <em>I</em> decide who I am. I
decide the limit of my capabilities. I will be not what I was made,
but what I make.&rdquo;</p>
<p>&ldquo;That&rsquo;s
it,&rdquo; I said. &ldquo;That&rsquo;s it exactly.&rdquo; I wondered
if I had got the Manager all wrong. Maybe he was like me:
misunderstood.</p>
<p>&ldquo;I could not
be prouder to count myself as one of your supporters.&rdquo; He put
one hand on my shoulder. &ldquo;So. Let&rsquo;s talk super-soldiers.&rdquo;</p>
