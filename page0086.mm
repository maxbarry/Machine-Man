<p>The door handle
clacked open and in he came: the Manager. I had to say, I was a
little disappointed. The way Cassandra Cautery had been acting, I
expected a collossus, a man with lightning crackling around the
shoulders of his tailored suit, trailing weeping virgins. And he was
in a suit, and I guess it probably was tailored, but otherwise he
looked very average. If I had been buying a car, and this guy walked
out of the salesroom, I would not have been surprised. The most
remarkable thing about him was his hair. That was very neat. I mean,
<em>very</em> neat.</p>
<p>&ldquo;Dr.
Neumann.&rdquo; He walked toward me with his hand outstretched,
smiling. &ldquo;Can I get you anything? Water? Coffee? Something to
eat?&rdquo;</p>
<p>&ldquo;No, thanks.&rdquo;
I shook his hand. He smiled at me the whole time, and I felt nervous.</p>
<p>&ldquo;Now, what&rsquo;s
the protocol here? Do I offer you a seat?&rdquo;</p>
<p>&ldquo;Uh, no need.
I&rsquo;m comfortable like this.&rdquo;</p>
<p>&ldquo;Me, too.&rdquo;
Cassandra Cautery, who had bounced out of her chair when the Manager
walked in, and was now in the process of lowering herself again,
arrested her descent. &ldquo;Do you mind, Cassie?&rdquo;</p>
<p>&ldquo;Not at all,&rdquo;
she said. Cassie. I would never look at her the same again.</p>
<p>The Manager folded
his arms. &ldquo;You know, this whole area, Dr. Neumann, it&rsquo;s
tremendously exciting.&rdquo;</p>
<p>&ldquo;Charlie.&rdquo;
I was beginning to like him. 
