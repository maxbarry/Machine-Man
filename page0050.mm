<p>Then Lola stopped doing anything. She lay listless. She didn&rsquo;t
respond to my questions. She stared at nothing. And I started
thinking, well, I don&rsquo;t really know her that well. Sure, we had
shared a couple of very intense experiences, but you add up the time
we&rsquo;d spent in each other&rsquo;s company and it was like four
hours. It was an amazing four hours. I&rsquo;m not saying it wasn&rsquo;t.
I&rsquo;m saying that when a person you&rsquo;ve just met completely
changes personality, you start to wonder whether you&rsquo;ve really
met them at all.</p>
<p>&ldquo;Get her to talk about the heart,&rdquo; said Cassandra
Cautery. This was later, in the hallway outside Lola Banks&rsquo;s
room. I had given up trying to get a reaction out of Lola and wheeled
myself away for some thinking time.</p>
<p>&ldquo;I can&rsquo;t get her to talk about anything,&rdquo; I said.
&ldquo;She&rsquo;s practically comatose.&rdquo;</p>
<p>&ldquo;You&rsquo;re not trying. You&rsquo;re just saying her name
