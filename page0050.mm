<p>She snuffled. It was a noise I had heard before. I had been lying on
the floor of my lab, after crushing my right leg. Cassandra Cautery
and Dick Peters had been standing over me. Dick Peters had said:
<em>Don&rsquo;t do that thing</em>.</p>
<p>&ldquo;I&rsquo;m sorry,&rdquo; she said. Her eyes were turning
