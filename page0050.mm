eyes. &ldquo;When I get emotional... it&rsquo;s a chemical
imbalance.&rdquo; Her chest hitched unstoppably, like a car someone
was trying to jump-start. I was transfixed with horror. &ldquo;It...
passes... in a... minute.&rdquo;</p>
<p>&ldquo;I&rsquo;ll talk to Lola,&rdquo; I said.</p>
