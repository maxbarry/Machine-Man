<p>I went for the Contours, half-expecting
Carl to stop me. I had, after all, used these to flee the company; I
would have understood if Carl&rsquo;s hand had closed on my wrist and
he said: <em>Choose something slower</em>.
But he just leaned against one wall with his arms folded as I lifted
myself onto the table and began to fit myself.</p>
<p>I won&rsquo;t lie.
It was possibly the most sensual experience of my life. I had missed
the legs, a lot, but still underestimated how good it would feel to
have them back. As I slid my terminated thighs into the restraints
and began aligning the tiny needles that transmitted my nerve signals
to circuitry, I could feel synapses popping in my brain like
fireworks. I closed my eyes.</p>
<p>&ldquo;You like
those things, huh?&rdquo; said Carl.</p>
<p>I looked at him.</p>
<p>&ldquo;They can
press, what, a ton?&rdquo;</p>
<p>&ldquo;Something
like that.&rdquo; It was four tons. And, frankly, that was nothing,
compared to some of the others. But I didn&rsquo;t say that, because
Carl was trying to engage me, and I didn&rsquo;t want to be engaged.</p>
<p>&ldquo;You can
never be strong enough,&rdquo; Carl said. &ldquo;That&rsquo;s for
sure.&rdquo;</p>
<p>I
stood up. The Contours&rsquo; hooves <em>thunk-thunked</em>
against the floor. I was tall again. &ldquo;I&rsquo;ve got it from
here, Carl.&rdquo;</p>
<p>He shrugged, and
left. That was the last time I ever saw him. All of him, I mean.</p>
