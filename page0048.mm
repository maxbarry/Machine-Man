<p>I was sickly as a child. I guess that comes as no surprise. I was
that kid who spent the whole summer inside, curtains drawn against
the sounds of others my age hooting and laughing in the street
outside. Glandular fever. At school, I walked up to the gym teacher
at the start of every class and showed him the note that said I was
to be excused to the library. He made me show him that note every
time, even though it clearly said <em>until further notice</em>. I
think he was waiting for the day I would decide, through strength of
character, that I was ready to handle gym class, and damn what my
note said. That day never came. That day was never going to come. In
the library, I read about trains and DNA and how they built Hoover
Dam. And later, walking home, I would watch a boom gate descend
across a level crossing and know it did so because the wheels of an
approaching train had dipped the track&rsquo;s inductance below a
pre-programmed level. Things like that made me feel good.</p>
<p>One day my note ran out. The gym teacher took it from my fingers and
deliberately crumpled it into a ball. I had known this day was
coming, but still, it felt like losing a friend. &ldquo;Now,
Charlie,&rdquo; said the gym teacher, a man with skin like parched
earth, eyes that had seen everything. &ldquo;Go get a ball.&rdquo;</p>
<p>I couldn&rsquo;t catch. I threw like a four-year-old. When I ran, my
arms and legs flailed like I was drowning, and after a hundred yards
I was exhausted. I swung at balls with desperate hope but no faith,
and was not surprised. I could not catch anyone, ever, not even the
kid with one leg shorter than the other, who wore a brace. On the
field, anyone with the ball wove through me like I wasn&rsquo;t
there. I stopped a soccer ball, one time. It was the physical
highlight of my term. I prepared to kick it forward, sensing glory, a
change of fortune, the kind of turning point my gym teacher believed
in, but my foot had barely begun its downward swing when another
player stole away the ball. I was so slow I amazed myself.</p>
<p>At the time, this distressed me. I wish I could talk to that kid now,
and say: <em>It doesn&rsquo;t matter.</em> I would tell him that being
able to kick a ball, that gets less and less important. By the end of
high school, those kids who leap and dive and throw balls like
missiles, a lot of them have already dropped out. The ones who remain
walk into class with their textbooks gripped tight, because the one
muscle they can&rsquo;t rely on, the one that&rsquo;s loose and
flabby and easily tired, it&rsquo;s the one in their heads.</p>
<p><em>You know how smart kids are like circus freaks?</em> I would say to
my junior high self. <em>How the strong boys get respect, and the
nerds are only good for entertainment? Well, pretty soon, that starts
to swap around.</em></p>
<p>That would have been a comfort. Because I have always known my body
is not on my side. But it would have been nice to know that was
fixable.</p>
