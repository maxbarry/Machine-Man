<p>Cassandra Cautery was waiting for me in a small, dark observation
room above an operating theater. She was a tiny, suited silhouette.
When Carl wheeled me in, she glanced at me, then returned her
attention to the green-cloaked figures revolving around the operating
table. Carl closed the door. Before he could get his hands on my
wheelchair again, I found the grips and pushed myself up to the
glass.</p>
<p>Lola Banks was on the table. I knew this because I could see one arm
poking out from beneath an ocean of green cloth. It was the only
piece of her I could see, but that was enough. It was her. A surgeon
stood with his back to me, his shoulders working. It felt very wrong,
her lying there while a man she didn&rsquo;t know dug into her chest.</p>
<p>&ldquo;I think Miss Banks must have got herself into a trial,&rdquo;
said Cassandra Cautery. &ldquo;The heart is a SynCardia model, but
not one our people recognize.&rdquo;</p>
<p>I could see it. The top, at least. It sat in a steel bowl on a tray
to a surgeon&rsquo;s right, a red-stained chunk of plastic and metal.
It looked strange, but then I suppose it had been deformed by two
close-range impacts from Carl&rsquo;s not-quite-lethal ammunition.</p>
<p>&ldquo;Unusual, to have steel components,&rdquo; she said. &ldquo;Very
unusual. Please ask her about that, when she wakes up.&rdquo;</p>
<p>I said nothing. I wasn&rsquo;t talking to Cassandra Cautery. I had
decided this in the wheelchair, as Carl pushed me through corridors
that smelt oddly of fresh paint. I wasn&rsquo;t talking to any of
these people until Lola was okay.</p>
<p>&ldquo;Fortunately, we have a replacement for her. A little custom
model of our own. And the facilities to install it.&rdquo; She looked
at me. &ldquo;This room didn&rsquo;t exist until two weeks ago. We
just finished construction. You believe in luck, Charlie?&rdquo;</p>
<p>I kept my mouth shut.</p>
<p>&ldquo;Me neither,&rdquo; she said. &ldquo;Someone&rsquo;s looking
out for your girl, that&rsquo;s what I think. Someone upstairs.&rdquo;</p>
<p>At first I thought she meant God. Then I thought she meant
management. Then I wasn&rsquo;t sure.</p>
<p>&ldquo;We built this place for you, you know. To help your project.&rdquo;</p>
<p>&ldquo;I don&rsquo;t think I want to continue the project.&rdquo; It
broke my vow of silence. But I couldn&rsquo;t let her keep talking
like we were on the same side.</p>
<p>Cassandra Cautery said nothing for a while. &ldquo;All right,
Charlie. Whatever you want.&rdquo; She didn&rsquo;t believe me. I
couldn&rsquo;t blame her. I wasn&rsquo;t sure I believed myself.</p>
<p>We watched the surgery. After a while, the surgeon with his back to
us moved aside. Lola&rsquo;s chest was a pit. It gaped at me, red and
wet.</p>
<p>&ldquo;As long as we&rsquo;ve got her open,&rdquo; Cassandra Cautery
said, almost to herself, &ldquo;I wonder if we should put anything
else in there.&rdquo; I looked at her, furious, but also embarrassed,
because I had been thinking the same thing.</p>
