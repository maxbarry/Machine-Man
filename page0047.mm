<p>Page 47.</p>
<p>Cassandra Cautery was waiting for me in a small, dark observation
room above an operating theater. She was a tiny, suited silhouette.
When Carl wheeled me in, she glanced at me, then returned her
attention to the green-cloaked figures that revolved around the
operating table. Carl closed the door. I found the wheelchair&rsquo;s
grips and pushed myself up to the glass.</p>
<p>Lola Banks was on the table. I knew this because I could see one arm
poking out from beneath an ocean of green cloth. It was the only
piece of her I could see, but that was enough. It was her. A surgeon
stood with his back to me, his arms working. It felt very wrong, her
lying there helpless while a man she didn&rsquo;t know dug into her
chest.</p>
<p>&ldquo;She had a SynCardia model heart,&rdquo; said Cassandra
Cautery. &ldquo;A new one. I think Miss Banks must have got herself
into a trial at that hospital.&rdquo;</p>
<p>I could see it. The top, at least. It sat in a steel bowl on a tray
to a surgeon&rsquo;s right, a red-stained chunk of plastic and metal.
It looked strange to me, but then it had been deformed by the two
close-range impacts from Carl&rsquo;s not-quite-lethal ammunition.</p>
<p>&ldquo;We&rsquo;re replacing the whole thing,&rdquo; said Cassandra
Cautery. &ldquo;Fortunately we were able to get our hands on a unit
at short notice.&rdquo;</p>
<p>I wasn&rsquo;t talking to Cassandra Cautery. I had decided this in
the wheelchair, as Carl pushed me through corridors that smelt of wet
paint. I wasn&rsquo;t talking to anyone until Lola was okay.</p>
<p>&ldquo;This room didn&rsquo;t exist until two weeks ago. We just
finished construction. How&rsquo;s that for good luck?&rdquo; She
looked at me. &ldquo;You believe in luck, Charlie?&rdquo;</p>
<p>I kept my mouth shut.</p>
<p>&ldquo;Me neither,&rdquo; said Cassandra Cautery. &ldquo;Someone&rsquo;s
looking out for your girl, that&rsquo;s what I think. Someone
upstairs.&rdquo;</p>
<p>At first I thought she meant God. Then I thought she meant
management. Then I wasn&rsquo;t sure.</p>
<p>&ldquo;We built this place for you. To help your project.&rdquo;</p>
<p>&ldquo;I don&rsquo;t know if I want to continue the project.&rdquo;
It broke my vow of silence. But I couldn&rsquo;t let her keep talking
like we were on the same side.</p>
<p>Cassandra Cautery said nothing for a while. I got the feeling she
didn&rsquo;t believe me.  &ldquo;All right, Charlie. Whatever you
want.&rdquo;</p>
<p>We watched the surgery. After a few minutes, the surgeon with his
back to us moved aside. Lola&rsquo;s chest was a red pit. It gaped
like a mouth.</p>
<p>&ldquo;As long as we&rsquo;ve got her open,&rdquo; Cassandra Cautery
said, as if to herself, &ldquo;I wonder if we should put anything
else in there.&rdquo; I looked at her, startled and angry, and also
embarrassed, because it was like she read my mind.</p>
