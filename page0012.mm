<p>Carl the security guard deposited me in
my chair in the Glass Room. We called it the Glass Room because it
looked over the adjoining labs, even though the walls were a
transparent plastic polymer. They used to be plain glass, apparently,
until an unfortunate incident involving a spilled beaker, an
experimental strain of weapons-grade pathogen, and some panicked
researchers with chairs. When I heard this story, the pathogen was
harmless, and served only as a wake-up call to management. But later
I overheard a version in which six people died before the complex
could be locked down, and thirty-one afterward, when they flooded the
labs with gas. This was according to Jason, one of my graduate lab
assistants. Since he was telling Candice, also a lab assistant, I
thought he was probably embellishing. But I&rsquo;ve never been able
to tell when people are lying. All I know is the walls are plastic
now.</p>
