<h5>CHAPTER 
THIRTY-ONE</h5>
<p>I stopped feeling
sorry for myself when I got off the table and saw the man on the
tiles. He was slumped against the far wall, his green scrubs speckled
dark down the front. His lips were very red. When he saw me, he
blinked once, slowly.</p>
<p>I was seven feet
tall, wearing titanium legs. I had a reactor in my abdomen. When I
had swung my legs off the table, they moved soundlessly, and when the
hooves touched the floor, some tiny part of them fired downward and
locked there with a click. That was new. When I turned, my torso
swiveled in multiple ring-sections. When water droplets hit my metal
skin, they made a sound like: <em>punk</em>.</p>
<p>&ldquo;Are you
okay?&rdquo; I said. &ldquo;What can I do?&rdquo; I already suspected
the answer was <em>nothing</em>. If our positions were reversed, maybe
this guy could help me: cut me open, find whichever internal organs
were crushed or torn. Except I wasn&rsquo;t sure I still had much in
the way of internal organs. Not biological ones, at least. If I was
in this guy&rsquo;s position, I would need a mechanic. So maybe
neither of us could help the other. But as I watched his chest hitch
and fall, I no longer felt so bad being metal.</p>
