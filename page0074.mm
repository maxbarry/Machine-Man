<h5>CHAPTER 
SIXTEEN</h5>
<p>I emerged from the Glass Room so tired
I could barely keep my head up. The Contours bore me smoothly toward
my bunk room, not minding that I nodded off once or twice along the
way. They were good legs.</p>
<p>Lola was outside my room, leaning
against the wall. That woke me up. &ldquo;Hi,&rdquo; she said. She
fingered the hem of her Better Future polo shirt. Lola had worn
nothing but company merchandise since her operation; I was pretty
sure she hadn&rsquo;t been outside.</p>
<p>&ldquo;Hi,&rdquo; I said.</p>
<p>&ldquo;I&rsquo;m sorry about before.&rdquo;
She took a step toward me, then a more confident one, and took my
hand, the one with fingers. &ldquo;I mean, I&rsquo;m sorry we
argued.&rdquo;</p>
<p>&ldquo;That&rsquo;s okay.&rdquo; And it
was. Now she was here, I couldn&rsquo;t even remember what we&rsquo;d
argued about.</p>
<p>&ldquo;I&rsquo;m a yeller.&rdquo; She
smiled crookedly. &ldquo;I maybe should have warned you about that.
It comes with growing up angry at your mom.&rdquo; She held out her
arms, and we hugged. It was fantastic. It went on forever. She turned
her head and kissed my neck, and I almost passed out. &ldquo;You&rsquo;ll
help Carl, won&rsquo;t you?&rdquo;</p>
<p>I stiffened.</p>
<p>Lola slipped out of the hug. &ldquo;I&rsquo;m
not saying <em>your</em> parts. I get
that now. Like my heart, that wasn&rsquo;t just a thing. It was <em>me.</em>
I should have realized you would feel the same way.&rdquo; She bit
her lip. &ldquo;But I thought, maybe, if you wanted, you could make
some new parts. For Carl. Do you think you could do that?&rdquo;</p>
<p>&ldquo;Yes,&rdquo;
I said. &ldquo;I think I can do that.&rdquo;</p>
<p>She grinned. &ldquo;Well,
that&rsquo;s so great.&rdquo;</p>
<p>I shrugged. &ldquo;Well.&rdquo;</p>
<p>&ldquo;You&rsquo;re
the best. I don&rsquo;t mean that like an expression.&rdquo; She
kicked my Contours. This registered as a non-localized tingling, like
I was standing outside in a thunderstorm. The hairs the back of neck
stood up. &ldquo;I mean, you <em>are</em>
the best, Charlie.&rdquo; She began backing away. &ldquo;You
need your sleep. Talk tomorrow, okay?&rdquo;</p>
<p>&ldquo;Okay.&rdquo; I went inside and
sat on the bunk. I began to feel very guilty. Because what I&rsquo;d
said about helping Carl, that was kind of a cheat. If Cassandra
Cautery did what I&rsquo;d asked, Carl would not be around to help.</p>
