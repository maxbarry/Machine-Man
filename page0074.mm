<h5>CHAPTER 
SIXTEEN</h5>
<p>I emerged from the Glass Room so tired
I could barely keep my head up. The Contours bore me smoothly toward
my bunk room, not minding that once or twice I nodded off. They were
good legs. Trustworthy legs. Legs you could stand behind.</p>
<p>Lola was leaning against the wall
outside my room. That woke me up. &ldquo;Hi,&rdquo; she said. She
fingered the hem of her Better Future polo shirt. She had worn
nothing but company merchandise for a while now. She hadn&rsquo;t
left the building since Carl shot her.</p>
<p>&ldquo;Hi,&rdquo; I said.</p>
<p>&ldquo;I&rsquo;m sorry about before.&rdquo;
She took a step toward me, then a more confident one, and took my
hand, the one with fingers. &ldquo;I mean, I&rsquo;m sorry we
argued.&rdquo;</p>
<p>&ldquo;That&rsquo;s okay.&rdquo; And it
was: I cared about nothing but that she was here. I couldn&rsquo;t
even remember what we&rsquo;d argued about.</p>
<p>&ldquo;I&rsquo;m a yeller.&rdquo; She
smiled crookedly. &ldquo;I maybe should have warned you about that.
It comes with growing up angry at your mom.&rdquo;</p>
<p>She put her arms out, and we hugged. It
was fantastic. It went on forever. She turned her head and kissed my
neck. I almost passed out.</p>
<p>She said: &ldquo;You&rsquo;ll help
Carl, won&rsquo;t you?&rdquo;</p>
<p>I stiffened.</p>
<p>Lola slipped out of the hug. &ldquo;I&rsquo;m
not saying <em>your</em> parts. I
thought about that, and I get it. It&rsquo;s like my heart. That
wasn&rsquo;t just a thing. It was a part of me, and a part of my dad.
I was dumb not to realize that.&rdquo; She bit her lip. &ldquo;But I
thought, maybe, if you wanted, you could make some new parts. For
Carl.&rdquo; She squinted. &ldquo;Do you think you could do that?&rdquo;</p>
<p>&ldquo;Yes,&rdquo;
I said. &ldquo;I think I can do that.&rdquo;</p>
<p>She grinned. &ldquo;Well,
that&rsquo;s so great.&rdquo;</p>
<p>&ldquo;Well.&rdquo;
I shrugged.</p>
<p>&ldquo;You&rsquo;re
the best. I don&rsquo;t mean that like an expression.&rdquo; She
kicked my Contours. This registered as a non-localized tingling, like
I was standing outside in a thunderstorm. The hairs the back of neck
stood up. &ldquo;I mean, you <em>are</em> the best.&rdquo;</p>
<p>&ldquo;Aw.&rdquo;</p>
<p>Lola began backing away. &ldquo;You
need your sleep. Talk tomorrow, okay?&rdquo;</p>
<p>&ldquo;Okay.&rdquo; After she left, I
went inside and sat on the bunk. I began to feel very guilty. Because
what I&rsquo;d said about helping Carl, that was kind of a cheat. If
Cassandra Cautery did what I asked, Carl would not be around to help.</p>
