<p>I emerged from the Glass Room hours
later, so tired I could barely keep my head up. The Contours bore me
smoothly toward my bunk room, not minding that once or twice I nodded
off. They were good legs.</p>
<p>Lola was waiting outside my room. That
woke me up pretty fast. &ldquo;Hi,&rdquo; she said. She fingered the
hem of her Better Future polo shirt. Lola had been wearing nothing
but company merchandise for a while now.</p>
<p>&ldquo;Hi,&rdquo; I said.</p>
<p>&ldquo;I&rsquo;m sorry about before.&rdquo;
She took a step toward me, then a more confident one, and took my
hand, the one with fingers. &ldquo;I mean, I&rsquo;m sorry we
argued.&rdquo;</p>
<p>&ldquo;That&rsquo;s okay,&rdquo; I
said, and it was: all I cared about now was that she was here.</p>
<p>&ldquo;I&rsquo;m a yeller.&rdquo; She
smiled crookedly. &ldquo;I maybe should have warned you about that.
It comes with growing up angry at your mom.&rdquo;</p>
<p>&ldquo;Oh.&rdquo;</p>
<p>She put her arms out, and we hugged. It
was fantastic. It went on forever. She turned her head and kissed my
neck. I almost passed out.</p>
<p>She said: &ldquo;You&rsquo;ll help
Carl, won&rsquo;t you?&rdquo;</p>
<p>I stiffened.</p>
<p>Lola slipped out of the hug. &ldquo;I&rsquo;m
not saying <em>your</em> parts. I
totally get that now. But I thought, maybe, if you wanted, you could
make some new ones. For Carl.&rdquo; She bit her lip. &ldquo;Do you
think you would do that?&rdquo;</p>
<p>&ldquo;Yes,&rdquo;
I said. &ldquo;I think I can do that.&rdquo;</p>
<p>She grinned.
&ldquo;That&rsquo;s so great.&rdquo;</p>
<p>&ldquo;Well.&rdquo;
I shrugged.</p>
<p>&ldquo;You&rsquo;re
the best, Charlie. I don&rsquo;t mean that like an expression. I
mean, you are the best.&rdquo;</p>
<p>&ldquo;Aw.&rdquo;</p>
<p>She began backing away. &ldquo;You need
your sleep. Talk tomorrow, okay?&rdquo;</p>
<p>&ldquo;Okay.&rdquo; After she left, I
went inside and sat on the bunk. I began to feel very guilty. Because
what I&rsquo;d said about helping Carl, that was kind of a cheat.
Once Cassandra Cautery did what I asked, Carl would not be around to
help.</p>
<p>I levered the Contours onto the bed.
This was a lot harder than it sounds, because of the weight. But it
beat scrambling around for them in the middle of the night, when the
phantom pain hit. I lay back and closed my eyes. But I couldn&rsquo;t
stop thinking about Carl. I felt as if someone were gently squeezing
my chest. I fell asleep and woke up, drenched in sweat, from a dream
of Carl trapped inside the Clamp. I sat up and checked the time. I
had been asleep for ten minutes.</p>
<p>I slept in half-hour snatches. At three
a.m., I decided this was intolerable. I had to do something. I began
inching the Contours off the bed. This guilt, I knew what it was.
More to the point, I knew where it was. It was in my brain&rsquo;s
ventromedial prefrontal cortex. And I was pretty sure I could take it
out.</p>
