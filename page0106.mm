<p>As soon as Lola
rang the doorbell, the yapping began. She had warned me that Angelica
had dogs: she hadn&rsquo;t mentioned how many. This was clearly four,
at a minimum. And they were small. I could tell.</p>
<p>The light flicked
on. I looked at Lola and she looked at me and I think we were both a
little shocked at how bad the other looked. Lola&rsquo;s eyes were
sunk into shadows. Her Better Future sweater was stained with road
dust. I had bled on her shoulder. We were not going to make a great
first impression.</p>
<p>The door opened. A
woman stood framed in the doorway, holding what I thought was a purse
until it barked. More yapping small dogs stood behind each of her
ankles. They were indeed wearing outfits: little red and green
tunics.</p>
<p>&ldquo;Angelica,&rdquo;
said Lola. &ldquo;I need your help.&rdquo;</p>
<p>&ldquo;Oh, God.&rdquo;
Angelica threw an arm around Lola and hugged her. Just one arm. The
other was still holding the dog. &ldquo;Come in!&rdquo;</p>
<p>She held open the
door and I followed Lola inside. As I stepped over the threshold, a
dog darted through my legs, and I almost stood on it. I mean, I had
to manually instruct the Contours to pull up. The dog was too small
to trigger the collision avoidance software. I thought: <em>That could
be a problem.</em></p>
