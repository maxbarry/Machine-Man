<p>Page 97.</p>
<p>I rocketed up the
side of the Better Future building. Smoked glass offices flashed past
my face. At the top of my arc through the air, I cleared a railing
with about two inches to spare, and landed on the balcony as gently
as if I was stepping out of my house. This would have been by far the
coolest thing I&rsquo;d ever done, if I hadn&rsquo;t just kicked a
Hummer into a building. But I barely even felt surprise. I had
realized something, down there on the lawn: I could trust my legs.</p>
<p>&ldquo;Charlie!&rdquo;
It was Lola, coming out of the suite. I had picked the right balcony;
of course I had. She threw herself into my arms. &ldquo;Did you feel
that, a second ago? Was that an earthquake?&rdquo;</p>
<p>&ldquo;No. That was
me.&rdquo;</p>
<p>She looked at me,
then leaned over the balcony. &ldquo;Did you jump up here? Did you
<em>jump</em>?&rdquo;</p>
<p>&ldquo;There have
been some developments. We need to get out of here.&rdquo;</p>
<p>&ldquo;What&rsquo;s
that smoke? Is that a car?&rdquo;</p>
<p>&ldquo;Lola.&rdquo;
I took hold of her shoulders. Her eyes expanded until they covered
the world. &ldquo;It&rsquo;s important we get out of here as fast as
possible.&rdquo;</p>
<p>She sucked in
breath. &ldquo;Okay.&rdquo;</p>
<p>Then I realized I
couldn&rsquo;t jump out of here. Or rather, I couldn&rsquo;t do it
with Lola. The moment my legs touched the ground and began to
decelerate the rest of me, Lola would weigh the equivalent of two
thousand pounds. She would be torn out of my arms.</p>
<p>Lola took my hand,
the biological one. &ldquo;I&rsquo;m ready.&rdquo;</p>
<p>Outside, I heard
the ding of the elevator. That would be security.</p>
<p>&ldquo;I knew you&rsquo;d
come back.&rdquo; She was shining again: looking at me like I was the
greatest thing in the world. &ldquo;I knew it.&rdquo;</p>
