<p>I don&rsquo;t want to sound weird, but don&rsquo;t you think there is
something seriously wrong with people? I mean, there is, obviously:
you hear these terrible stories where the father just snaps, and you
know that guy was broken. Something happened in his head, some
leakage, and what spilled out was beyond comprehension. Everyone
agrees on this. They ask if you heard about that man and those poor
boys, and say, <em>You just can&rsquo;t understand it, can you?</em></p>
<p>It&rsquo;s the look they give you then that bothers me. I am not a
great reader of people, so at first I thought I was misinterpreting.
But now I don&rsquo;t think so. <em>Can you?</em> I think it&rsquo;s a
real question. And when you shake your head and say, <em>No, it&rsquo;s
unbelievable</em>, they are trying to figure out what you really
think.</p>
<p>I have two theories. The first is they suspect you, too, may be
broken: that your brain contains fissures. That, under the right
circumstances, or the wrong ones, you might do something unbearable.
They want to know if they ought to take precautions.</p>
<p>The second is they think they might. And what they&rsquo;re looking
for is a sign they&rsquo;re not alone. Because sometimes, when
they&rsquo;re humiliated by their boss or the baby won&rsquo;t stop
howling, the red thoughts that burst inside their brain, they&rsquo;re
really horrific.</p>
<p>I
don&rsquo;t blame anyone. It&rsquo;s not a personal failing, in my
opinion, to feel violent when your brain floods with vasopressin.
That&rsquo;s just what happens. You drop a glass, it falls toward the
Earth; maybe that&rsquo;s not the outcome you want, but don&rsquo;t
blame the glass. Don&rsquo;t pass moral judgment because cause
produced effect. Because stimulus provoked response. That&rsquo;s
just how it works.</p>
<p>Still, I am disturbed by the idea that I&rsquo;m surrounded by
polite, smiling men and women one seratonin dip away from savagery.
I&rsquo;m not saying it&rsquo;s anyone&rsquo;s fault. I&rsquo;m just
saying I think that situation can be improved.</p>
