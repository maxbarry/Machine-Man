<p>I took a step
forward. My lips were pursing to form the beginning of a nonchalant
<em>What?</em> Lola&rsquo;s eyes shone with pride. Angelica was
watching my legs. And I stepped on a dog.</p>
<p>&ldquo;<em>Biggles!</em>&rdquo;
Angelica shrieked. I have never heard a more piercing sound, and I&rsquo;ve
worked in metals fabrication. She rushed forward, her fingers
stretched into claws. I looked down and saw a dog, Biggles, I guess,
howling from under the rubber toe of the pole. It was hard to know
which part of the dog was caught because it was wearing a smart blue
vest. I raised my leg, but I was snagged on the vest, and the pole
came down on the dog again. Then I had no balance, and could do
nothing but pivot. Angelica&rsquo;s shriek entered a whole new octave
of outrage. It possibly looked to her like I was deliberately
grinding on her dog.</p>
<p>She banged into me,
shoulder first. I hit the floor in a tangle of poles. When I levered
myself up on my elbows, Angelica was cradling Biggles in her arms and
crying. Biggles licked her face, whimpering.</p>
<p>&ldquo;They planned
this.&rdquo; I didn&rsquo;t quite mean to say this out loud. It would
have been smarter to keep my mouth shut. But it had suddenly occurred
to me: this was Dog&rsquo;s plan. The hive mind had finally figured
out how to drive me away. &ldquo;It&rsquo;s a set-up.&rdquo; I looked
around, because I knew four little faces would be watching this from
a doorway somewhere. &ldquo;Those little dogs set me up.&rdquo;</p>
<p>Then Angelica was
hitting me, for some reason. I tried to fend her off and was not very
successful. Her nails raked my face. Biggles bit me on the finger. On
all sides was yapping and shrieking and I couldn&rsquo;t tell who was
doing what. &ldquo;<em>Get out!</em>&rdquo; Angelica screamed. I curled
into a ball beneath the assault. &ldquo;<em>Get out get out get out!</em>&rdquo;</p>
<p>Eventually, Lola
convinced Angelica to stop hitting me. She did this by screaming at
Angelica while Angelica screamed at her and every dog in the house
barked and yowled. I once attended a demonstration of Better Future
sonics-based non-lethal weaponry, and what came out of that gun did
not sound as bad as this. It ended with Angelica scooping up as many
dogs as she could carry and running to the master bedroom. I
uncurled. My ears hurt.</p>
<p>Lola helped me into
a kitchen chair. &ldquo;That was an accident,&rdquo; I said.</p>
<p>&ldquo;I know.&rdquo;</p>
<p>&ldquo;I wouldn&rsquo;t
deliberately step on a dog.&rdquo;</p>
<p>&ldquo;I know,
Charlie.&rdquo; She inspected my maimed finger. 
</p>
<p>&ldquo;Ow,&rdquo; I
said. &ldquo;He can&rsquo;t be too hurt, if he can bite like that.&rdquo;</p>
<p>&ldquo;Shh,&rdquo;
said Lola.</p>
<p>I shushed. In the
silence, I could hear Angelica muttering to her dogs. &ldquo;We have
to go now, don&rsquo;t we?&rdquo;</p>
<p>&ldquo;I think we
do.&rdquo;</p>
<p>Angelica&rsquo;s
voice rose. It was oddly strident. I had never heard her talk to her
dogs like that before. &ldquo;Is Angelica on the phone?&rdquo;</p>
<p>We listened. I
could almost hear it already: the white van, the Better Future logo
on the side, the guards in back, speeding toward us.</p>
