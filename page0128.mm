<p>I took a step
forward. Angelica&rsquo;s eyes widened; Lola&rsquo;s shone with
pride. My lips pursed, beginning a nonchalant <em>What?</em> Then I
stepped on a dog.</p>
<p>&ldquo;<em>Biggles!</em>&rdquo;
I have never heard a more piercing sound, and I&rsquo;ve worked in
metals fabrication. Angelica rushed forward, her fingers hooked into
claws. I looked down and saw a dog, Biggles, I guess, trapped under
the rubber toe of the pole. Actually, mostly it was Biggles&rsquo;s
little blue vest trapped under my pole, but he was making a hell of a
racket, so maybe some of Biggles was caught there too. I tried to
raise my leg but snagged on his coat. Then I was off balance and
could do nothing but pivot. Angelica&rsquo;s shriek reached a whole
new pitch of outrage. Possibly it looked to her like I was
deliberately grinding on her dog.</p>
<p>She banged into me,
shoulder first. I hit the floor in a tangle of poles. When I levered
myself up on my elbows, Angelica was cradling Biggles in her arms and
crying. Biggles licked her face, whimpering.</p>
<p>Then it occurred to
me: they had planned this. Dog had finally figured out how to drive
me away. It had sent Biggles to throw himself under my poles. He was
a martyr. I looked around, because I knew furry little faces would be
watching this from a doorway somewhere. &ldquo;It&rsquo;s a set-up.&rdquo;
In retrospect, of course, I should have kept these thoughts to
myself. But at the time, it seemed important to expose the truth.
&ldquo;Those little dogs did this on purpose.&rdquo;</p>
<p>Then Angelica was
hitting me, for some reason. I tried to fend her off and was not very
successful. Her nails raked my cheek. Biggles bit me on the finger.
On all sides came yapping and shrieking. Dogs streamed out of the
walls to pile on top of me. &ldquo;<em>Get out!</em>&rdquo; Angelica
screamed. I curled into a ball beneath her assault. &ldquo;<em>Get
out, get out, get out!</em>&rdquo;</p>
<p>Eventually, Lola
convinced Angelica to stop hitting me. She did this by screaming at
Angelica while Angelica screamed at her and every dog in the house
barked and yowled. One time I attended a demonstration of Better
Future sonics-based non-lethal weaponry, and what came out of that
gun did not sound as bad as this. Angelica ran to the master bedroom,
Biggles in arms, trailed by little dogs. One looked back at me before
he disappeared, and I swear, he looked satisfied.</p>
<p>Lola helped me into
a kitchen chair. &ldquo;That was an accident,&rdquo; I said.</p>
<p>&ldquo;I know.&rdquo;</p>
<p>&ldquo;I wouldn&rsquo;t
deliberately step on a dog.&rdquo;</p>
<p>&ldquo;I know,
Charlie.&rdquo; She grimaced at my face. &ldquo;You&rsquo;re all
scratched up. Let me see that finger.&rdquo;</p>
<p>&ldquo;Ow,&rdquo; I
said. &ldquo;Biggles can&rsquo;t be too hurt, if he can bite like
that.&rdquo;</p>
<p>&ldquo;Shh,&rdquo;
said Lola.</p>
<p>I shushed. In the
silence, I could hear Angelica muttering to her dogs. &ldquo;She&rsquo;s
going to make us leave now, isn&rsquo;t she?&rdquo;</p>
<p>&ldquo;I think so,
Charlie.&rdquo;</p>
<p>I hesitated.
Angelica&rsquo;s voice was oddly strident. I had never heard her talk
to the dogs like that.</p>
<p>&ldquo;She doesn&rsquo;t
see you like I do,&rdquo; said Lola. &ldquo;Nobody does.&rdquo;</p>
<p>&ldquo;Wait,&rdquo;
I said. &ldquo;Is Angelica on the phone?&rdquo;</p>
<p>We listened. I
could almost hear it already: the white van, Better Future logo on
its side, coming for us.</p>
