<p>I came to see
Angelica&rsquo;s dogs as a hive mind. They were many, but one. They
communicated via telepathy. If I yelled at one in the bathroom, I
would emerge to find three more, trembling on the carpet with shared
outrage. Taken as individuals, these were some of the stupidest
mammals I had ever encountered. When I walked toward one, more often
than not it would freeze, wanting to flee but unable to figure out if
I would maintain my current trajectory or maybe start teleporting
around the room. As a collective, though, the dogs were plotting
something. I was sure of it. They wanted me out of the house and were
beginning to figure out methods. Any time I entered a room to
multiple sets of glittering eyes, I got nervous.</p>
<p>&ldquo;You&rsquo;re
so silly,&rdquo; said Lola, scratching a tiny brown head. &ldquo;They&rsquo;re
gorgeous. Aren&rsquo;t you, baby? Yes, you are.&rdquo;</p>
<p>&ldquo;They hate
me.&rdquo;</p>
<p>&ldquo;Try being
nicer to them.&rdquo; The dog began licking Lola&rsquo;s neck. She
shrieked and tried to push it away, but not very effectively. &ldquo;Stop
that! Bad puppy!&rdquo;</p>
<p>I noticed another
dog sitting on the arm of the sofa. It was staring at me, despite the
play-fight going on right in front of it. I got the feeling this dog
was sending me a message. Something like: <em>We have your girlfriend</em>.</p>
<p>My legs stepped
forward. The dog leaped off the sofa and scampered away. I smiled
despite myself. <em>Stop that</em>, I said. <em>Bad legs</em>.</p>
