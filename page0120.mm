<p>On the third day,
Lola came into my room. Until this, she had been leaving trays of
food outside. I had learned to wait until her footsteps receded, then
pull myself to the door and drag in the food before the dogs
descended. I would shut the door in their little faces and chew,
alone.</p>
<p>But this day she
opened the door. It was a small shock, seeing another human. She was
wearing a green blouse and an air of hopelessness. As she stared at
me, her eyes glimmered.</p>
<p>I was sitting in
the middle of the carpet. Arranged around me in concentric circles
were pieces of my legs and left hand. It was as if I had suffered the
world&rsquo;s neatest explosion. Which I had. The electromagnetic
pulse that had come out of Lola had hurt no-one, disturbed nothing
that wasn&rsquo;t electronic. It had only fried half the circuits in
a half-block radius. More specifically, it had fried all the delicate
ones.</p>
<p>&ldquo;I want...&rdquo;
said Lola. &ldquo;I think...&rdquo;</p>
<p>This arrangement,
it didn&rsquo;t mean I was fixing the Contours. It might have looked
that way. In truth, I had disassembled them because I couldn&rsquo;t
do anything else. I was trying to break my problem into pieces until
it was small enough to handle. I had no real faith that I could reach
that point. But that was how you solved anything: you divided it. 
</p>
<p>&ldquo;I think it
was really unfair of you to say this is my fault.&rdquo;</p>
<p>&ldquo;It&rsquo;s
not your fault.&rdquo; I did not look at Lola as I said this, because
I did not really believe it.</p>
<p>&ldquo;They put it
in me,&rdquo; she said. &ldquo;Without even <em>telling</em> me.&rdquo;</p>
<p>&ldquo;You could
have calmed down.&rdquo;</p>
<p>Lola inhaled in a
hiss. 
</p>
