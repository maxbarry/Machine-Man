<h5>CHAPTER 
FOURTEEN</h5>
<p>I sat silently in
Cassandra Cautery&rsquo;s office while she talked about processes and
due diligence and ticking boxes. She was telling me why she did not
want me to have surgery on Friday. There had maybe been an alarming
expression on my face right after she said, <em>Imagine they could</em>,
because since then she had been speaking very quickly, and of all the
reasons she offered for keeping me out of the operating theater, none
of them was: <em>Because we&rsquo;re doing Carl</em>.</p>
<p>Maybe they had
already done Carl. Maybe he was recovering in that medical room
they&rsquo;d given Lola, before they sent her upstairs to the suite.
Maybe some of my legs were missing.</p>
<p>Silence. Cassandra
Cautery had paused. I mentally backtracked: her last word had been
something like <em>eventually</em>.</p>
<p>&ldquo;Okay,&rdquo;
I said. &ldquo;I understand.&rdquo;</p>
<p>She blinked. Her
forehead didn&rsquo;t move, though. &ldquo;That&rsquo;s...&rdquo; she
said. &ldquo;That&rsquo;s good to hear, Charlie. I really appreciate
your attitude.&rdquo;</p>
<p>I nodded. I wasn&rsquo;t
an idiot. Letting Cassandra Cautery know how much I cared about
swapping out my arm would make things difficult. I had been locked in
a room, not long ago. And while I&rsquo;d been in there, I vowed
never to let myself be put in that position again. It was better if
everyone believed I was with the program. Planning no more
amputations. I wouldn&rsquo;t get to use that operating theater. But
that hadn&rsquo;t stopped me before.</p>
