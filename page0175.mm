<p>As I looked at
Lola, I felt a bubble of grief rising within me. Because she seemed
very lovely, standing here telling me to shed myself: she was a girl
who loved me and they did not come along every day; they had not come
along in my lifetime prior to this. &ldquo;Lola,&rdquo; I said, and
my parts said: <em>Shoot her</em>. Which kind of proved Lola&rsquo;s
point, and the feeling of grief sharpened, and I realized it wasn&rsquo;t
for her: it was for them. &ldquo;I love you,&rdquo; I said, and I
wasn&rsquo;t sure who I was talking to, but my parts said: <em>I love
you too</em>.</p>
<p>Then my arm died. I
gasped and immediately retracted everything bad I had ever thought
about my parts. I wailed: <em>Come back come back!</em> I tried to flex
the arm and it moved, and that was when I realized it was not dead.
It was an undoing of the tingling I had felt on my way here. The gun
was offline.</p>
<p>Beams of light slid
across the rubble. A Hummer edged toward us, stopped. Doors slammed.
It was too bright; I couldn&rsquo;t see anything. But Cassandra
Cautery&rsquo;s voice rang out of the night: &ldquo;Oh, Charlie.
Thank God you&rsquo;re all right.&rdquo; And I recalled the speaker,
the microphone in my chest.</p>
