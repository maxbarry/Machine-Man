<p>I gasped and
mentally retracted everything bad I had ever thought about my parts.
I wailed: <em>Come back, come back!</em> I tried to flex the arm and it
moved, and that was when I realized it was not dead at all. What I
had felt was an undoing of the tingling that had occurred earlier, on
my way here. I had felt the gun going offline.</p>
<p>Twin beams of light
slid through the air, illuminating glittering columns of concrete
dust. Doors thumped. I heard Cassandra Cautery say: &ldquo;Charlie.
Thank God you&rsquo;re okay.&rdquo; And I recalled the speaker, the
microphone in my chest.</p>
