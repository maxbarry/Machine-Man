<p>As the Contours
took me through the corridors, I ate my muffin and tried to figure
out how to replace my own arm. There was an obvious logistical
problem: once I removed my meat arm, that left me with just one, and
I wasn&rsquo;t sure that was enough to attach the metal one. Which
made it a frustrating little logic puzzle, like having to transport a
fox, chicken, and bag of seed on a boat that could only bear two of
them. If I <em>had</em> a mechanical arm, there would be no problem: I
could program it for a slice and dice, sedate myself, and wake to a
finished job. Well, that&rsquo;s an exaggeration. I wouldn&rsquo;t be
comfortable trusting it that far. Not to the point of doing surgery
on me while I slept. But still. It was like needing a ladder that&rsquo;s
on the roof.</p>
<p>I reached the
elevator, swiped my pass, and pressed the button for sub-floor level
three. The panel emitted a regretful tone. The screen read: CONTACT
MANAGEMENT.</p>
<p>&ldquo;Hmm,&rdquo;
I said. I pressed the button again, just in case. But no. I did not
have access.</p>
<p>Stymied. It wasn&rsquo;t
a total surprise. But it made certain things clear.</p>
<p>I stepped out of
the elevator and chewed my lip. After a minute, I decided two things.
First, I needed Lola to help me with this arm. Second, it would be
smart to go to the Repository, collect all my parts, and never let
them out of my sight again.</p>
